# BUILD-06: Sprite animation from frames
# ---------------------------------------------------------------------------


def generate_sprite_animation_script(
    clip_name: str,
    sprite_folder: str,
    frame_rate: int = 12,
    loop: bool = True,
    output_path: str = "",
) -> str:
    """Generate a C# editor script that creates an AnimationClip from sprite frames.

    Loads all sprites from a folder, sorts them alphabetically (frame order),
    and creates an AnimationClip with ObjectReferenceKeyframes bound to
    SpriteRenderer.m_Sprite.

    Args:
        clip_name: Name of the animation clip.
        sprite_folder: Unity folder containing individual frame sprites.
        frame_rate: Frames per second for the animation.
        loop: Whether the animation should loop.
        output_path: Output path for the .anim asset. Auto-derived if empty.

    Returns:
        Complete C# editor script source.
    """
    safe_name = _sanitize_cs_identifier(clip_name)
    safe_folder = _sanitize_cs_string(sprite_folder)

    if not output_path:
        output_path = f"Assets/Art/Animations/{_sanitize_cs_identifier(clip_name)}.anim"
    safe_output = _sanitize_cs_string(output_path)

    wrap_mode = "WrapMode.Loop" if loop else "WrapMode.Once"
    loop_str = "true" if loop else "false"

    return f'''using UnityEditor;
using UnityEngine;
using System.IO;
using System.Linq;

public class VeilBreakers_Create{safe_name}Anim
{{
    [MenuItem("VeilBreakers/Assets/Create {safe_name} Animation")]
    public static void Execute()
    {{
        // Find all sprites in the folder
        string[] guids = AssetDatabase.FindAssets("t:Sprite", new[] {{ "{safe_folder}" }});

        if (guids.Length == 0)
        {{
            Debug.LogError("No sprites found in: {safe_folder}");
            return;
        }}

        // Load and sort sprites by name (alphabetical = frame order)
        var sprites = guids
            .Select(g => AssetDatabase.GUIDToAssetPath(g))
            .OrderBy(p => p)
            .Select(p => AssetDatabase.LoadAssetAtPath<Sprite>(p))
            .Where(s => s != null)
            .ToArray();

        if (sprites.Length == 0)
        {{
            Debug.LogError("Failed to load sprites from: {safe_folder}");
            return;
        }}

        // Create animation clip
        var clip = new AnimationClip();
        clip.frameRate = {frame_rate};
        clip.name = "{safe_name}";

        // Create keyframes
        var keyframes = new ObjectReferenceKeyframe[sprites.Length];
        for (int i = 0; i < sprites.Length; i++)
        {{
            keyframes[i] = new ObjectReferenceKeyframe
            {{
                time = (float)i / {frame_rate}f,
                value = sprites[i],
            }};
        }}

        // Bind to SpriteRenderer.m_Sprite
        var binding = EditorCurveBinding.PPtrCurve(
            "",
            typeof(SpriteRenderer),
            "m_Sprite"
        );
        AnimationUtility.SetObjectReferenceCurve(clip, binding, keyframes);

        // Set loop / wrap mode
        var settings = AnimationUtility.GetAnimationClipSettings(clip);
        settings.loopTime = {loop_str};
        AnimationUtility.SetAnimationClipSettings(clip, settings);
        clip.wrapMode = {wrap_mode};

        // Ensure output directory exists
        string outputDir = Path.GetDirectoryName("{safe_output}");
        if (!string.IsNullOrEmpty(outputDir) && !AssetDatabase.IsValidFolder(outputDir))
        {{
            string[] parts = outputDir.Split('/');
            string current = parts[0];
            for (int i = 1; i < parts.Length; i++)
            {{
                string next = current + "/" + parts[i];
                if (!AssetDatabase.IsValidFolder(next))
                    AssetDatabase.CreateFolder(current, parts[i]);
                current = next;
            }}
        }}

        // Save clip
        AssetDatabase.CreateAsset(clip, "{safe_output}");
        AssetDatabase.SaveAssets();
        AssetDatabase.Refresh();

        // Write result
        string json = "{{\\"status\\": \\"success\\", \\"clip_path\\": \\"" + "{safe_output}".Replace("\\\\", "/") + "\\", \\"frame_count\\": " + sprites.Length + ", \\"frame_rate\\": {frame_rate}, \\"loop\\": {loop_str}}}";
        File.WriteAllText(
            Path.Combine(Application.dataPath, "../Temp/vb_result.json"),
            json
        );

        Debug.Log($"AnimationClip created: {safe_output} ({{sprites.Length}} frames at {frame_rate} fps)");
    }}
}}
'''


