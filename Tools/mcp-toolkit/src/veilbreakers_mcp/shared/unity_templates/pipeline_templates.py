# IMP-04: Normal map baking (Blender Python)
# ---------------------------------------------------------------------------


def generate_normal_map_bake_script(
    high_poly_name: str,
    low_poly_name: str,
    output_image_name: str = "normal_map",
    image_size: int = 2048,
    cage_extrusion: float = 0.1,
    samples: int = 16,
    normal_space: str = "TANGENT",
) -> str:
    """Generate Blender Python code for normal map baking from high to low poly.

    Produces a script that:
    - Selects the low poly object and sets it as active
    - Creates a bake target image
    - Generates a cage mesh from the low poly
    - Sets up material node tree with Image Texture node for baking
    - Configures Cycles render engine and bake settings
    - Executes the bake and saves the result

    This returns Blender Python (bpy), NOT C# -- it is meant to be executed
    via the blender_execute tool.

    Args:
        high_poly_name: Name of the high-poly source object in Blender.
        low_poly_name: Name of the low-poly target object in Blender.
        output_image_name: Name for the baked image (without extension).
        image_size: Resolution of the baked image (width and height).
        cage_extrusion: Distance to extrude cage mesh normals outward.
        samples: Number of Cycles render samples for baking.
        normal_space: Normal map space -- "TANGENT" or "OBJECT".

    Returns:
        Blender Python code string.
    """
    # Validate normal_space
    if normal_space not in ("TANGENT", "OBJECT"):
        normal_space = "TANGENT"

    # Escape user-supplied names for safe embedding in Blender Python strings
    def _escape_bpy_string(s: str) -> str:
        return (
            s.replace("\\", "\\\\")
            .replace("'", "\\'")
            .replace('"', '\\"')
            .replace("\n", "\\n")
        )

    safe_high = _escape_bpy_string(high_poly_name)
    safe_low = _escape_bpy_string(low_poly_name)
    safe_img = _escape_bpy_string(output_image_name)

    script = f'''import bpy
import mathutils

# -----------------------------------------------
# Normal Map Baking: {safe_high} -> {safe_low}
# Generated by VeilBreakers MCP Toolkit
# -----------------------------------------------

# Configuration
HIGH_POLY = "{safe_high}"
LOW_POLY = "{safe_low}"
IMAGE_NAME = "{safe_img}"
IMAGE_SIZE = {image_size}
CAGE_EXTRUSION = {cage_extrusion}
SAMPLES = {samples}
NORMAL_SPACE = "{normal_space}"

# Ensure objects exist
high_obj = bpy.data.objects.get(HIGH_POLY)
low_obj = bpy.data.objects.get(LOW_POLY)

if high_obj is None:
    raise ValueError(f"High-poly object '{{HIGH_POLY}}' not found")
if low_obj is None:
    raise ValueError(f"Low-poly object '{{LOW_POLY}}' not found")

# Set render engine to Cycles (required for baking)
bpy.context.scene.render.engine = "CYCLES"
bpy.context.scene.cycles.samples = SAMPLES

# Create bake target image
if IMAGE_NAME in bpy.data.images:
    bpy.data.images.remove(bpy.data.images[IMAGE_NAME])

bake_image = bpy.data.images.new(
    IMAGE_NAME,
    width=IMAGE_SIZE,
    height=IMAGE_SIZE,
    alpha=False,
    float_buffer=True,
)
bake_image.colorspace_settings.name = "Non-Color"

# Generate cage mesh from low poly
cage_name = LOW_POLY + "_cage"
if cage_name in bpy.data.objects:
    old_cage = bpy.data.objects[cage_name]
    bpy.data.objects.remove(old_cage, do_unlink=True)

# Duplicate low poly to create cage
bpy.ops.object.select_all(action="DESELECT")
low_obj.select_set(True)
bpy.context.view_layer.objects.active = low_obj
bpy.ops.object.duplicate()
cage_obj = bpy.context.active_object
cage_obj.name = cage_name

# Extrude cage along normals
cage_mesh = cage_obj.data
for vertex in cage_mesh.vertices:
    vertex.co += vertex.normal * CAGE_EXTRUSION

# Ensure low poly has a material with an Image Texture node for baking
if len(low_obj.data.materials) == 0:
    mat = bpy.data.materials.new(name=LOW_POLY + "_BakeMat")
    mat.use_nodes = True
    low_obj.data.materials.append(mat)

# Set up Image Texture node on each material of low poly
for mat_slot in low_obj.material_slots:
    mat = mat_slot.material
    if mat is None:
        continue
    mat.use_nodes = True
    nodes = mat.node_tree.nodes

    # Create or find Image Texture node for bake target
    bake_node = None
    for node in nodes:
        if node.type == "TEX_IMAGE" and node.image == bake_image:
            bake_node = node
            break

    if bake_node is None:
        bake_node = nodes.new(type="ShaderNodeTexImage")
        bake_node.name = "BakeTarget"
        bake_node.location = (-300, 300)

    bake_node.image = bake_image

    # Set as active node (Blender bakes to the active image texture node)
    nodes.active = bake_node

# Set up bake configuration
bpy.context.scene.render.bake.use_selected_to_active = True
bpy.context.scene.render.bake.cage_extrusion = CAGE_EXTRUSION
bpy.context.scene.render.bake.normal_space = NORMAL_SPACE

# Select high poly, make low poly active
bpy.ops.object.select_all(action="DESELECT")
high_obj.select_set(True)
low_obj.select_set(True)
bpy.context.view_layer.objects.active = low_obj

# Execute bake
bpy.ops.object.bake(type="NORMAL")

# Save baked image
output_path = "//" + IMAGE_NAME + ".exr"
bake_image.filepath_raw = output_path
bake_image.file_format = "OPEN_EXR"
bake_image.save()

# Clean up cage object
cage_ref = bpy.data.objects.get(cage_name)
if cage_ref:
    bpy.data.objects.remove(cage_ref, do_unlink=True)

# Result
result = {{
    "status": "success",
    "output_path": output_path,
    "image_size": IMAGE_SIZE,
    "bake_settings": {{
        "normal_space": NORMAL_SPACE,
        "cage_extrusion": CAGE_EXTRUSION,
        "samples": SAMPLES,
        "use_selected_to_active": True,
    }},
}}
print(result)
'''
    return script


