"""Asset pipeline template generators for Unity project setup and workflow automation.

Covers Git LFS configuration, normal map baking workflow, sprite atlas creation,
sprite animation, Sprite Editor configuration, and AssetPostprocessor scripts
for automated import pipelines.

Exports:
    generate_gitlfs_config              -- IMP-03: .gitattributes for Unity LFS tracking
    generate_gitignore                  -- IMP-03: .gitignore for Unity projects
    generate_normal_map_bake_script     -- IMP-04: Blender Python for high-to-low normal bake
    generate_sprite_atlas_script        -- BUILD-06: C# editor script for SpriteAtlas creation
    generate_sprite_animation_script    -- BUILD-06: C# editor script for sprite AnimationClip
    generate_sprite_editor_config_script -- TWO-03: C# editor script for Sprite Editor config
    generate_asset_postprocessor_script -- PIPE-08: C# AssetPostprocessor with folder rules

Helpers:
    _sanitize_cs_string                 -- C# string literal escaping
    _sanitize_cs_identifier             -- C# identifier sanitization
"""

from __future__ import annotations

import re


# ---------------------------------------------------------------------------
# Sanitization helpers (each template module has its own copy)
# ---------------------------------------------------------------------------


def _sanitize_cs_string(value: str) -> str:
    """Escape a value for safe embedding inside a C# string literal.

    Prevents C# code injection by escaping backslashes, quotes, and
    newlines.
    """
    value = value.replace("\\", "\\\\")
    value = value.replace('"', '\\"')
    value = value.replace("\n", "\\n")
    value = value.replace("\r", "\\r")
    return value


def _sanitize_cs_identifier(value: str) -> str:
    """Sanitize a value for use as a C# identifier (class name, method name)."""
    return re.sub(r"[^a-zA-Z0-9_]", "", value)


# C# reserved words (needed by _safe_namespace)
_CS_RESERVED = frozenset({
    "abstract", "as", "base", "bool", "break", "byte", "case", "catch", "char",
    "checked", "class", "const", "continue", "decimal", "default", "delegate",
    "do", "double", "else", "enum", "event", "explicit", "extern", "false",
    "finally", "fixed", "float", "for", "foreach", "goto", "if", "implicit",
    "in", "int", "interface", "internal", "is", "lock", "long", "namespace",
    "new", "null", "object", "operator", "out", "override", "params", "private",
    "protected", "public", "readonly", "ref", "return", "sbyte", "sealed",
    "short", "sizeof", "stackalloc", "static", "string", "struct", "switch",
    "this", "throw", "true", "try", "typeof", "uint", "ulong", "unchecked",
    "unsafe", "ushort", "using", "virtual", "void", "volatile", "while",
})


def _safe_namespace(ns: str) -> str:
    """Sanitize a C# namespace to prevent code injection."""
    sanitized = re.sub(r"[^a-zA-Z0-9_.]", "", ns)
    sanitized = re.sub(r"\.{2,}", ".", sanitized).strip(".")
    if not sanitized:
        return "Generated"
    segments = sanitized.split(".")
    fixed: list[str] = []
    for seg in segments:
        if not seg:
            continue
        if seg[0].isdigit():
            seg = f"_{seg}"
        if seg in _CS_RESERVED:
            seg = f"@{seg}"
        fixed.append(seg)
    return ".".join(fixed) if fixed else "Generated"


# ---------------------------------------------------------------------------
# LFS extension categories
# ---------------------------------------------------------------------------

_LFS_CATEGORIES: list[tuple[str, list[str]]] = [
    ("3D Models", ["fbx", "obj", "blend"]),
    ("Textures", ["png", "psd", "tga", "tif", "exr", "hdr", "jpg"]),
    ("Audio", ["wav", "mp3", "ogg"]),
    ("Fonts", ["ttf", "otf"]),
    ("Video", ["mp4", "mov"]),
    ("Unity binary", ["cubemap", "unitypackage"]),
]

_UNITY_YAML_TYPES: list[str] = [
    "unity", "prefab", "mat", "anim", "controller",
]


# ---------------------------------------------------------------------------
# IMP-03: Git LFS configuration
# ---------------------------------------------------------------------------


def generate_gitlfs_config(
    extra_extensions: list[str] | None = None,
    include_unity_yaml_merge: bool = True,
) -> str:
    """Generate a .gitattributes file content string for a Unity project.

    Tracks binary assets with Git LFS and optionally configures Unity YAML
    merge strategy for scene/prefab/material files.

    NOTE: .asset files are NOT tracked with LFS because Unity's Force Text
    serialization mode stores them as YAML text.

    Args:
        extra_extensions: Additional file extensions to track with LFS
            (with or without leading dot, e.g. [".custom", "dat"]).
        include_unity_yaml_merge: Whether to include Unity YAML merge
            strategy rules for *.unity, *.prefab, *.mat, etc.

    Returns:
        Complete .gitattributes file content.
    """
    lines: list[str] = []
    lines.append("# Generated by VeilBreakers MCP Toolkit")
    lines.append("# Git LFS tracking rules for Unity projects")
    lines.append("")

    # LFS tracking rules by category
    for category, extensions in _LFS_CATEGORIES:
        lines.append(f"# {category}")
        for ext in extensions:
            lines.append(f"*.{ext} filter=lfs diff=lfs merge=lfs -text")
        lines.append("")

    # Extra extensions
    if extra_extensions:
        lines.append("# Custom extensions")
        for ext in extra_extensions:
            ext = ext.lstrip(".")
            lines.append(f"*.{ext} filter=lfs diff=lfs merge=lfs -text")
        lines.append("")

    # Unity YAML merge strategy
    if include_unity_yaml_merge:
        lines.append("# Unity YAML merge strategy")
        for yaml_type in _UNITY_YAML_TYPES:
            lines.append(f"*.{yaml_type} merge=unityyamlmerge eol=lf")
        lines.append("")

    return "\n".join(lines)


