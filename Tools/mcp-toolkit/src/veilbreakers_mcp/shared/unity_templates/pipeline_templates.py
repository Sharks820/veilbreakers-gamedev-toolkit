def generate_sprite_editor_config_script(
    sprite_path: str,
    pivot: tuple[float, float] | None = None,
    border: tuple[int, int, int, int] | None = None,
    pixels_per_unit: int = 100,
    sprite_mode: int = 1,
    custom_physics_shape: list[list[tuple[float, float]]] | None = None,
) -> str:
    """Generate a C# editor script that configures Sprite Editor features.

    Applies TextureImporter settings for pivot, 9-slice border, pixels per unit,
    sprite mode, and custom physics shapes.

    Args:
        sprite_path: Path to the sprite asset (e.g. "Assets/Art/Sprites/icon.png").
        pivot: Custom pivot point as (x, y) normalized 0-1. None for default center.
        border: 9-slice border as (left, bottom, right, top). None to skip.
        pixels_per_unit: Sprite pixels per unit.
        sprite_mode: 0=none, 1=single, 2=multiple.
        custom_physics_shape: List of polygon outlines for physics shapes.
            Each outline is a list of (x, y) tuples.

    Returns:
        Complete C# editor script source.
    """
    safe_path = _sanitize_cs_string(sprite_path)
    safe_class = _sanitize_cs_identifier(
        sprite_path.rsplit("/", 1)[-1].rsplit(".", 1)[0]
    )

    # Alignment: 0=Center, 9=Custom when pivot provided
    alignment = 9 if pivot is not None else 0

    # Build pivot code
    pivot_code = ""
    if pivot is not None:
        pivot_code = f"""
        // Set custom pivot
        importer.spritePivot = new Vector2({pivot[0]}f, {pivot[1]}f);
        importer.spriteAlignment = {alignment};"""

    # Build border code
    border_code = ""
    if border is not None:
        border_code = f"""
        // Set 9-slice border (left, bottom, right, top)
        importer.spriteBorder = new Vector4({border[0]}f, {border[1]}f, {border[2]}f, {border[3]}f);"""

    # Build physics shape code
    physics_code = ""
    if custom_physics_shape is not None:
        shape_entries: list[str] = []
        for outline_idx, outline in enumerate(custom_physics_shape):
            points = ", ".join(
                f"new Vector2({x}f, {y}f)" for x, y in outline
            )
            shape_entries.append(
                f"            importer.SetPhysicsShape(0, {outline_idx}, "
                f"new[] {{ {points} }});"
            )
        physics_code = "\n        // Set custom physics shapes\n" + "\n".join(
            shape_entries
        )

    return f'''using UnityEditor;
using UnityEngine;
using System.IO;

public class VeilBreakers_ConfigSprite{safe_class}
{{
    [MenuItem("VeilBreakers/Assets/Configure Sprite {safe_class}")]
    public static void Execute()
    {{
        // Load TextureImporter
        var importer = AssetImporter.GetAtPath("{safe_path}") as TextureImporter;
        if (importer == null)
        {{
            Debug.LogError("TextureImporter not found for: {safe_path}");
            return;
        }}

        // Configure sprite settings
        importer.textureType = TextureImporterType.Sprite;
        importer.spriteImportMode = (SpriteImportMode){sprite_mode};
        importer.spritePixelsPerUnit = {pixels_per_unit};
{pivot_code}
{border_code}
{physics_code}

        // Apply and reimport
        importer.SaveAndReimport();

        // Write result
        string json = "{{\\"status\\": \\"success\\", \\"sprite_path\\": \\"" + "{safe_path}".Replace("\\\\", "/") + "\\", \\"pixels_per_unit\\": {pixels_per_unit}, \\"sprite_mode\\": {sprite_mode}}}";
        File.WriteAllText(
            Path.Combine(Application.dataPath, "../Temp/vb_result.json"),
            json
        );

        Debug.Log("Sprite configured: {safe_path}");
    }}
}}
'''


