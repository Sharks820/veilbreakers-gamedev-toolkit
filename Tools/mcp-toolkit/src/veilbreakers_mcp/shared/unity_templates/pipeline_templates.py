def _build_preprocess_method(
    method_name: str,
    importer_type: str,
    rules: list[dict] | None,
    setting_map: dict[str, tuple[str, callable]],
) -> str:
    """Build a single OnPreprocess* method body from folder rules.

    Args:
        method_name: C# method name (e.g. "OnPreprocessTexture").
        importer_type: C# importer cast type (e.g. "TextureImporter").
        rules: List of rule dicts with folder_pattern and settings_dict.
        setting_map: Mapping from setting names to (property, formatter).

    Returns:
        Indented C# method block.
    """
    if not rules:
        # Still generate the method stub for completeness
        return f"""        void {method_name}()
        {{
            // No rules configured for {importer_type}
        }}
"""

    lines: list[str] = []
    lines.append(f"        void {method_name}()")
    lines.append("        {")
    lines.append(f"            var importer = ({importer_type})assetImporter;")
    lines.append("")

    first = True
    for rule in rules:
        folder_pattern = rule.get("folder_pattern", "")
        settings = rule.get("settings_dict", {})
        safe_pattern = _sanitize_cs_string(folder_pattern)

        keyword = "if" if first else "else if"
        lines.append(
            f'            {keyword} (assetPath.Contains("{safe_pattern}"))'
        )
        lines.append("            {")
        lines.append(f'                // Rules for "{safe_pattern}" folder')

        for setting_name, setting_value in settings.items():
            if setting_name in setting_map:
                prop, formatter = setting_map[setting_name]
                cs_value = formatter(setting_value)
                lines.append(f"                {prop} = {cs_value};")

        lines.append("            }")
        first = False

    lines.append("        }")
    lines.append("")

    return "\n".join(lines)
