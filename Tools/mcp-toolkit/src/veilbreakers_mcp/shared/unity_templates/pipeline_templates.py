def generate_asset_postprocessor_script(
    processor_name: str,
    version: int = 1,
    texture_rules: list[dict] | None = None,
    model_rules: list[dict] | None = None,
    audio_rules: list[dict] | None = None,
    namespace: str = "VeilBreakers.Editor",
) -> str:
    """Generate a C# AssetPostprocessor script with folder-convention-based rules.

    Creates a postprocessor that auto-configures import settings based on
    folder patterns. Uses OnPreprocess callbacks (NOT OnPostprocess) to avoid
    infinite reimport loops. Includes GetVersion() override for reimport
    detection.

    Args:
        processor_name: Class name for the postprocessor.
        version: Postprocessor version number (increment when changing logic).
        texture_rules: List of dicts with 'folder_pattern' and 'settings_dict'
            for texture import rules. Settings can include: textureType,
            maxTextureSize, textureCompression, sRGBTexture, mipmapEnabled,
            spritePixelsPerUnit, filterMode.
        model_rules: List of dicts with 'folder_pattern' and 'settings_dict'
            for model import rules. Settings can include: importMaterials,
            importAnimation, animationType, meshCompression, globalScale.
        audio_rules: List of dicts with 'folder_pattern' and 'settings_dict'
            for audio import rules. Settings can include: forceToMono,
            loadInBackground, compressionFormat.
        namespace: C# namespace for the generated class.

    Returns:
        Complete C# AssetPostprocessor script source.
    """
    safe_name = _sanitize_cs_identifier(processor_name)
    safe_ns = _sanitize_cs_identifier(namespace.replace(".", "_"))
    clean_ns = _safe_namespace(namespace)

    # Build texture rules code
    texture_method = _build_preprocess_method(
        "OnPreprocessTexture",
        "TextureImporter",
        texture_rules,
        _TEXTURE_SETTING_MAP,
    )

    # Build model rules code
    model_method = _build_preprocess_method(
        "OnPreprocessModel",
        "ModelImporter",
        model_rules,
        _MODEL_SETTING_MAP,
    )

    # Build audio rules code
    audio_method = _build_preprocess_method(
        "OnPreprocessAudio",
        "AudioImporter",
        audio_rules,
        _AUDIO_SETTING_MAP,
    )

    return f'''using UnityEditor;
using UnityEngine;

namespace {clean_ns}
{{
    /// <summary>
    /// Auto-configures import settings based on folder conventions.
    /// Generated by VeilBreakers MCP Toolkit.
    /// </summary>
    public class {safe_name} : AssetPostprocessor
    {{
        /// <summary>
        /// Version number for reimport detection. Increment when changing
        /// processor logic to force re-processing of already-imported assets.
        /// </summary>
        public override uint GetVersion() {{ return {version}; }}

{texture_method}
{model_method}
{audio_method}
    }}
}}
'''


