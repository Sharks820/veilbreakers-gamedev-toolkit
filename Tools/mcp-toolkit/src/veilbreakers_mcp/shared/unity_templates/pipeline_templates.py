def generate_sprite_atlas_script(
    atlas_name: str,
    source_folder: str,
    output_path: str = "",
    padding: int = 4,
    enable_tight_packing: bool = True,
    enable_rotation: bool = False,
    max_texture_size: int = 4096,
    srgb: bool = True,
    filter_mode: str = "Bilinear",
    include_in_build: bool = True,
) -> str:
    """Generate a C# editor script that creates a SpriteAtlas asset.

    Uses the V1 SpriteAtlas API (.spriteatlas extension) for stable
    programmatic creation. Configures packing settings, texture settings,
    platform settings, and adds the source folder as a packable object.

    Args:
        atlas_name: Name of the atlas (used for class name and asset name).
        source_folder: Unity folder containing sprites (e.g. "Assets/Art/Sprites/UI").
        output_path: Output path for the .spriteatlas asset. Auto-derived if empty.
        padding: Padding between packed sprites in pixels.
        enable_tight_packing: Whether to enable tight mesh packing.
        enable_rotation: Whether to allow sprite rotation during packing.
        max_texture_size: Maximum texture size for the atlas.
        srgb: Whether the atlas texture uses sRGB color space.
        filter_mode: Texture filter mode ("Bilinear", "Point", or "Trilinear").
        include_in_build: Whether to include the atlas in builds.

    Returns:
        Complete C# editor script source.
    """
    safe_name = _sanitize_cs_identifier(atlas_name)
    safe_folder = _sanitize_cs_string(source_folder)

    if not output_path:
        output_path = f"Assets/Art/Atlases/{_sanitize_cs_identifier(atlas_name)}.spriteatlas"
    safe_output = _sanitize_cs_string(output_path)

    # Ensure .spriteatlas extension (V1 API)
    if not safe_output.endswith(".spriteatlas"):
        safe_output = safe_output.rsplit(".", 1)[0] + ".spriteatlas"

    tight_str = "true" if enable_tight_packing else "false"
    rotation_str = "true" if enable_rotation else "false"
    srgb_str = "true" if srgb else "false"
    include_str = "true" if include_in_build else "false"

    # Map filter mode string to Unity enum
    filter_map = {
        "Point": "FilterMode.Point",
        "Bilinear": "FilterMode.Bilinear",
        "Trilinear": "FilterMode.Trilinear",
    }
    filter_enum = filter_map.get(filter_mode, "FilterMode.Bilinear")

    return f'''using UnityEditor;
using UnityEditor.U2D;
using UnityEngine;
using UnityEngine.U2D;
using System.IO;

public class VeilBreakers_Create{safe_name}Atlas
{{
    [MenuItem("VeilBreakers/Assets/Create {safe_name} Atlas")]
    public static void Execute()
    {{
        // Ensure output directory exists
        string outputDir = Path.GetDirectoryName("{safe_output}");
        if (!string.IsNullOrEmpty(outputDir) && !AssetDatabase.IsValidFolder(outputDir))
        {{
            string[] parts = outputDir.Split('/');
            string current = parts[0];
            for (int i = 1; i < parts.Length; i++)
            {{
                string next = current + "/" + parts[i];
                if (!AssetDatabase.IsValidFolder(next))
                    AssetDatabase.CreateFolder(current, parts[i]);
                current = next;
            }}
        }}

        // Create atlas (V1 API)
        var atlas = new SpriteAtlas();

        // Configure packing settings
        var packSettings = new SpriteAtlasPackingSettings
        {{
            padding = {padding},
            enableTightPacking = {tight_str},
            enableRotation = {rotation_str},
        }};
        atlas.SetPackingSettings(packSettings);

        // Configure texture settings
        var texSettings = new SpriteAtlasTextureSettings
        {{
            sRGB = {srgb_str},
            filterMode = {filter_enum},
        }};
        atlas.SetTextureSettings(texSettings);

        // Configure platform settings
        var platSettings = atlas.GetPlatformSettings("DefaultTexturePlatform");
        platSettings.overridden = true;
        platSettings.maxTextureSize = {max_texture_size};
        atlas.SetPlatformSettings(platSettings);

        // Add source folder as packable
        var folder = AssetDatabase.LoadAssetAtPath<Object>("{safe_folder}");
        if (folder != null)
        {{
            atlas.Add(new[] {{ folder }});
        }}
        else
        {{
            Debug.LogWarning("Source folder not found: {safe_folder}");
        }}

        // Set include in build
        atlas.SetIncludeInBuild({include_str});

        // Save atlas
        AssetDatabase.CreateAsset(atlas, "{safe_output}");
        AssetDatabase.SaveAssets();
        AssetDatabase.Refresh();

        // Write result
        string json = "{{\\"status\\": \\"success\\", \\"atlas_path\\": \\"" + "{safe_output}".Replace("\\\\", "/") + "\\", \\"source\\": \\"" + "{safe_folder}".Replace("\\\\", "/") + "\\"}}";
        File.WriteAllText(
            Path.Combine(Application.dataPath, "../Temp/vb_result.json"),
            json
        );

        Debug.Log("SpriteAtlas created: {safe_output}");
    }}
}}
'''


