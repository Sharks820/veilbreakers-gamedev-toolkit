    sections.append("")

    # Unity directories
    sections.append("# Unity generated directories")
    sections.append("/[Ll]ibrary/")
    sections.append("/[Tt]emp/")
    sections.append("/[Oo]bj/")
    sections.append("/[Bb]uild/")
    sections.append("/[Bb]uilds/")
    sections.append("/[Ll]ogs/")
    sections.append("/[Uu]ser[Ss]ettings/")
    sections.append("/[Mm]emoryCaptures/")
    sections.append("/[Rr]ecordings/")
    sections.append("")

    # IDE and build artifacts
    sections.append("# IDE and build artifacts")
    sections.append("*.csproj")
    sections.append("*.sln")
    sections.append("*.suo")
    sections.append("*.tmp")
    sections.append("*.user")
    sections.append("*.userprefs")
    sections.append("*.pidb")
    sections.append("*.booproj")
    sections.append("*.svd")
    sections.append("*.pdb")
    sections.append("*.mdb")
    sections.append("*.opendb")
    sections.append("*.VC.db")
    sections.append("")

    # Build outputs
    sections.append("# Build outputs")
    sections.append("*.apk")
    sections.append("*.aab")
    sections.append("*.unitypackage")
    sections.append("*.app")
    sections.append("")

    # JetBrains Rider
    sections.append("# JetBrains Rider")
    sections.append("/Assets/Plugins/Editor/JetBrains*")
    sections.append("")

    # Crashlytics
    sections.append("# Crashlytics")
    sections.append("crashlytics-build.properties")
    sections.append("")

    # OS-specific
    sections.append("# macOS")
    sections.append(".DS_Store")
    sections.append("*.swp")
    sections.append("")
    sections.append("# Windows")
    sections.append("Thumbs.db")
    sections.append("*.lnk")
    sections.append("")

    # Extra patterns
    if extra_patterns:
        sections.append("# Custom patterns")
        for pattern in extra_patterns:
            sections.append(pattern)
        sections.append("")

    return "\n".join(sections)


