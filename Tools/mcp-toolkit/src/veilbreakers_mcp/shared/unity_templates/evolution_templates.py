def _c(rgba: list[float]) -> str:
    """Format an RGBA list as a C# ``new Color(...)`` literal."""
    return f"new Color({rgba[0]}f, {rgba[1]}f, {rgba[2]}f, {rgba[3]}f)"


def _v4(rgba: list[float]) -> str:
    """Format as ``new Vector4(...)``."""
    return f"new Vector4({rgba[0]}f, {rgba[1]}f, {rgba[2]}f, {rgba[3]}f)"


