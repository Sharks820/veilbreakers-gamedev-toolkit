def _brand_color_case_block(indent: str = "                ") -> str:
    """Generate a C# switch block mapping brand string to primary/glow colors."""
    lines: list[str] = []
    for b in ALL_BRANDS:
        p = BRAND_PRIMARY_COLORS[b]
        g = BRAND_GLOW_COLORS[b]
        lines.append(f'{indent}case "{b}":')
        lines.append(f"{indent}    primary = {_c(p)};")
        lines.append(f"{indent}    glow = {_c(g)};")
        lines.append(f"{indent}    break;")
    return "\n".join(lines)


