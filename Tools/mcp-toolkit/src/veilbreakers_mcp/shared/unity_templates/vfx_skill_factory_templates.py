_CS_EXECUTE_SEQUENCE = r'''
        // =============================================================
        // Phase sequencer
        // =============================================================

        IEnumerator ExecuteSkillSequence(SkillVFXConfig cfg, Vector3 origin, Vector3 target)
        {
            string brand = cfg.brand ?? "SURGE";
            OnSkillStarted?.Invoke(cfg.skillName, brand);

            // -- Phase 1: Charge --
            if (cfg.chargePhase != null)
                yield return StartCoroutine(RunPhase(cfg.chargePhase, brand, origin, target, cfg.chargeDuration));

            // -- Phase 2: Execute --
            if (cfg.executePhase != null)
                yield return StartCoroutine(RunPhase(cfg.executePhase, brand, origin, target, cfg.executeDuration));

            // -- Screen effects at impact --
            if (cfg.screenShakeIntensity > 0f) ApplyScreenShake(cfg.screenShakeIntensity);
            if (cfg.chromAbIntensity > 0f)     ApplyChromaticAberration(cfg.chromAbIntensity);
            if (cfg.slowMoScale < 1f && cfg.slowMoDuration > 0f)
                StartCoroutine(ApplySlowMo(cfg.slowMoScale, cfg.slowMoDuration));

            // -- Phase 3: Impact --
            if (cfg.impactPhase != null)
                yield return StartCoroutine(RunPhase(cfg.impactPhase, brand, origin, target, cfg.impactDuration));

            // -- Phase 4: Aftermath --
            if (cfg.aftermathPhase != null)
                yield return StartCoroutine(RunPhase(cfg.aftermathPhase, brand, origin, target, cfg.aftermathDuration));

            OnSkillFinished?.Invoke(cfg.skillName, brand);
        }

        IEnumerator RunPhase(ArchetypeCall[] calls, string brand, Vector3 origin, Vector3 target, float phaseDur)
        {
            float phaseStart = Time.time;
            foreach (var call in calls)
            {
                if (call.delay > 0f) yield return new WaitForSeconds(call.delay);
                SpawnArchetype(call, brand, origin, target, phaseDur);
            }
            float elapsed = Time.time - phaseStart;
            if (elapsed < phaseDur)
                yield return new WaitForSeconds(phaseDur - elapsed);
        }

        void SpawnArchetype(ArchetypeCall call, string brand, Vector3 origin, Vector3 target, float lifetime)
        {
            Vector3 pos = origin + call.offset;
            float s = call.scale;
            switch (call.archetype)
            {
                case "arc_bolt":           Archetype_ArcBolt(pos, target, brand, s, lifetime); break;
                case "projectile_orb":     Archetype_ProjectileOrb(pos, target, brand, s, lifetime); break;
                case "ground_ring":        Archetype_GroundRing(pos, brand, s, lifetime); break;
                case "shield_dome":        Archetype_ShieldDome(pos, brand, s, lifetime); break;
                case "beam_ray":           Archetype_BeamRay(pos, target, brand, s, lifetime); break;
                case "cone_blast":         Archetype_ConeBlast(pos, target, brand, s, lifetime); break;
                case "summon_circle":      Archetype_SummonCircle(pos, brand, s, lifetime); break;
                case "aura_wrap":          Archetype_AuraWrap(pos, brand, s, lifetime); break;
                case "impact_burst":       Archetype_ImpactBurst(pos, brand, s, lifetime); break;
                case "vortex_suction":     Archetype_VortexSuction(pos, brand, s, lifetime); break;
                case "trail_slash":        Archetype_TrailSlash(pos, target, brand, s, lifetime); break;
                case "environmental_mark": Archetype_EnvironmentalMark(pos, brand, s, lifetime); break;
                default:
                    Debug.LogWarning($"[VBSkillVFX] Unknown archetype: {call.archetype}");
                    break;
            }
        }
'''

