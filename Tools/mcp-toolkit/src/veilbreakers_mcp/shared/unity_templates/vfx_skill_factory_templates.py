_CS_ARCHETYPE_ARC_BOLT = r'''
        // =============================================================
        // Archetype 1: ARC BOLT -- jagged lightning between two points
        // =============================================================

        void Archetype_ArcBolt(Vector3 start, Vector3 end, string brand, float scale, float lifetime)
        {
            Color glow = HDR(GetGlow(brand), brand);
            Color core = HDR(Color.white, brand);
            float width = 0.08f * scale;
            int segments = 12;
            int branches = Mathf.RoundToInt(2 * scale);

            // -- Core bolt LineRenderer --
            var boltGO = new GameObject("VFX_ArcBolt");
            var lr = boltGO.AddComponent<LineRenderer>();
            lr.material = CreateAdditiveMat(glow);
            lr.startWidth = width; lr.endWidth = width * 0.3f;
            lr.widthCurve = AnimationCurve.EaseInOut(0, 1f, 1f, 0.2f);
            lr.positionCount = segments + 1;
            lr.useWorldSpace = true;
            GenerateBoltPath(lr, start, end, segments, scale * 0.25f);

            // -- Glow trail particles along path --
            var glowGO = CreateChild(boltGO, "GlowTrail");
            var ps = AddParticleSystem(glowGO, glow, 40, lifetime * 0.5f, 0.06f * scale);
            var shape = ps.shape; shape.shapeType = ParticleSystemShapeType.Cone;
            shape.radius = Vector3.Distance(start, end);
            var em = ps.emission; em.rateOverTime = 60f;

            // -- Core flash --
            var flashGO = CreateChild(boltGO, "CoreFlash");
            var fPS = AddParticleSystem(flashGO, core, 3, 0.06f, 0.2f * scale);
            var fEmit = fPS.emission; fEmit.rateOverTime = 0;
            fEmit.SetBursts(new[] { new ParticleSystem.Burst(0f, 3) });

            // -- Branch sub-bolts --
            for (int i = 0; i < branches; i++)
            {
                float t = UnityEngine.Random.Range(0.2f, 0.8f);
                Vector3 branchStart = Vector3.Lerp(start, end, t);
                Vector3 branchEnd = branchStart + UnityEngine.Random.insideUnitSphere * scale * 1.2f;
                var brGO = CreateChild(boltGO, $"Branch_{i}");
                var brLR = brGO.AddComponent<LineRenderer>();
                brLR.material = CreateAdditiveMat(glow * 0.7f);
                brLR.startWidth = width * 0.5f; brLR.endWidth = width * 0.1f;
                brLR.positionCount = 6;
                brLR.useWorldSpace = true;
                GenerateBoltPath(brLR, branchStart, branchEnd, 5, scale * 0.15f);
            }

            // -- Flicker regeneration coroutine --
            StartCoroutine(FlickerBolt(lr, start, end, segments, scale * 0.25f, lifetime));

            // -- Point light --
            var lightGO = CreateChild(boltGO, "BoltLight");
            var light = lightGO.AddComponent<Light>();
            light.type = LightType.Point; light.color = glow;
            light.intensity = 3f * scale; light.range = 4f * scale;
            lightGO.transform.position = Vector3.Lerp(start, end, 0.5f);
            Tween.LightIntensity(light, 0f, lifetime, Ease.InQuad);

            Destroy(boltGO, lifetime + 0.1f);
        }

        void GenerateBoltPath(LineRenderer lr, Vector3 a, Vector3 b, int segs, float jitter)
        {
            for (int i = 0; i <= segs; i++)
            {
                float t = (float)i / segs;
                Vector3 p = Vector3.Lerp(a, b, t);
                if (i > 0 && i < segs)
                {
                    Vector3 perp = Vector3.Cross(b - a, Vector3.up).normalized;
                    p += perp * UnityEngine.Random.Range(-jitter, jitter);
                    p.y += UnityEngine.Random.Range(-jitter * 0.5f, jitter * 0.5f);
                }
                lr.SetPosition(i, p);
            }
        }

        IEnumerator FlickerBolt(LineRenderer lr, Vector3 a, Vector3 b, int segs, float jitter, float dur)
        {
            float t = 0f;
            while (t < dur && lr != null)
            {
                GenerateBoltPath(lr, a, b, segs, jitter * (1f - t / dur));
                lr.startWidth = Mathf.Lerp(lr.startWidth, 0f, t / dur * 0.3f);
                yield return new WaitForSeconds(0.03f);
                t += 0.03f;
            }
        }
'''

_CS_ARCHETYPE_PROJECTILE_ORB = r'''
        // =============================================================
        // Archetype 2: PROJECTILE ORB -- homing/straight projectile
        // =============================================================

        void Archetype_ProjectileOrb(Vector3 origin, Vector3 target, string brand, float scale, float lifetime)
        {
            Color primary = HDR(GetPrimary(brand), brand);
            Color glow = HDR(GetGlow(brand), brand);
            float speed = 12f * scale;
            float size = 0.2f * scale;

            var root = new GameObject("VFX_ProjectileOrb");
            root.transform.position = origin;

            // -- Core glow particle --
            var coreGO = CreateChild(root, "Core");
            var corePS = AddParticleSystem(coreGO, glow, 1, lifetime, size);
            var coreMain = corePS.main;
            coreMain.simulationSpace = ParticleSystemSimulationSpace.Local;
            var coreEmit = corePS.emission; coreEmit.rateOverTime = 0;
            coreEmit.SetBursts(new[] { new ParticleSystem.Burst(0f, 1) });

            // -- Trail smoke particles --
            var trailGO = CreateChild(root, "Trail");
            var trailPS = AddParticleSystem(trailGO, primary * 0.6f, 30, 0.4f, size * 0.6f);
            var trailMain = trailPS.main;
            trailMain.simulationSpace = ParticleSystemSimulationSpace.World;
            var trailShape = trailPS.shape;
            trailShape.shapeType = ParticleSystemShapeType.Sphere;
            trailShape.radius = size * 0.3f;
            var trailSOL = trailPS.sizeOverLifetime; trailSOL.enabled = true;
            trailSOL.size = new ParticleSystem.MinMaxCurve(1f, AnimationCurve.EaseInOut(0, 1, 1, 0));

            // -- Point light --
            var lightGO = CreateChild(root, "OrbLight");
            var light = lightGO.AddComponent<Light>();
            light.type = LightType.Point; light.color = GetGlow(brand);
            light.intensity = 2.5f * scale; light.range = 3f * scale;

            // -- Fly toward target --
            StartCoroutine(FlyProjectile(root, origin, target, speed, brand, scale, lifetime));
        }

        IEnumerator FlyProjectile(GameObject obj, Vector3 from, Vector3 to, float speed, string brand, float scale, float maxLife)
        {
            float t = 0f;
            float dist = Vector3.Distance(from, to);
            float dur = dist / Mathf.Max(speed, 0.1f);
            while (t < dur && t < maxLife && obj != null)
            {
                t += Time.deltaTime;
                obj.transform.position = Vector3.Lerp(from, to, t / dur);
                yield return null;
            }
            if (obj != null)
            {
                // Impact burst at arrival
                SpawnImpactBurstAt(obj.transform.position, brand, scale * 1.2f);
                Destroy(obj);
            }
        }

        void SpawnImpactBurstAt(Vector3 pos, string brand, float scale)
        {
            Archetype_ImpactBurst(pos, brand, scale, 0.5f);
        }
'''

