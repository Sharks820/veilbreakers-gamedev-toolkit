def generate_damage_feedback_script(
    namespace: str = "VeilBreakers.Combat",
) -> str:
    """Generate C# for a visual and audio damage feedback system.

    Produces a VB_DamageFeedback MonoBehaviour that provides multiple
    layers of feedback when damage is dealt or received.

    Features:
        - Damage number spawning (world-space TextMeshPro with animation)
        - Hit flash (MaterialPropertyBlock flash white for 0.05s)
        - Directional damage indicator (screen-space arrow toward attacker)
        - Health bar shake on damage
        - Screen blood splatter at low HP
        - Controller rumble integration via Input System haptics
        - Severity tiers: light, medium, heavy, critical

    Returns:
        Complete C# source string for VB_DamageFeedback.cs
    """
    ns = _safe_namespace(namespace)
    L: list[str] = []

    L.append("using System;")
    L.append("using System.Collections;")
    L.append("using System.Collections.Generic;")
    L.append("using UnityEngine;")
    L.append("using UnityEngine.Events;")
    L.append("")
    L.append(f"namespace {ns}")
    L.append("{")

    # DamageSeverity enum
    L.append("    /// <summary>Severity tier controlling which feedback layers activate.</summary>")
    L.append("    public enum DamageSeverity")
    L.append("    {")
    L.append("        /// <summary>Flash only.</summary>")
    L.append("        Light,")
    L.append("        /// <summary>Flash + shake.</summary>")
    L.append("        Medium,")
    L.append("        /// <summary>Flash + shake + screen effect.</summary>")
    L.append("        Heavy,")
    L.append("        /// <summary>All effects + slow-mo.</summary>")
    L.append("        Critical")
    L.append("    }")
    L.append("")

    # DamageEvent struct
    L.append("    /// <summary>Data payload for a damage feedback event.</summary>")
    L.append("    [Serializable]")
    L.append("    public struct DamageEvent")
    L.append("    {")
    L.append("        /// <summary>Amount of damage dealt.</summary>")
    L.append("        public float Damage;")
    L.append("")
    L.append("        /// <summary>World position where the damage occurred.</summary>")
    L.append("        public Vector3 HitPosition;")
    L.append("")
    L.append("        /// <summary>Direction from attacker to target (world space).</summary>")
    L.append("        public Vector3 HitDirection;")
    L.append("")
    L.append("        /// <summary>Severity tier.</summary>")
    L.append("        public DamageSeverity Severity;")
    L.append("")
    L.append("        /// <summary>Whether this was a critical hit.</summary>")
    L.append("        public bool IsCritical;")
    L.append("")
    L.append("        /// <summary>Name of the element/brand (for colored damage numbers).</summary>")
    L.append("        public string ElementName;")
    L.append("    }")
    L.append("")

    # Main class
    L.append("    /// <summary>")
    L.append("    /// Multi-layer damage feedback system for AAA combat feel.")
    L.append("    /// Triggers hit flash, damage numbers, directional indicators, screen effects,")
    L.append("    /// and haptic rumble based on damage severity.")
    L.append("    /// </summary>")
    L.append("    public class VB_DamageFeedback : MonoBehaviour")
    L.append("    {")

    L.append("        /// <summary>Singleton instance.</summary>")
    L.append("        public static VB_DamageFeedback Instance { get; private set; }")
    L.append("")

    # Hit flash
    L.append("        [Header(\"Hit Flash\")]")
    L.append("        [Tooltip(\"Color to flash renderers on hit.\")]")
    L.append("        [SerializeField] private Color _flashColor = Color.white;")
    L.append("")
    L.append("        [Tooltip(\"Duration of the flash effect in seconds.\")]")
    L.append("        [SerializeField] private float _flashDuration = 0.05f;")
    L.append("")
    L.append("        [Tooltip(\"Shader property name for emission color override.\")]")
    L.append("        [SerializeField] private string _emissionProperty = \"_EmissionColor\";")
    L.append("")

    # Damage numbers
    L.append("        [Header(\"Damage Numbers\")]")
    L.append("        [Tooltip(\"Prefab for damage number display (should have TextMeshPro component).\")]")
    L.append("        [SerializeField] private GameObject _damageNumberPrefab;")
    L.append("")
    L.append("        [Tooltip(\"Vertical offset for damage number spawn position.\")]")
    L.append("        [SerializeField] private float _damageNumberYOffset = 1.5f;")
    L.append("")
    L.append("        [Tooltip(\"Random horizontal spread for damage numbers.\")]")
    L.append("        [SerializeField] private float _damageNumberSpread = 0.5f;")
    L.append("")
    L.append("        [Tooltip(\"How long damage numbers persist before being destroyed.\")]")
    L.append("        [SerializeField] private float _damageNumberLifetime = 1.2f;")
    L.append("")
    L.append("        [Tooltip(\"Upward float speed for damage numbers.\")]")
    L.append("        [SerializeField] private float _damageNumberFloatSpeed = 1f;")
    L.append("")

    # Directional indicator
    L.append("        [Header(\"Directional Damage Indicator\")]")
    L.append("        [Tooltip(\"UI element prefab for directional damage arrows (screen space).\")]")
    L.append("        [SerializeField] private GameObject _directionalIndicatorPrefab;")
    L.append("")
    L.append("        [Tooltip(\"How long the directional indicator persists.\")]")
    L.append("        [SerializeField] private float _indicatorDuration = 0.8f;")
    L.append("")

    # Screen effects
    L.append("        [Header(\"Screen Effects\")]")
    L.append("        [Tooltip(\"Blood splatter overlay material for full-screen effect.\")]")
    L.append("        [SerializeField] private Material _bloodSplatterMaterial;")
    L.append("")
    L.append("        [Tooltip(\"HP threshold (0-1) below which blood splatter appears.\")]")
    L.append("        [Range(0f, 1f)]")
    L.append("        [SerializeField] private float _bloodSplatterThreshold = 0.3f;")
    L.append("")
    L.append("        [Tooltip(\"Intensity property name on the splatter material.\")]")
    L.append("        [SerializeField] private string _splatterIntensityProperty = \"_Intensity\";")
    L.append("")

    # Rumble
    L.append("        [Header(\"Controller Haptics\")]")
    L.append("        [Tooltip(\"Light hit rumble intensity (low frequency motor, 0-1).\")]")
    L.append("        [Range(0f, 1f)]")
    L.append("        [SerializeField] private float _lightRumbleLow = 0.1f;")
    L.append("")
    L.append("        [Tooltip(\"Light hit rumble intensity (high frequency motor, 0-1).\")]")
    L.append("        [Range(0f, 1f)]")
    L.append("        [SerializeField] private float _lightRumbleHigh = 0.15f;")
    L.append("")
    L.append("        [Tooltip(\"Heavy hit rumble intensity (low frequency motor, 0-1).\")]")
    L.append("        [Range(0f, 1f)]")
    L.append("        [SerializeField] private float _heavyRumbleLow = 0.6f;")
    L.append("")
    L.append("        [Tooltip(\"Heavy hit rumble intensity (high frequency motor, 0-1).\")]")
    L.append("        [Range(0f, 1f)]")
    L.append("        [SerializeField] private float _heavyRumbleHigh = 0.8f;")
    L.append("")
    L.append("        [Tooltip(\"Rumble duration in seconds.\")]")
    L.append("        [SerializeField] private float _rumbleDuration = 0.15f;")
    L.append("")

    # Events
    L.append("        [Header(\"Events\")]")
    L.append("        [Tooltip(\"Fired when damage feedback is triggered.\")]")
    L.append("        [SerializeField] private UnityEvent<DamageEvent> _onDamageFeedback = new UnityEvent<DamageEvent>();")
    L.append("")
    L.append("        [Tooltip(\"Fired to request screen shake. Parameter: intensity.\")]")
    L.append("        [SerializeField] private UnityEvent<float> _onScreenShake = new UnityEvent<float>();")
    L.append("")
    L.append("        [Tooltip(\"Fired to request hit-stop. Parameter: hit type enum value.\")]")
    L.append("        [SerializeField] private UnityEvent<int> _onHitStop = new UnityEvent<int>();")
    L.append("")

    # Pool
    L.append("        private readonly Queue<GameObject> _numberPool = new Queue<GameObject>();")
    L.append("        private MaterialPropertyBlock _propertyBlock;")
    L.append("        private Coroutine _rumbleCoroutine;")
    L.append("")

    # Awake
    L.append("        private void Awake()")
    L.append("        {")
    L.append("            if (Instance != null && Instance != this)")
    L.append("            {")
    L.append("                Destroy(gameObject);")
    L.append("                return;")
    L.append("            }")
    L.append("            Instance = this;")
    L.append("            _propertyBlock = new MaterialPropertyBlock();")
    L.append("        }")
    L.append("")

    L.append("        private void OnDestroy()")
    L.append("        {")
    L.append("            if (Instance == this) Instance = null;")
    L.append("        }")
    L.append("")

    # TriggerFeedback
    L.append("        /// <summary>")
    L.append("        /// Trigger the full damage feedback pipeline based on severity.")
    L.append("        /// Light: flash only. Medium: flash + shake. Heavy: flash + shake + screen.")
    L.append("        /// Critical: all effects + slow-mo request.")
    L.append("        /// </summary>")
    L.append("        public void TriggerFeedback(DamageEvent damageEvent, Renderer[] targetRenderers = null)")
    L.append("        {")
    L.append("            // Always flash on any hit")
    L.append("            if (targetRenderers != null)")
    L.append("            {")
    L.append("                foreach (Renderer rend in targetRenderers)")
    L.append("                {")
    L.append("                    if (rend != null)")
    L.append("                    {")
    L.append("                        StartCoroutine(HitFlashCoroutine(rend));")
    L.append("                    }")
    L.append("                }")
    L.append("            }")
    L.append("")
    L.append("            // Spawn damage number")
    L.append("            SpawnDamageNumber(damageEvent);")
    L.append("")
    L.append("            // Medium+: screen shake + directional indicator")
    L.append("            if (damageEvent.Severity >= DamageSeverity.Medium)")
    L.append("            {")
    L.append("                float shakeIntensity = damageEvent.Severity == DamageSeverity.Medium ? 0.3f :")
    L.append("                                      damageEvent.Severity == DamageSeverity.Heavy ? 0.6f : 1f;")
    L.append("                _onScreenShake?.Invoke(shakeIntensity);")
    L.append("                SpawnDirectionalIndicator(damageEvent.HitDirection);")
    L.append("            }")
    L.append("")
    L.append("            // Heavy+: request hit-stop")
    L.append("            if (damageEvent.Severity >= DamageSeverity.Heavy)")
    L.append("            {")
    L.append("                int hitTypeValue = damageEvent.Severity == DamageSeverity.Heavy ? 2 : 3;")
    L.append("                _onHitStop?.Invoke(hitTypeValue);")
    L.append("            }")
    L.append("")
    L.append("            // Critical: rumble")
    L.append("            if (damageEvent.Severity >= DamageSeverity.Heavy)")
    L.append("            {")
    L.append("                float lowFreq = damageEvent.Severity == DamageSeverity.Critical ? _heavyRumbleLow : _lightRumbleLow;")
    L.append("                float highFreq = damageEvent.Severity == DamageSeverity.Critical ? _heavyRumbleHigh : _lightRumbleHigh;")
    L.append("                TriggerRumble(lowFreq, highFreq);")
    L.append("            }")
    L.append("")
    L.append("            _onDamageFeedback?.Invoke(damageEvent);")
    L.append("        }")
    L.append("")

    # HitFlash
    L.append("        /// <summary>Flash a renderer white briefly using MaterialPropertyBlock.</summary>")
    L.append("        public void HitFlash(Renderer targetRenderer)")
    L.append("        {")
    L.append("            if (targetRenderer != null)")
    L.append("            {")
    L.append("                StartCoroutine(HitFlashCoroutine(targetRenderer));")
    L.append("            }")
    L.append("        }")
    L.append("")

    L.append("        private IEnumerator HitFlashCoroutine(Renderer targetRenderer)")
    L.append("        {")
    L.append("            if (targetRenderer == null) yield break;")
    L.append("")
    L.append("            targetRenderer.GetPropertyBlock(_propertyBlock);")
    L.append("            Color originalEmission = _propertyBlock.GetColor(_emissionProperty);")
    L.append("            _propertyBlock.SetColor(_emissionProperty, _flashColor * 5f);")
    L.append("            targetRenderer.SetPropertyBlock(_propertyBlock);")
    L.append("")
    L.append("            yield return new WaitForSecondsRealtime(_flashDuration);")
    L.append("")
    L.append("            if (targetRenderer != null)")
    L.append("            {")
    L.append("                targetRenderer.GetPropertyBlock(_propertyBlock);")
    L.append("                _propertyBlock.SetColor(_emissionProperty, originalEmission);")
    L.append("                targetRenderer.SetPropertyBlock(_propertyBlock);")
    L.append("            }")
    L.append("        }")
    L.append("")

    # SpawnDamageNumber
    L.append("        /// <summary>Spawn a damage number at the hit position.</summary>")
    L.append("        public void SpawnDamageNumber(DamageEvent damageEvent)")
    L.append("        {")
    L.append("            if (_damageNumberPrefab == null) return;")
    L.append("")
    L.append("            Vector3 spawnPos = damageEvent.HitPosition +")
    L.append("                Vector3.up * _damageNumberYOffset +")
    L.append("                new Vector3(")
    L.append("                    UnityEngine.Random.Range(-_damageNumberSpread, _damageNumberSpread),")
    L.append("                    UnityEngine.Random.Range(0f, _damageNumberSpread * 0.5f),")
    L.append("                    0f")
    L.append("                );")
    L.append("")
    L.append("            GameObject numObj = GetPooledNumber();")
    L.append("            numObj.transform.position = spawnPos;")
    L.append("            numObj.SetActive(true);")
    L.append("")
    L.append("            // Set text via TMPro if available")
    L.append("            var tmPro = numObj.GetComponent<TMPro.TextMeshPro>();")
    L.append("            if (tmPro != null)")
    L.append("            {")
    L.append("                tmPro.text = Mathf.RoundToInt(damageEvent.Damage).ToString();")
    L.append("                if (damageEvent.IsCritical)")
    L.append("                {")
    L.append("                    tmPro.fontSize *= 1.5f;")
    L.append("                    tmPro.color = Color.yellow;")
    L.append("                }")
    L.append("            }")
    L.append("")
    L.append("            StartCoroutine(AnimateDamageNumber(numObj));")
    L.append("        }")
    L.append("")

    L.append("        private IEnumerator AnimateDamageNumber(GameObject numObj)")
    L.append("        {")
    L.append("            float elapsed = 0f;")
    L.append("            Vector3 startPos = numObj.transform.position;")
    L.append("")
    L.append("            while (elapsed < _damageNumberLifetime)")
    L.append("            {")
    L.append("                elapsed += Time.unscaledDeltaTime;")
    L.append("                float t = elapsed / _damageNumberLifetime;")
    L.append("")
    L.append("                // Float upward")
    L.append("                numObj.transform.position = startPos + Vector3.up * (_damageNumberFloatSpeed * elapsed);")
    L.append("")
    L.append("                // Fade out in second half")
    L.append("                if (t > 0.5f)")
    L.append("                {")
    L.append("                    float alpha = 1f - ((t - 0.5f) * 2f);")
    L.append("                    var tmPro = numObj.GetComponent<TMPro.TextMeshPro>();")
    L.append("                    if (tmPro != null)")
    L.append("                    {")
    L.append("                        Color c = tmPro.color;")
    L.append("                        c.a = alpha;")
    L.append("                        tmPro.color = c;")
    L.append("                    }")
    L.append("                }")
    L.append("")
    L.append("                yield return null;")
    L.append("            }")
    L.append("")
    L.append("            numObj.SetActive(false);")
    L.append("            _numberPool.Enqueue(numObj);")
    L.append("        }")
    L.append("")

    # Pool helper
    L.append("        private GameObject GetPooledNumber()")
    L.append("        {")
    L.append("            if (_numberPool.Count > 0)")
    L.append("            {")
    L.append("                GameObject pooled = _numberPool.Dequeue();")
    L.append("                if (pooled != null) return pooled;")
    L.append("            }")
    L.append("            return Instantiate(_damageNumberPrefab, transform);")
    L.append("        }")
    L.append("")

    # SpawnDirectionalIndicator
    L.append("        /// <summary>Spawn a directional damage indicator pointing toward the attacker.</summary>")
    L.append("        public void SpawnDirectionalIndicator(Vector3 hitDirection)")
    L.append("        {")
    L.append("            if (_directionalIndicatorPrefab == null) return;")
    L.append("")
    L.append("            GameObject indicator = Instantiate(_directionalIndicatorPrefab, transform);")
    L.append("")
    L.append("            // Rotate to face the hit direction (projected onto screen plane)")
    L.append("            float angle = Mathf.Atan2(hitDirection.x, hitDirection.z) * Mathf.Rad2Deg;")
    L.append("            indicator.transform.localRotation = Quaternion.Euler(0f, 0f, -angle);")
    L.append("")
    L.append("            Destroy(indicator, _indicatorDuration);")
    L.append("        }")
    L.append("")

    # UpdateBloodSplatter
    L.append("        /// <summary>")
    L.append("        /// Update the blood splatter screen overlay based on current HP ratio.")
    L.append("        /// Call this whenever health changes.")
    L.append("        /// </summary>")
    L.append("        /// <param name=\"hpRatio\">Current HP ratio (0-1).</param>")
    L.append("        public void UpdateBloodSplatter(float hpRatio)")
    L.append("        {")
    L.append("            if (_bloodSplatterMaterial == null) return;")
    L.append("")
    L.append("            if (hpRatio < _bloodSplatterThreshold)")
    L.append("            {")
    L.append("                float intensity = 1f - (hpRatio / _bloodSplatterThreshold);")
    L.append("                _bloodSplatterMaterial.SetFloat(_splatterIntensityProperty, intensity);")
    L.append("            }")
    L.append("            else")
    L.append("            {")
    L.append("                _bloodSplatterMaterial.SetFloat(_splatterIntensityProperty, 0f);")
    L.append("            }")
    L.append("        }")
    L.append("")

    # TriggerRumble
    L.append("        /// <summary>")
    L.append("        /// Trigger controller haptic feedback via Input System.")
    L.append("        /// </summary>")
    L.append("        public void TriggerRumble(float lowFrequency, float highFrequency)")
    L.append("        {")
    L.append("#if ENABLE_INPUT_SYSTEM")
    L.append("            var gamepad = UnityEngine.InputSystem.Gamepad.current;")
    L.append("            if (gamepad != null)")
    L.append("            {")
    L.append("                gamepad.SetMotorSpeeds(lowFrequency, highFrequency);")
    L.append("                if (_rumbleCoroutine != null) StopCoroutine(_rumbleCoroutine);")
    L.append("                _rumbleCoroutine = StartCoroutine(StopRumbleAfterDelay());")
    L.append("            }")
    L.append("#endif")
    L.append("        }")
    L.append("")

    L.append("        private IEnumerator StopRumbleAfterDelay()")
    L.append("        {")
    L.append("            yield return new WaitForSecondsRealtime(_rumbleDuration);")
    L.append("#if ENABLE_INPUT_SYSTEM")
    L.append("            var gamepad = UnityEngine.InputSystem.Gamepad.current;")
    L.append("            if (gamepad != null)")
    L.append("            {")
    L.append("                gamepad.SetMotorSpeeds(0f, 0f);")
    L.append("            }")
    L.append("#endif")
    L.append("            _rumbleCoroutine = null;")
    L.append("        }")

    L.append("    }")
    L.append("}")

    return "\n".join(L) + "\n"


