def generate_hitstop_system_script(
    namespace: str = "VeilBreakers.Combat",
) -> str:
    """Generate C# for a hit-stop (freeze frame) system.

    Produces a VB_HitStopSystem MonoBehaviour that pauses time briefly
    on weapon impacts, creating the visceral freeze-frame effect used by
    FromSoftware and God of War.

    Features:
        - TriggerHitStop(float duration, float timeScale) with coroutine restore
        - Per-hit-type presets: Light, Medium, Heavy, Critical, Parry
        - Screen shake integration via UnityEvent
        - Camera zoom punch on heavy hits (DOF focus shift)
        - Configurable default/heavy/critical duration ranges

    Returns:
        Complete C# source string for VB_HitStopSystem.cs
    """
    ns = _safe_namespace(namespace)
    L: list[str] = []

    L.append("using System;")
    L.append("using System.Collections;")
    L.append("using UnityEngine;")
    L.append("using UnityEngine.Events;")
    L.append("")
    L.append(f"namespace {ns}")
    L.append("{")

    # HitType enum
    L.append("    /// <summary>Severity tier for a combat hit, controlling hit-stop duration and effects.</summary>")
    L.append("    public enum HitType")
    L.append("    {")
    L.append("        Light,")
    L.append("        Medium,")
    L.append("        Heavy,")
    L.append("        Critical,")
    L.append("        Parry")
    L.append("    }")
    L.append("")

    # HitStopPreset
    L.append("    /// <summary>Configuration preset for a single hit-stop type.</summary>")
    L.append("    [Serializable]")
    L.append("    public struct HitStopPreset")
    L.append("    {")
    L.append("        [Tooltip(\"Duration of the time freeze in seconds.\")]")
    L.append("        public float Duration;")
    L.append("")
    L.append("        [Tooltip(\"Time scale during the freeze (0 = full stop, 0.1 = near-stop).\")]")
    L.append("        [Range(0f, 0.5f)]")
    L.append("        public float TimeScale;")
    L.append("")
    L.append("        [Tooltip(\"Whether to trigger screen shake on this hit type.\")]")
    L.append("        public bool TriggerScreenShake;")
    L.append("")
    L.append("        [Tooltip(\"Screen shake intensity multiplier.\")]")
    L.append("        public float ShakeIntensity;")
    L.append("")
    L.append("        [Tooltip(\"Whether to apply camera zoom punch.\")]")
    L.append("        public bool ApplyZoomPunch;")
    L.append("")
    L.append("        [Tooltip(\"FOV offset for zoom punch (negative = zoom in).\")]")
    L.append("        public float ZoomPunchFOVOffset;")
    L.append("    }")
    L.append("")

    # Main class
    L.append("    /// <summary>")
    L.append("    /// Hit-stop (freeze frame) system for AAA combat feel.")
    L.append("    /// Briefly pauses Time.timeScale on weapon impacts to create visceral feedback.")
    L.append("    /// Supports per-hit-type presets, screen shake integration, and camera zoom punch.")
    L.append("    /// </summary>")
    L.append("    public class VB_HitStopSystem : MonoBehaviour")
    L.append("    {")

    # Singleton-style access
    L.append("        /// <summary>Singleton instance for global access.</summary>")
    L.append("        public static VB_HitStopSystem Instance { get; private set; }")
    L.append("")

    # Presets
    L.append("        [Header(\"Hit-Stop Presets\")]")
    L.append("        [Tooltip(\"Light hit preset (0.05-0.08s duration).\")]")
    L.append("        [SerializeField] private HitStopPreset _lightPreset = new HitStopPreset")
    L.append("        {")
    L.append("            Duration = 0.05f, TimeScale = 0.1f,")
    L.append("            TriggerScreenShake = false, ShakeIntensity = 0f,")
    L.append("            ApplyZoomPunch = false, ZoomPunchFOVOffset = 0f")
    L.append("        };")
    L.append("")
    L.append("        [Tooltip(\"Medium hit preset (0.08-0.12s duration).\")]")
    L.append("        [SerializeField] private HitStopPreset _mediumPreset = new HitStopPreset")
    L.append("        {")
    L.append("            Duration = 0.1f, TimeScale = 0.05f,")
    L.append("            TriggerScreenShake = true, ShakeIntensity = 0.3f,")
    L.append("            ApplyZoomPunch = false, ZoomPunchFOVOffset = 0f")
    L.append("        };")
    L.append("")
    L.append("        [Tooltip(\"Heavy hit preset (0.15-0.3s duration).\")]")
    L.append("        [SerializeField] private HitStopPreset _heavyPreset = new HitStopPreset")
    L.append("        {")
    L.append("            Duration = 0.2f, TimeScale = 0.02f,")
    L.append("            TriggerScreenShake = true, ShakeIntensity = 0.7f,")
    L.append("            ApplyZoomPunch = true, ZoomPunchFOVOffset = -5f")
    L.append("        };")
    L.append("")
    L.append("        [Tooltip(\"Critical hit preset (0.2-0.4s duration).\")]")
    L.append("        [SerializeField] private HitStopPreset _criticalPreset = new HitStopPreset")
    L.append("        {")
    L.append("            Duration = 0.3f, TimeScale = 0.01f,")
    L.append("            TriggerScreenShake = true, ShakeIntensity = 1.0f,")
    L.append("            ApplyZoomPunch = true, ZoomPunchFOVOffset = -8f")
    L.append("        };")
    L.append("")
    L.append("        [Tooltip(\"Parry hit preset (distinct feel with brief full stop).\")]")
    L.append("        [SerializeField] private HitStopPreset _parryPreset = new HitStopPreset")
    L.append("        {")
    L.append("            Duration = 0.15f, TimeScale = 0.0f,")
    L.append("            TriggerScreenShake = true, ShakeIntensity = 0.5f,")
    L.append("            ApplyZoomPunch = true, ZoomPunchFOVOffset = -3f")
    L.append("        };")
    L.append("")

    # Events
    L.append("        [Header(\"Events\")]")
    L.append("        [Tooltip(\"Fired when screen shake should be triggered. Parameter: intensity.\")]")
    L.append("        [SerializeField] private UnityEvent<float> _onScreenShake = new UnityEvent<float>();")
    L.append("")
    L.append("        [Tooltip(\"Fired when camera zoom punch should be applied. Parameter: FOV offset.\")]")
    L.append("        [SerializeField] private UnityEvent<float> _onZoomPunch = new UnityEvent<float>();")
    L.append("")
    L.append("        [Tooltip(\"Fired when hit-stop begins. Parameter: hit type.\")]")
    L.append("        [SerializeField] private UnityEvent<HitType> _onHitStopBegin = new UnityEvent<HitType>();")
    L.append("")
    L.append("        [Tooltip(\"Fired when hit-stop ends.\")]")
    L.append("        [SerializeField] private UnityEvent _onHitStopEnd = new UnityEvent();")
    L.append("")

    # State
    L.append("        private Coroutine _activeHitStop;")
    L.append("        private float _savedTimeScale = 1f;")
    L.append("        private float _savedFixedDeltaTime;")
    L.append("")

    # Properties
    L.append("        /// <summary>Whether a hit-stop is currently active.</summary>")
    L.append("        public bool IsHitStopActive => _activeHitStop != null;")
    L.append("")

    # Awake
    L.append("        private void Awake()")
    L.append("        {")
    L.append("            if (Instance != null && Instance != this)")
    L.append("            {")
    L.append("                Destroy(gameObject);")
    L.append("                return;")
    L.append("            }")
    L.append("            Instance = this;")
    L.append("            _savedFixedDeltaTime = Time.fixedDeltaTime;")
    L.append("        }")
    L.append("")

    L.append("        private void OnDestroy()")
    L.append("        {")
    L.append("            if (Instance == this)")
    L.append("            {")
    L.append("                Instance = null;")
    L.append("                // Restore time scale if destroyed during hit-stop")
    L.append("                Time.timeScale = 1f;")
    L.append("                Time.fixedDeltaTime = _savedFixedDeltaTime;")
    L.append("            }")
    L.append("        }")
    L.append("")

    # TriggerHitStop by type
    L.append("        /// <summary>")
    L.append("        /// Trigger a hit-stop using a preset for the given hit type.")
    L.append("        /// Cancels any currently active hit-stop.")
    L.append("        /// </summary>")
    L.append("        public void TriggerHitStop(HitType hitType)")
    L.append("        {")
    L.append("            HitStopPreset preset = GetPreset(hitType);")
    L.append("            TriggerHitStop(preset.Duration, preset.TimeScale);")
    L.append("")
    L.append("            if (preset.TriggerScreenShake)")
    L.append("            {")
    L.append("                _onScreenShake?.Invoke(preset.ShakeIntensity);")
    L.append("            }")
    L.append("            if (preset.ApplyZoomPunch)")
    L.append("            {")
    L.append("                _onZoomPunch?.Invoke(preset.ZoomPunchFOVOffset);")
    L.append("            }")
    L.append("")
    L.append("            _onHitStopBegin?.Invoke(hitType);")
    L.append("        }")
    L.append("")

    # TriggerHitStop manual
    L.append("        /// <summary>")
    L.append("        /// Trigger a hit-stop with explicit duration and time scale.")
    L.append("        /// Duration is in real (unscaled) seconds.")
    L.append("        /// </summary>")
    L.append("        /// <param name=\"duration\">Freeze duration in real seconds.</param>")
    L.append("        /// <param name=\"timeScale\">Time scale during freeze (0 = full stop).</param>")
    L.append("        public void TriggerHitStop(float duration, float timeScale)")
    L.append("        {")
    L.append("            duration = Mathf.Clamp(duration, 0.01f, 1f);")
    L.append("            timeScale = Mathf.Clamp01(timeScale);")
    L.append("")
    L.append("            if (_activeHitStop != null)")
    L.append("            {")
    L.append("                StopCoroutine(_activeHitStop);")
    L.append("                RestoreTimeScale();")
    L.append("            }")
    L.append("")
    L.append("            _activeHitStop = StartCoroutine(HitStopCoroutine(duration, timeScale));")
    L.append("        }")
    L.append("")

    # Coroutine
    L.append("        private IEnumerator HitStopCoroutine(float duration, float timeScale)")
    L.append("        {")
    L.append("            _savedTimeScale = Time.timeScale;")
    L.append("            Time.timeScale = timeScale;")
    L.append("            Time.fixedDeltaTime = _savedFixedDeltaTime * timeScale;")
    L.append("")
    L.append("            // Wait in real (unscaled) time")
    L.append("            yield return new WaitForSecondsRealtime(duration);")
    L.append("")
    L.append("            RestoreTimeScale();")
    L.append("            _activeHitStop = null;")
    L.append("            _onHitStopEnd?.Invoke();")
    L.append("        }")
    L.append("")

    # RestoreTimeScale
    L.append("        private void RestoreTimeScale()")
    L.append("        {")
    L.append("            Time.timeScale = _savedTimeScale;")
    L.append("            Time.fixedDeltaTime = _savedFixedDeltaTime;")
    L.append("        }")
    L.append("")

    # GetPreset
    L.append("        /// <summary>Get the HitStopPreset for a given hit type.</summary>")
    L.append("        public HitStopPreset GetPreset(HitType hitType)")
    L.append("        {")
    L.append("            switch (hitType)")
    L.append("            {")
    L.append("                case HitType.Light:    return _lightPreset;")
    L.append("                case HitType.Medium:   return _mediumPreset;")
    L.append("                case HitType.Heavy:    return _heavyPreset;")
    L.append("                case HitType.Critical: return _criticalPreset;")
    L.append("                case HitType.Parry:    return _parryPreset;")
    L.append("                default:               return _mediumPreset;")
    L.append("            }")
    L.append("        }")

    L.append("    }")
    L.append("}")

    return "\n".join(L) + "\n"


