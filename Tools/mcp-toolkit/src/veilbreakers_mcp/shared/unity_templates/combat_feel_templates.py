"""AAA combat feel C# template generators for Unity.

Runtime MonoBehaviour generators that produce FromSoftware / God of War
quality combat feel systems -- hit-stop, input buffering, combat camera,
damage feedback, dodge roll, and attack chain systems.

These are RUNTIME scripts.  They must NEVER reference ``using UnityEditor;``.

Exports:
    generate_hitstop_system_script      -- Freeze-frame hit-stop with per-type presets
    generate_input_buffer_script        -- Ring-buffer input buffering for responsive combat
    generate_combat_camera_script       -- Lock-on, impact zoom, execution cam, boss intro
    generate_damage_feedback_script     -- Hit flash, damage numbers, screen effects, rumble
    generate_dodge_roll_script          -- I-frame dodge roll with stamina integration
    generate_attack_chain_script        -- Combo chain system with ScriptableObject data
"""

from __future__ import annotations

