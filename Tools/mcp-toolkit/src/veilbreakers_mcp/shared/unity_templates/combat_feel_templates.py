def generate_dodge_roll_script(
    namespace: str = "VeilBreakers.Combat",
) -> str:
    """Generate C# for an i-frame dodge roll system.

    Produces a VB_DodgeRoll MonoBehaviour that handles invincibility-frame
    dodge rolling with stamina integration, trail VFX hookpoints, and
    animation event callbacks.

    Features:
        - PerformDodge(Vector3 direction) with configurable i-frames
        - iFrameStart (0.05s), iFrameDuration (0.3s), dodgeCooldown (0.5s)
        - Stamina cost integration point
        - Trail VFX event hookpoint (UnityEvent)
        - Animation event callbacks: DodgeStart, Invincible, Vulnerable, DodgeEnd
        - Direction snapping (8-way or free direction)
        - CharacterController or Rigidbody movement support

    Returns:
        Complete C# source string for VB_DodgeRoll.cs
    """
    ns = _safe_namespace(namespace)
    L: list[str] = []

    L.append("using System;")
    L.append("using System.Collections;")
    L.append("using UnityEngine;")
    L.append("using UnityEngine.Events;")
    L.append("")
    L.append(f"namespace {ns}")
    L.append("{")

    # DodgeState enum
    L.append("    /// <summary>Current state of the dodge roll sequence.</summary>")
    L.append("    public enum DodgeState")
    L.append("    {")
    L.append("        /// <summary>Ready to dodge.</summary>")
    L.append("        Ready,")
    L.append("        /// <summary>Startup frames before i-frames begin.</summary>")
    L.append("        WindUp,")
    L.append("        /// <summary>Invincibility frames active.</summary>")
    L.append("        Invincible,")
    L.append("        /// <summary>Dodge active but no longer invincible.</summary>")
    L.append("        Recovery,")
    L.append("        /// <summary>On cooldown, cannot dodge.</summary>")
    L.append("        Cooldown")
    L.append("    }")
    L.append("")

    # DirectionSnapMode
    L.append("    /// <summary>Direction snapping mode for dodge roll.</summary>")
    L.append("    public enum DirectionSnapMode")
    L.append("    {")
    L.append("        /// <summary>Free direction -- dodge in exact input direction.</summary>")
    L.append("        Free,")
    L.append("        /// <summary>Snap to nearest 8-way cardinal direction.</summary>")
    L.append("        EightWay")
    L.append("    }")
    L.append("")

    # Main class
    L.append("    /// <summary>")
    L.append("    /// I-frame dodge roll system for action RPG combat.")
    L.append("    /// Provides invincibility frames during the dodge window, stamina cost integration,")
    L.append("    /// trail VFX hookpoints, and animation event callbacks.")
    L.append("    /// </summary>")
    L.append("    public class VB_DodgeRoll : MonoBehaviour")
    L.append("    {")

