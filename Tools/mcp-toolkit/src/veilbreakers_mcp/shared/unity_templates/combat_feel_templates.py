def generate_input_buffer_script(
    namespace: str = "VeilBreakers.Combat",
) -> str:
    """Generate C# for a ring-buffer input buffering system.

    Produces a VB_InputBuffer MonoBehaviour that stores recent inputs
    with timestamps and allows consuming buffered actions within a
    configurable time window.  Essential for responsive action RPG combat
    where inputs during recovery frames should still register.

    Features:
        - Ring buffer of recent inputs with Time.unscaledTime timestamps
        - BufferInput / ConsumeBufferedInput API
        - Configurable buffer window (default 0.15s)
        - Animation state awareness (blocked state list)
        - Support for attack chains, dodge cancel, ability queueing

    Returns:
        Complete C# source string for VB_InputBuffer.cs
    """
    ns = _safe_namespace(namespace)
    L: list[str] = []

    L.append("using System;")
    L.append("using System.Collections.Generic;")
    L.append("using UnityEngine;")
    L.append("")
    L.append(f"namespace {ns}")
    L.append("{")

    # BufferedInput struct
    L.append("    /// <summary>A single buffered input entry with action name and timestamp.</summary>")
    L.append("    [Serializable]")
    L.append("    public struct BufferedInput")
    L.append("    {")
    L.append("        /// <summary>Name of the action (e.g. \"LightAttack\", \"Dodge\", \"Ability1\").</summary>")
    L.append("        public string ActionName;")
    L.append("")
    L.append("        /// <summary>Unscaled timestamp when the input was recorded.</summary>")
    L.append("        public float Timestamp;")
    L.append("")
    L.append("        /// <summary>Whether this entry has been consumed.</summary>")
    L.append("        public bool Consumed;")
    L.append("    }")
    L.append("")

    # Main class
    L.append("    /// <summary>")
    L.append("    /// Input buffering system for responsive action RPG combat.")
    L.append("    /// Stores recent inputs in a ring buffer so that actions pressed during")
    L.append("    /// recovery/startup frames still register when the character becomes actionable.")
    L.append("    /// </summary>")
    L.append("    public class VB_InputBuffer : MonoBehaviour")
    L.append("    {")

    L.append("        /// <summary>Singleton instance for global access.</summary>")
    L.append("        public static VB_InputBuffer Instance { get; private set; }")
    L.append("")

    # Config
    L.append("        [Header(\"Buffer Configuration\")]")
    L.append("        [Tooltip(\"Maximum number of inputs stored in the ring buffer.\")]")
    L.append("        [SerializeField] private int _bufferCapacity = 16;")
    L.append("")
    L.append("        [Tooltip(\"Default time window in seconds for input validity.\")]")
    L.append("        [SerializeField] private float _defaultBufferWindow = 0.15f;")
    L.append("")
    L.append("        [Header(\"State Awareness\")]")
    L.append("        [Tooltip(\"Animator state tags during which input buffering is blocked.\")]")
    L.append("        [SerializeField] private List<string> _blockedStateTags = new List<string>();")
    L.append("")

    # Fields
    L.append("        private BufferedInput[] _buffer;")
    L.append("        private int _writeIndex;")
    L.append("        private Animator _animator;")
    L.append("")

    # Awake
    L.append("        private void Awake()")
    L.append("        {")
    L.append("            if (Instance != null && Instance != this)")
    L.append("            {")
    L.append("                Destroy(gameObject);")
    L.append("                return;")
    L.append("            }")
    L.append("            Instance = this;")
    L.append("            _buffer = new BufferedInput[_bufferCapacity];")
    L.append("            _writeIndex = 0;")
    L.append("            _animator = GetComponentInParent<Animator>();")
    L.append("        }")
    L.append("")

    L.append("        private void OnDestroy()")
    L.append("        {")
    L.append("            if (Instance == this) Instance = null;")
    L.append("        }")
    L.append("")

    # BufferInput
    L.append("        /// <summary>")
    L.append("        /// Record an input action into the ring buffer with the current unscaled time.")
    L.append("        /// </summary>")
    L.append("        /// <param name=\"actionName\">Name of the action to buffer.</param>")
    L.append("        public void BufferInput(string actionName)")
    L.append("        {")
    L.append("            if (string.IsNullOrEmpty(actionName)) return;")
    L.append("")
    L.append("            // Check blocked states")
    L.append("            if (IsInBlockedState()) return;")
    L.append("")
    L.append("            _buffer[_writeIndex] = new BufferedInput")
    L.append("            {")
    L.append("                ActionName = actionName,")
    L.append("                Timestamp = Time.unscaledTime,")
    L.append("                Consumed = false")
    L.append("            };")
    L.append("            _writeIndex = (_writeIndex + 1) % _bufferCapacity;")
    L.append("        }")
    L.append("")

    # ConsumeBufferedInput
    L.append("        /// <summary>")
    L.append("        /// Check for and consume a buffered input within a time window.")
    L.append("        /// Returns true if a matching unconsumed input was found within the window.")
    L.append("        /// </summary>")
    L.append("        /// <param name=\"actionName\">Name of the action to look for.</param>")
    L.append("        /// <param name=\"windowSeconds\">Time window to search (0 = use default).</param>")
    L.append("        /// <returns>True if a matching input was consumed.</returns>")
    L.append("        public bool ConsumeBufferedInput(string actionName, float windowSeconds = 0f)")
    L.append("        {")
    L.append("            if (string.IsNullOrEmpty(actionName)) return false;")
    L.append("")
    L.append("            float window = windowSeconds > 0f ? windowSeconds : _defaultBufferWindow;")
    L.append("            float cutoff = Time.unscaledTime - window;")
    L.append("")
    L.append("            // Search buffer from most recent to oldest")
    L.append("            for (int i = 0; i < _bufferCapacity; i++)")
    L.append("            {")
    L.append("                int idx = (_writeIndex - 1 - i + _bufferCapacity) % _bufferCapacity;")
    L.append("                ref BufferedInput entry = ref _buffer[idx];")
    L.append("")
    L.append("                if (entry.Consumed) continue;")
    L.append("                if (entry.Timestamp < cutoff) continue;")
    L.append("                if (string.IsNullOrEmpty(entry.ActionName)) continue;")
    L.append("")
    L.append("                if (string.Equals(entry.ActionName, actionName, StringComparison.Ordinal))")
    L.append("                {")
    L.append("                    entry.Consumed = true;")
    L.append("                    return true;")
    L.append("                }")
    L.append("            }")
    L.append("")
    L.append("            return false;")
    L.append("        }")
    L.append("")

    # HasBufferedInput (peek without consuming)
    L.append("        /// <summary>")
    L.append("        /// Check whether a buffered input exists without consuming it.")
    L.append("        /// </summary>")
    L.append("        public bool HasBufferedInput(string actionName, float windowSeconds = 0f)")
    L.append("        {")
    L.append("            if (string.IsNullOrEmpty(actionName)) return false;")
    L.append("")
    L.append("            float window = windowSeconds > 0f ? windowSeconds : _defaultBufferWindow;")
    L.append("            float cutoff = Time.unscaledTime - window;")
    L.append("")
    L.append("            for (int i = 0; i < _bufferCapacity; i++)")
    L.append("            {")
    L.append("                int idx = (_writeIndex - 1 - i + _bufferCapacity) % _bufferCapacity;")
    L.append("                BufferedInput entry = _buffer[idx];")
    L.append("")
    L.append("                if (entry.Consumed) continue;")
    L.append("                if (entry.Timestamp < cutoff) continue;")
    L.append("                if (string.IsNullOrEmpty(entry.ActionName)) continue;")
    L.append("")
    L.append("                if (string.Equals(entry.ActionName, actionName, StringComparison.Ordinal))")
    L.append("                {")
    L.append("                    return true;")
    L.append("                }")
    L.append("            }")
    L.append("")
    L.append("            return false;")
    L.append("        }")
    L.append("")

    # ClearBuffer
    L.append("        /// <summary>Clear all buffered inputs.</summary>")
    L.append("        public void ClearBuffer()")
    L.append("        {")
    L.append("            for (int i = 0; i < _bufferCapacity; i++)")
    L.append("            {")
    L.append("                _buffer[i] = default;")
    L.append("            }")
    L.append("            _writeIndex = 0;")
    L.append("        }")
    L.append("")

    # IsInBlockedState
    L.append("        /// <summary>Check whether the animator is in a state that blocks input buffering.</summary>")
    L.append("        private bool IsInBlockedState()")
    L.append("        {")
    L.append("            if (_animator == null || _blockedStateTags.Count == 0) return false;")
    L.append("")
    L.append("            AnimatorStateInfo stateInfo = _animator.GetCurrentAnimatorStateInfo(0);")
    L.append("            for (int i = 0; i < _blockedStateTags.Count; i++)")
    L.append("            {")
    L.append("                if (stateInfo.IsTag(_blockedStateTags[i]))")
    L.append("                {")
    L.append("                    return true;")
    L.append("                }")
    L.append("            }")
    L.append("            return false;")
    L.append("        }")

    L.append("    }")
    L.append("}")

    return "\n".join(L) + "\n"


