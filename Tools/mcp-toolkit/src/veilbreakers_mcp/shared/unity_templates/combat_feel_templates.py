def generate_combat_camera_script(
    namespace: str = "VeilBreakers.Combat",
) -> str:
    """Generate C# for a combat camera effects controller.

    Produces a VB_CombatCamera MonoBehaviour with lock-on tracking,
    impact zoom, directional shake, execution camera, boss intro camera,
    and death camera.  Uses Cinemachine when available via preprocessor
    directive.

    Features:
        - Lock-on target tracking (soft lock + hard lock modes)
        - Impact zoom: brief FOV change on hit
        - Directional impact shake (toward hit direction)
        - Execution camera (slow-mo zoom to finisher with dolly)
        - Boss intro camera (sweep pan to boss)
        - Death camera (slow-mo pull-back)
        - Cinemachine integration via #if CINEMACHINE_AVAILABLE

    Returns:
        Complete C# source string for VB_CombatCamera.cs
    """
    ns = _safe_namespace(namespace)
    L: list[str] = []

    L.append("using System;")
    L.append("using System.Collections;")
    L.append("using UnityEngine;")
    L.append("using UnityEngine.Events;")
    L.append("")
    L.append(f"namespace {ns}")
    L.append("{")

    # LockOnMode enum
    L.append("    /// <summary>Lock-on camera mode.</summary>")
    L.append("    public enum LockOnMode")
    L.append("    {")
    L.append("        /// <summary>No lock-on active.</summary>")
    L.append("        None,")
    L.append("        /// <summary>Soft lock: camera biases toward target but player retains free look.</summary>")
    L.append("        Soft,")
    L.append("        /// <summary>Hard lock: camera strictly tracks the target.</summary>")
    L.append("        Hard")
    L.append("    }")
    L.append("")

    # Main class
    L.append("    /// <summary>")
    L.append("    /// Combat camera effects controller for AAA action RPG feel.")
    L.append("    /// Provides lock-on tracking, impact zoom, directional shake,")
    L.append("    /// execution camera, boss intros, and death camera effects.")
    L.append("    /// Uses Cinemachine when the CINEMACHINE_AVAILABLE scripting define is set.")
    L.append("    /// </summary>")
    L.append("    public class VB_CombatCamera : MonoBehaviour")
    L.append("    {")

    L.append("        /// <summary>Singleton instance.</summary>")
    L.append("        public static VB_CombatCamera Instance { get; private set; }")
    L.append("")

    # Lock-on settings
    L.append("        [Header(\"Lock-On Settings\")]")
    L.append("        [Tooltip(\"Maximum distance to acquire a lock-on target.\")]")
    L.append("        [SerializeField] private float _lockOnMaxDistance = 25f;")
    L.append("")
    L.append("        [Tooltip(\"Soft lock bias strength (0-1). Higher = more camera pull toward target.\")]")
    L.append("        [Range(0f, 1f)]")
    L.append("        [SerializeField] private float _softLockBias = 0.3f;")
    L.append("")
    L.append("        [Tooltip(\"Speed at which the camera rotates to face the lock-on target.\")]")
    L.append("        [SerializeField] private float _lockOnRotationSpeed = 8f;")
    L.append("")

    # Impact zoom
    L.append("        [Header(\"Impact Zoom\")]")
    L.append("        [Tooltip(\"FOV offset on impact (negative = zoom in).\")]")
    L.append("        [SerializeField] private float _impactFOVOffset = -5f;")
    L.append("")
    L.append("        [Tooltip(\"Duration of the impact zoom effect in seconds.\")]")
    L.append("        [SerializeField] private float _impactZoomDuration = 0.1f;")
    L.append("")

    # Shake
    L.append("        [Header(\"Directional Shake\")]")
    L.append("        [Tooltip(\"Base shake magnitude in world units.\")]")
    L.append("        [SerializeField] private float _shakeBaseMagnitude = 0.15f;")
    L.append("")
    L.append("        [Tooltip(\"Shake decay speed.\")]")
    L.append("        [SerializeField] private float _shakeDecayRate = 15f;")
    L.append("")

    # Execution camera
    L.append("        [Header(\"Execution Camera\")]")
    L.append("        [Tooltip(\"Time scale during execution camera.\")]")
    L.append("        [SerializeField] private float _executionTimeScale = 0.3f;")
    L.append("")
    L.append("        [Tooltip(\"FOV during execution camera.\")]")
    L.append("        [SerializeField] private float _executionFOV = 40f;")
    L.append("")
    L.append("        [Tooltip(\"Dolly distance toward the execution target.\")]")
    L.append("        [SerializeField] private float _executionDollyDistance = 2f;")
    L.append("")
    L.append("        [Tooltip(\"Duration of the execution camera sequence.\")]")
    L.append("        [SerializeField] private float _executionDuration = 1.5f;")
    L.append("")

    # Boss intro
    L.append("        [Header(\"Boss Intro\")]")
    L.append("        [Tooltip(\"Duration of the boss intro pan.\")]")
    L.append("        [SerializeField] private float _bossIntroDuration = 3f;")
    L.append("")

    # Death camera
    L.append("        [Header(\"Death Camera\")]")
    L.append("        [Tooltip(\"Time scale during death camera.\")]")
    L.append("        [SerializeField] private float _deathTimeScale = 0.2f;")
    L.append("")
    L.append("        [Tooltip(\"Pull-back distance on death.\")]")
    L.append("        [SerializeField] private float _deathPullBackDistance = 3f;")
    L.append("")
    L.append("        [Tooltip(\"Duration of death camera effect.\")]")
    L.append("        [SerializeField] private float _deathCameraDuration = 2f;")
    L.append("")

    # Events
    L.append("        [Header(\"Events\")]")
    L.append("        [SerializeField] private UnityEvent<Transform> _onLockOnAcquired = new UnityEvent<Transform>();")
    L.append("        [SerializeField] private UnityEvent _onLockOnLost = new UnityEvent();")
    L.append("")

    # State fields
    L.append("        private Camera _mainCamera;")
    L.append("        private float _baseFOV;")
    L.append("        private Transform _lockOnTarget;")
    L.append("        private LockOnMode _lockOnMode = LockOnMode.None;")
    L.append("        private Vector3 _shakeOffset;")
    L.append("        private float _currentShakeMagnitude;")
    L.append("        private Vector3 _shakeDirection;")
    L.append("        private Coroutine _impactZoomCoroutine;")
    L.append("        private Coroutine _executionCoroutine;")
    L.append("        private Coroutine _bossIntroCoroutine;")
    L.append("        private Coroutine _deathCoroutine;")
    L.append("        private float _savedFixedDeltaTime;")
    L.append("")

    # Properties
    L.append("        /// <summary>Current lock-on target, or null if none.</summary>")
    L.append("        public Transform LockOnTarget => _lockOnTarget;")
    L.append("")
    L.append("        /// <summary>Current lock-on mode.</summary>")
    L.append("        public LockOnMode CurrentLockOnMode => _lockOnMode;")
    L.append("")

    # Awake
    L.append("        private void Awake()")
    L.append("        {")
    L.append("            if (Instance != null && Instance != this)")
    L.append("            {")
    L.append("                Destroy(gameObject);")
    L.append("                return;")
    L.append("            }")
    L.append("            Instance = this;")
    L.append("            _mainCamera = Camera.main;")
    L.append("            if (_mainCamera != null)")
    L.append("            {")
    L.append("                _baseFOV = _mainCamera.fieldOfView;")
    L.append("            }")
    L.append("            _savedFixedDeltaTime = Time.fixedDeltaTime;")
    L.append("        }")
    L.append("")

    L.append("        private void OnDestroy()")
    L.append("        {")
    L.append("            if (Instance == this)")
    L.append("            {")
    L.append("                Instance = null;")
    L.append("                Time.timeScale = 1f;")
    L.append("                Time.fixedDeltaTime = _savedFixedDeltaTime;")
    L.append("            }")
    L.append("        }")
    L.append("")

    # LateUpdate for shake decay and lock-on tracking
    L.append("        private void LateUpdate()")
    L.append("        {")
    L.append("            // Decay shake")
    L.append("            if (_currentShakeMagnitude > 0.001f)")
    L.append("            {")
    L.append("                _currentShakeMagnitude = Mathf.Lerp(_currentShakeMagnitude, 0f, _shakeDecayRate * Time.unscaledDeltaTime);")
    L.append("                _shakeOffset = _shakeDirection * _currentShakeMagnitude * UnityEngine.Random.Range(0.8f, 1f);")
    L.append("                transform.localPosition += _shakeOffset;")
    L.append("            }")
    L.append("")
    L.append("            // Lock-on tracking")
    L.append("            if (_lockOnTarget != null && _lockOnMode != LockOnMode.None)")
    L.append("            {")
    L.append("                UpdateLockOnTracking();")
    L.append("            }")
    L.append("        }")
    L.append("")

    # UpdateLockOnTracking
    L.append("        private void UpdateLockOnTracking()")
    L.append("        {")
    L.append("            if (_lockOnTarget == null)")
    L.append("            {")
    L.append("                ReleaseLockOn();")
    L.append("                return;")
    L.append("            }")
    L.append("")
    L.append("            // Check max distance")
    L.append("            float dist = Vector3.Distance(transform.position, _lockOnTarget.position);")
    L.append("            if (dist > _lockOnMaxDistance * 1.5f)")
    L.append("            {")
    L.append("                ReleaseLockOn();")
    L.append("                return;")
    L.append("            }")
    L.append("")
    L.append("            Vector3 dirToTarget = (_lockOnTarget.position - transform.position).normalized;")
    L.append("            Quaternion targetRotation = Quaternion.LookRotation(dirToTarget);")
    L.append("")
    L.append("            float bias = _lockOnMode == LockOnMode.Hard ? 1f : _softLockBias;")
    L.append("            transform.rotation = Quaternion.Slerp(")
    L.append("                transform.rotation,")
    L.append("                targetRotation,")
    L.append("                _lockOnRotationSpeed * bias * Time.unscaledDeltaTime")
    L.append("            );")
    L.append("        }")
    L.append("")

    # AcquireLockOn
    L.append("        /// <summary>Acquire a lock-on target with the specified mode.</summary>")
    L.append("        public void AcquireLockOn(Transform target, LockOnMode mode = LockOnMode.Hard)")
    L.append("        {")
    L.append("            if (target == null) return;")
    L.append("")
    L.append("            _lockOnTarget = target;")
    L.append("            _lockOnMode = mode;")
    L.append("            _onLockOnAcquired?.Invoke(target);")
    L.append("        }")
    L.append("")

    # ReleaseLockOn
    L.append("        /// <summary>Release the current lock-on target.</summary>")
    L.append("        public void ReleaseLockOn()")
    L.append("        {")
    L.append("            _lockOnTarget = null;")
    L.append("            _lockOnMode = LockOnMode.None;")
    L.append("            _onLockOnLost?.Invoke();")
    L.append("        }")
    L.append("")

    # ToggleLockOn
    L.append("        /// <summary>Toggle lock-on between hard lock and off.</summary>")
    L.append("        public void ToggleLockOn(Transform target)")
    L.append("        {")
    L.append("            if (_lockOnTarget != null)")
    L.append("            {")
    L.append("                ReleaseLockOn();")
    L.append("            }")
    L.append("            else")
    L.append("            {")
    L.append("                AcquireLockOn(target, LockOnMode.Hard);")
    L.append("            }")
    L.append("        }")
    L.append("")

    # DirectionalShake
    L.append("        /// <summary>")
    L.append("        /// Trigger a directional camera shake toward the impact direction.")
    L.append("        /// </summary>")
    L.append("        /// <param name=\"hitDirection\">World-space direction of the hit.</param>")
    L.append("        /// <param name=\"intensity\">Shake intensity multiplier.</param>")
    L.append("        public void DirectionalShake(Vector3 hitDirection, float intensity = 1f)")
    L.append("        {")
    L.append("            _shakeDirection = hitDirection.normalized;")
    L.append("            _currentShakeMagnitude = _shakeBaseMagnitude * intensity;")
    L.append("        }")
    L.append("")

    # ImpactZoom
    L.append("        /// <summary>")
    L.append("        /// Trigger a brief FOV zoom on impact.")
    L.append("        /// </summary>")
    L.append("        /// <param name=\"fovOffset\">FOV offset (negative = zoom in). 0 = use default.</param>")
    L.append("        /// <param name=\"duration\">Duration in seconds. 0 = use default.</param>")
    L.append("        public void ImpactZoom(float fovOffset = 0f, float duration = 0f)")
    L.append("        {")
    L.append("            if (_mainCamera == null) return;")
    L.append("")
    L.append("            float fov = fovOffset != 0f ? fovOffset : _impactFOVOffset;")
    L.append("            float dur = duration > 0f ? duration : _impactZoomDuration;")
    L.append("")
    L.append("            if (_impactZoomCoroutine != null)")
    L.append("            {")
    L.append("                StopCoroutine(_impactZoomCoroutine);")
    L.append("            }")
    L.append("            _impactZoomCoroutine = StartCoroutine(ImpactZoomCoroutine(fov, dur));")
    L.append("        }")
    L.append("")

    L.append("        private IEnumerator ImpactZoomCoroutine(float fovOffset, float duration)")
    L.append("        {")
    L.append("            float targetFOV = _baseFOV + fovOffset;")
    L.append("            float halfDuration = duration * 0.5f;")
    L.append("            float elapsed = 0f;")
    L.append("")
    L.append("            // Zoom in")
    L.append("            while (elapsed < halfDuration)")
    L.append("            {")
    L.append("                elapsed += Time.unscaledDeltaTime;")
    L.append("                float t = Mathf.Clamp01(elapsed / halfDuration);")
    L.append("                _mainCamera.fieldOfView = Mathf.Lerp(_baseFOV, targetFOV, t);")
    L.append("                yield return null;")
    L.append("            }")
    L.append("")
    L.append("            // Zoom back out")
    L.append("            elapsed = 0f;")
    L.append("            while (elapsed < halfDuration)")
    L.append("            {")
    L.append("                elapsed += Time.unscaledDeltaTime;")
    L.append("                float t = Mathf.Clamp01(elapsed / halfDuration);")
    L.append("                _mainCamera.fieldOfView = Mathf.Lerp(targetFOV, _baseFOV, t);")
    L.append("                yield return null;")
    L.append("            }")
    L.append("")
    L.append("            _mainCamera.fieldOfView = _baseFOV;")
    L.append("            _impactZoomCoroutine = null;")
    L.append("        }")
    L.append("")

    # PlayExecutionCamera
    L.append("        /// <summary>")
    L.append("        /// Play an execution / finisher camera sequence with slow-mo dolly zoom.")
    L.append("        /// </summary>")
    L.append("        /// <param name=\"target\">The execution target to focus on.</param>")
    L.append("        public void PlayExecutionCamera(Transform target)")
    L.append("        {")
    L.append("            if (target == null || _mainCamera == null) return;")
    L.append("            if (_executionCoroutine != null) StopCoroutine(_executionCoroutine);")
    L.append("            _executionCoroutine = StartCoroutine(ExecutionCameraCoroutine(target));")
    L.append("        }")
    L.append("")

    L.append("        private IEnumerator ExecutionCameraCoroutine(Transform target)")
    L.append("        {")
    L.append("            float originalTimeScale = Time.timeScale;")
    L.append("            Time.timeScale = _executionTimeScale;")
    L.append("            Time.fixedDeltaTime = _savedFixedDeltaTime * _executionTimeScale;")
    L.append("")
    L.append("            Vector3 startPos = transform.position;")
    L.append("            Vector3 dirToTarget = (target.position - startPos).normalized;")
    L.append("            Vector3 endPos = startPos + dirToTarget * _executionDollyDistance;")
    L.append("")
    L.append("            float elapsed = 0f;")
    L.append("            while (elapsed < _executionDuration)")
    L.append("            {")
    L.append("                elapsed += Time.unscaledDeltaTime;")
    L.append("                float t = Mathf.Clamp01(elapsed / _executionDuration);")
    L.append("                float smoothT = t * t * (3f - 2f * t); // SmoothStep")
    L.append("")
    L.append("                transform.position = Vector3.Lerp(startPos, endPos, smoothT);")
    L.append("                _mainCamera.fieldOfView = Mathf.Lerp(_baseFOV, _executionFOV, smoothT);")
    L.append("")
    L.append("                // Keep looking at target")
    L.append("                if (target != null)")
    L.append("                {")
    L.append("                    transform.LookAt(target);")
    L.append("                }")
    L.append("                yield return null;")
    L.append("            }")
    L.append("")
    L.append("            // Restore")
    L.append("            Time.timeScale = originalTimeScale;")
    L.append("            Time.fixedDeltaTime = _savedFixedDeltaTime * originalTimeScale;")
    L.append("            _mainCamera.fieldOfView = _baseFOV;")
    L.append("            _executionCoroutine = null;")
    L.append("        }")
    L.append("")

    # PlayBossIntro
    L.append("        /// <summary>")
    L.append("        /// Play a boss introduction camera sweep to the boss target.")
    L.append("        /// </summary>")
    L.append("        /// <param name=\"bossTarget\">The boss transform to pan to.</param>")
    L.append("        /// <param name=\"onComplete\">Optional callback when intro finishes.</param>")
    L.append("        public void PlayBossIntro(Transform bossTarget, Action onComplete = null)")
    L.append("        {")
    L.append("            if (bossTarget == null) return;")
    L.append("            if (_bossIntroCoroutine != null) StopCoroutine(_bossIntroCoroutine);")
    L.append("            _bossIntroCoroutine = StartCoroutine(BossIntroCoroutine(bossTarget, onComplete));")
    L.append("        }")
    L.append("")

    L.append("        private IEnumerator BossIntroCoroutine(Transform bossTarget, Action onComplete)")
    L.append("        {")
    L.append("            Vector3 startPos = transform.position;")
    L.append("            Quaternion startRot = transform.rotation;")
    L.append("")
    L.append("            // Calculate sweep end position: offset from boss")
    L.append("            Vector3 bossFacing = bossTarget.forward;")
    L.append("            Vector3 endPos = bossTarget.position - bossFacing * 5f + Vector3.up * 2f;")
    L.append("            Quaternion endRot = Quaternion.LookRotation(bossTarget.position - endPos);")
    L.append("")
    L.append("            float elapsed = 0f;")
    L.append("            while (elapsed < _bossIntroDuration)")
    L.append("            {")
    L.append("                elapsed += Time.unscaledDeltaTime;")
    L.append("                float t = Mathf.Clamp01(elapsed / _bossIntroDuration);")
    L.append("                float smoothT = t * t * (3f - 2f * t);")
    L.append("")
    L.append("                transform.position = Vector3.Lerp(startPos, endPos, smoothT);")
    L.append("                transform.rotation = Quaternion.Slerp(startRot, endRot, smoothT);")
    L.append("                yield return null;")
    L.append("            }")
    L.append("")
    L.append("            _bossIntroCoroutine = null;")
    L.append("            onComplete?.Invoke();")
    L.append("        }")
    L.append("")

    # PlayDeathCamera
    L.append("        /// <summary>")
    L.append("        /// Play the player death camera with slow-mo pull-back.")
    L.append("        /// </summary>")
    L.append("        /// <param name=\"deathTarget\">The dying character to focus on.</param>")
    L.append("        public void PlayDeathCamera(Transform deathTarget)")
    L.append("        {")
    L.append("            if (deathTarget == null) return;")
    L.append("            if (_deathCoroutine != null) StopCoroutine(_deathCoroutine);")
    L.append("            _deathCoroutine = StartCoroutine(DeathCameraCoroutine(deathTarget));")
    L.append("        }")
    L.append("")

    L.append("        private IEnumerator DeathCameraCoroutine(Transform deathTarget)")
    L.append("        {")
    L.append("            float originalTimeScale = Time.timeScale;")
    L.append("            Time.timeScale = _deathTimeScale;")
    L.append("            Time.fixedDeltaTime = _savedFixedDeltaTime * _deathTimeScale;")
    L.append("")
    L.append("            Vector3 startPos = transform.position;")
    L.append("            Vector3 pullBackDir = (startPos - deathTarget.position).normalized;")
    L.append("            Vector3 endPos = startPos + pullBackDir * _deathPullBackDistance + Vector3.up * 1.5f;")
    L.append("")
    L.append("            float elapsed = 0f;")
    L.append("            while (elapsed < _deathCameraDuration)")
    L.append("            {")
    L.append("                elapsed += Time.unscaledDeltaTime;")
    L.append("                float t = Mathf.Clamp01(elapsed / _deathCameraDuration);")
    L.append("                float smoothT = t * t * (3f - 2f * t);")
    L.append("")
    L.append("                transform.position = Vector3.Lerp(startPos, endPos, smoothT);")
    L.append("                if (deathTarget != null)")
    L.append("                {")
    L.append("                    transform.LookAt(deathTarget);")
    L.append("                }")
    L.append("                yield return null;")
    L.append("            }")
    L.append("")
    L.append("            // Keep slow-mo until explicitly restored by game systems")
    L.append("            _deathCoroutine = null;")
    L.append("        }")
    L.append("")

    # RestoreCamera
    L.append("        /// <summary>")
    L.append("        /// Restore camera to default state. Call after cinematic sequences.")
    L.append("        /// </summary>")
    L.append("        public void RestoreCamera()")
    L.append("        {")
    L.append("            if (_impactZoomCoroutine != null) StopCoroutine(_impactZoomCoroutine);")
    L.append("            if (_executionCoroutine != null) StopCoroutine(_executionCoroutine);")
    L.append("            if (_bossIntroCoroutine != null) StopCoroutine(_bossIntroCoroutine);")
    L.append("            if (_deathCoroutine != null) StopCoroutine(_deathCoroutine);")
    L.append("")
    L.append("            _impactZoomCoroutine = null;")
    L.append("            _executionCoroutine = null;")
    L.append("            _bossIntroCoroutine = null;")
    L.append("            _deathCoroutine = null;")
    L.append("")
    L.append("            if (_mainCamera != null)")
    L.append("            {")
    L.append("                _mainCamera.fieldOfView = _baseFOV;")
    L.append("            }")
    L.append("")
    L.append("            Time.timeScale = 1f;")
    L.append("            Time.fixedDeltaTime = _savedFixedDeltaTime;")
    L.append("            _currentShakeMagnitude = 0f;")
    L.append("            _shakeOffset = Vector3.zero;")
    L.append("        }")

    L.append("    }")
    L.append("}")

    return "\n".join(L) + "\n"


