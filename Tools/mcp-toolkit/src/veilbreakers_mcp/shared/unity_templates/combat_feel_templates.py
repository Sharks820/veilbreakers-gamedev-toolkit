    L.append("        [Header(\"Dodge Timing\")]")
    L.append("        [Tooltip(\"Delay before i-frames begin (wind-up).\")]")
    L.append("        [SerializeField] private float _iFrameStart = 0.05f;")
    L.append("")
    L.append("        [Tooltip(\"Duration of the invincibility window.\")]")
    L.append("        [SerializeField] private float _iFrameDuration = 0.3f;")
    L.append("")
    L.append("        [Tooltip(\"Recovery time after i-frames end before dodge completes.\")]")
    L.append("        [SerializeField] private float _recoveryDuration = 0.15f;")
    L.append("")
    L.append("        [Tooltip(\"Cooldown before another dodge can be performed.\")]")
    L.append("        [SerializeField] private float _dodgeCooldown = 0.5f;")
    L.append("")

    # Movement
    L.append("        [Header(\"Movement\")]")
    L.append("        [Tooltip(\"Distance covered during the dodge.\")]")
    L.append("        [SerializeField] private float _dodgeDistance = 4f;")
    L.append("")
    L.append("        [Tooltip(\"Speed curve over the dodge duration (0=start, 1=end).\")]")
    L.append("        [SerializeField] private AnimationCurve _dodgeSpeedCurve = AnimationCurve.EaseInOut(0f, 1f, 1f, 0f);")
    L.append("")
    L.append("        [Tooltip(\"Direction snap mode.\")]")
    L.append("        [SerializeField] private DirectionSnapMode _snapMode = DirectionSnapMode.Free;")
    L.append("")

    # Stamina
    L.append("        [Header(\"Stamina Integration\")]")
    L.append("        [Tooltip(\"Stamina cost per dodge. Set to 0 to disable stamina check.\")]")
    L.append("        [SerializeField] private float _staminaCost = 20f;")
    L.append("")

    # Layer
    L.append("        [Header(\"Invincibility\")]")
    L.append("        [Tooltip(\"Layer to switch to during i-frames (e.g. 'Invincible' layer).\")]")
    L.append("        [SerializeField] private string _invincibleLayer = \"\";")
    L.append("")

    # Events
    L.append("        [Header(\"Events\")]")
    L.append("        [SerializeField] private UnityEvent _onDodgeStart = new UnityEvent();")
    L.append("        [SerializeField] private UnityEvent _onInvincibleStart = new UnityEvent();")
    L.append("        [SerializeField] private UnityEvent _onInvincibleEnd = new UnityEvent();")
    L.append("        [SerializeField] private UnityEvent _onDodgeEnd = new UnityEvent();")
    L.append("        [Tooltip(\"Fired during i-frames for trail VFX.\")]")
    L.append("        [SerializeField] private UnityEvent<bool> _onTrailVFX = new UnityEvent<bool>();")
    L.append("")
    L.append("        [Tooltip(\"Stamina check callback. Return true if enough stamina. If null, always allows dodge.\")]")
    L.append("        public Func<float, bool> StaminaCheck;")
    L.append("")
    L.append("        [Tooltip(\"Stamina consume callback. Called when dodge starts.\")]")
    L.append("        public Action<float> StaminaConsume;")
    L.append("")

    # State
    L.append("        private DodgeState _state = DodgeState.Ready;")
    L.append("        private CharacterController _characterController;")
    L.append("        private Rigidbody _rigidbody;")
    L.append("        private Animator _animator;")
    L.append("        private int _originalLayer;")
    L.append("        private Coroutine _dodgeCoroutine;")
    L.append("")

    # Properties
    L.append("        /// <summary>Current dodge state.</summary>")
    L.append("        public DodgeState State => _state;")
    L.append("")
    L.append("        /// <summary>Whether the character is currently invincible.</summary>")
    L.append("        public bool IsInvincible => _state == DodgeState.Invincible;")
