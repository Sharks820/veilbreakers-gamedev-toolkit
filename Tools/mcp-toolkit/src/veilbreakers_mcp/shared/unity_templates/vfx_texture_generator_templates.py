"""Procedural particle texture generator C# template for Unity.

Generates a static utility class ``VBProceduralParticleTextures`` that creates
high-quality particle textures entirely from code at runtime -- no external
assets required.  Textures are cached on first access so subsequent calls are
free.

Texture types (10):
    smoke, spark, soft_glow, magic_rune, blood_splatter,
    lightning, energy_orb, distortion_normal, ground_crack, dissolve_mask

Also includes two material factory helpers:
    CreateAdditiveMaterial  -- additive blend (sparks, glows, lightning)
    CreateAlphaBlendMaterial -- alpha blend (smoke, blood, cracks)

All noise algorithms (Perlin, Voronoi) are implemented inline in C# so
the class has zero external dependencies beyond Unity 2022.3 URP.

Each function returns ``{"script_path", "script_content", "next_steps"}``.
"""

