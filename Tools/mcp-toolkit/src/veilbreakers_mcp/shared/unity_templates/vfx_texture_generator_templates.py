_CS_VORONOI = r'''
        // =============================================================
        // Voronoi distance helpers
        // =============================================================

        /// <summary>
        /// Returns the distance to the nearest Voronoi cell edge (F2-F1).
        /// Useful for crack and splatter patterns.
        /// </summary>
        private static float VoronoiEdge(float x, float y, float cellScale)
        {
            x *= cellScale;
            y *= cellScale;
            int ix = (int)Mathf.Floor(x);
            int iy = (int)Mathf.Floor(y);
            float d1 = float.MaxValue;
            float d2 = float.MaxValue;

            for (int ox = -1; ox <= 1; ox++)
            {
                for (int oy = -1; oy <= 1; oy++)
                {
                    int cx = ix + ox;
                    int cy = iy + oy;
                    // Deterministic hash for cell point
                    float hx = Frac(Mathf.Sin(cx * 127.1f + cy * 311.7f) * 43758.5453f);
                    float hy = Frac(Mathf.Sin(cx * 269.5f + cy * 183.3f) * 43758.5453f);
                    float px = cx + hx;
                    float py = cy + hy;
                    float dist = (px - x) * (px - x) + (py - y) * (py - y);
                    if (dist < d1) { d2 = d1; d1 = dist; }
                    else if (dist < d2) { d2 = dist; }
                }
            }
            return Mathf.Sqrt(d2) - Mathf.Sqrt(d1);
        }

        /// <summary>Returns distance to the nearest Voronoi cell center (F1).</summary>
        private static float VoronoiF1(float x, float y, float cellScale)
        {
            x *= cellScale;
            y *= cellScale;
            int ix = (int)Mathf.Floor(x);
            int iy = (int)Mathf.Floor(y);
            float d1 = float.MaxValue;
            for (int ox = -1; ox <= 1; ox++)
            {
                for (int oy = -1; oy <= 1; oy++)
                {
                    int cx = ix + ox;
                    int cy = iy + oy;
                    float hx = Frac(Mathf.Sin(cx * 127.1f + cy * 311.7f) * 43758.5453f);
                    float hy = Frac(Mathf.Sin(cx * 269.5f + cy * 183.3f) * 43758.5453f);
                    float px = cx + hx;
                    float py = cy + hy;
                    float dist = (px - x) * (px - x) + (py - y) * (py - y);
                    if (dist < d1) d1 = dist;
                }
            }
            return Mathf.Sqrt(d1);
        }

        private static float Frac(float v) => v - Mathf.Floor(v);

        /// <summary>Seeded hash for deterministic random per pixel.</summary>
        private static float Hash(float x, float y)
        {
            return Frac(Mathf.Sin(x * 12.9898f + y * 78.233f) * 43758.5453f);
        }

'''

