def generate_procedural_particle_textures_script() -> dict:
    """Return C# script dict for ``VBProceduralParticleTextures``.

    The generated class lives under ``Assets/Scripts/VFX/`` (runtime, not
    Editor) so any gameplay script can call
    ``VBProceduralParticleTextures.GetTexture("smoke")`` at runtime.
    """

    script = _CS_HEADER + _CS_CACHE + _CS_NOISE + _CS_VORONOI + _CS_TEXTURES + _CS_MATERIALS + _CS_FOOTER

    return {
        "script_path": "Assets/Scripts/VFX/VBProceduralParticleTextures.cs",
        "script_content": script,
        "next_steps": [
            "Open Unity Editor and wait for compilation",
            "Call VBProceduralParticleTextures.GetTexture(\"smoke\") from any script",
            "Or use CreateAdditiveMaterial / CreateAlphaBlendMaterial helpers",
            "Textures are generated once on first access and cached automatically",
        ],
    }


