    lines.append("            while (currentXP >= XPToNextLevel && currentLevel < maxLevel)")
    lines.append("            {")
    lines.append("                currentXP -= XPToNextLevel;")
    lines.append("                LevelUp();")
    lines.append("            }")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Process a level-up: increment level, grant stat points, fire event.")
    lines.append("        /// </summary>")
    lines.append("        private void LevelUp()")
    lines.append("        {")
    lines.append("            currentLevel++;")
    lines.append("")
    lines.append("            // Per-path stat scaling")
    lines.append("            ApplyPathStatBonus(heroPath);")
    lines.append("")
    lines.append("            // Grant manual stat points")
    lines.append("            unspentStatPoints += 3;")
    lines.append("")
    lines.append("            // Fire EventBus event")
    lines.append("            EventBus.HeroLevelUp(heroId, currentLevel);")
    lines.append("")
    lines.append("            Debug.Log($\"[XP] {heroId} leveled up to {currentLevel}!\");")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Apply automatic stat bonuses based on Path.")
    lines.append("        /// Different paths favor different stats.")
    lines.append("        /// </summary>")
