"""VeilBreakers-specific combat system C# template generators.

Each function returns a complete C# source string for a runtime MonoBehaviour
or static utility class. These are placed in the Unity project's
Assets/Scripts/Runtime/ directory -- they are NOT editor scripts and must
NEVER reference the UnityEditor namespace.

CRITICAL DESIGN PRINCIPLE: These generators produce WIRING code --
MonoBehaviours that call into existing static utility classes. They do NOT
reimplement brand effectiveness matrices, synergy calculations, corruption
formulas, or damage pipelines. The generated code references existing
VeilBreakers classes by name:
  - BrandSystem.GetEffectiveness()
  - SynergySystem.GetSynergyTier() / GetDamageBonus()
  - CorruptionSystem.GetStatMultiplier() / GetCorruptionState()
  - DamageCalculator.Calculate()
  - EventBus (various events)

Exports:
    generate_player_combat_script      -- VB-01: Player combat controller with FSM
    generate_ability_system_script     -- VB-02: Brand-specific ability system
    generate_synergy_engine_script     -- VB-03: Synergy wiring (delegates to SynergySystem)
    generate_corruption_gameplay_script -- VB-04: Corruption effects (delegates to CorruptionSystem)
    generate_xp_leveling_script        -- VB-05: XP/leveling with EventBus integration
    generate_currency_system_script    -- VB-06: Multi-currency system
    generate_damage_type_script        -- VB-07: Brand-specific damage types (delegates to BrandSystem)
"""

from __future__ import annotations

import re

from ._cs_sanitize import sanitize_cs_string


# ---------------------------------------------------------------------------
# C# reserved words
# ---------------------------------------------------------------------------

_CS_RESERVED = frozenset({
    "abstract", "as", "base", "bool", "break", "byte", "case", "catch", "char",
    "checked", "class", "const", "continue", "decimal", "default", "delegate",
    "do", "double", "else", "enum", "event", "explicit", "extern", "false",
    "finally", "fixed", "float", "for", "foreach", "goto", "if", "implicit",
    "in", "int", "interface", "internal", "is", "lock", "long", "namespace",
    "new", "null", "object", "operator", "out", "override", "params", "private",
    "protected", "public", "readonly", "ref", "return", "sbyte", "sealed",
    "short", "sizeof", "stackalloc", "static", "string", "struct", "switch",
    "this", "throw", "true", "try", "typeof", "uint", "ulong", "unchecked",
    "unsafe", "ushort", "using", "virtual", "void", "volatile", "while",
})


def _safe_namespace(ns: str) -> str:
    """Sanitize a C# namespace to prevent code injection.

    Valid C# namespaces allow only letters, digits, underscores, and dots.
    Strips everything else. Segments starting with a digit get a ``_``
    prefix, and segments that are C# reserved words get an ``@`` prefix.

    Args:
        ns: Raw namespace string.

    Returns:
        Sanitized namespace string safe for C# ``namespace`` declarations.
    """
    sanitized = re.sub(r"[^a-zA-Z0-9_.]", "", ns)
    # Strip leading/trailing dots and collapse consecutive dots
    sanitized = re.sub(r"\.{2,}", ".", sanitized).strip(".")
    if not sanitized:
        return "Generated"
    # Validate each segment: fix leading-digit and reserved-word segments
    segments = sanitized.split(".")
    fixed: list[str] = []
    for seg in segments:
        if not seg:
            continue
        if seg[0].isdigit():
            seg = f"_{seg}"
        if seg in _CS_RESERVED:
            seg = f"@{seg}"
        fixed.append(seg)
    return ".".join(fixed) or "Generated"


# ---------------------------------------------------------------------------
# VB-01: Player combat controller (FSM with combos, dodge, block)
# ---------------------------------------------------------------------------


def generate_player_combat_script(
    light_combo_count: int = 3,
    heavy_combo_count: int = 2,
    dodge_iframe_duration: float = 0.2,
    dodge_distance: float = 4.0,
    block_stamina_drain: float = 10.0,
    stamina_max: float = 100.0,
    stamina_regen_rate: float = 15.0,
    namespace: str = "VeilBreakers.Combat",
    combat_mode: str = "realtime",
) -> str:
    """Generate C# runtime MonoBehaviour for a player combat controller.

    NOTE: This generates a REAL-TIME action combat controller for overworld/exploration combat.
    VeilBreakers also has a TURN-BASED battle system (BattleManager/Combatant) for structured encounters.
    This controller is designed for the overworld, NOT as a replacement for the existing turn-based system.
    Use this for real-time overworld encounters; the existing BattleManager handles structured battles.

    When ``combat_mode`` is ``"turnbased"``, generates a turn-based combat wrapper
    (VB_TurnBasedCombat) that delegates to BattleManager instead of the real-time FSM.

    Produces an FSM-based combat system with light/heavy attack combos,
    dodge with i-frames, block with stamina drain, hit reactions, and
    stamina management. Integrates with existing Combatant, DamageCalculator,
    and EventBus systems.

    Args:
        light_combo_count: Number of hits in the light attack combo chain.
        heavy_combo_count: Number of hits in the heavy attack combo chain.
        dodge_iframe_duration: Duration of invincibility frames during dodge.
        dodge_distance: Distance covered by a dodge roll.
        block_stamina_drain: Stamina consumed per blocked hit.
        stamina_max: Maximum stamina pool.
        stamina_regen_rate: Stamina regenerated per second when idle.
        namespace: C# namespace for the generated class.
        combat_mode: ``"realtime"`` (default) for overworld action combat,
            or ``"turnbased"`` for structured turn-based encounters.

    Returns:
        Complete C# source string.
    """
    if combat_mode == "turnbased":
        return _generate_turnbased_combat_script(namespace=namespace)

    lines = []
    lines.append("using UnityEngine;")
    lines.append("using VeilBreakers.Combat;")
    lines.append("using VeilBreakers.Core;")
    lines.append("using VeilBreakers.Systems;")
    lines.append("using VeilBreakers.Data;")
    lines.append("")
    lines.append("namespace " + _safe_namespace(namespace))
    lines.append("{")
    lines.append("    /// <summary>")
    lines.append("    /// Player combat controller with FSM state machine.")
    lines.append("    /// Supports light/heavy attack combos, dodge with i-frames,")
    lines.append("    /// block with stamina drain, and hit reactions.")
    lines.append("    /// Generated by VeilBreakers MCP toolkit.")
    lines.append("    /// </summary>")
    lines.append("    public class VB_PlayerCombat : MonoBehaviour")
    lines.append("    {")
    lines.append("        /// <summary>Combat state machine states.</summary>")
    lines.append("        public enum CombatState")
    lines.append("        {")
    lines.append("            Idle,")
    lines.append("            LightAttack,")
    lines.append("            HeavyAttack,")
    lines.append("            Dodge,")
    lines.append("            Block,")
    lines.append("            HitReaction,")
    lines.append("            Dead")
    lines.append("        }")
    lines.append("")
    lines.append("        [Header(\"Combo Settings\")]")
    lines.append("        public int lightComboCount = " + str(light_combo_count) + ";")
    lines.append("        public int heavyComboCount = " + str(heavy_combo_count) + ";")
    lines.append("        public float comboWindowDuration = 0.5f;")
    lines.append("        public float heavyDamageMultiplier = 1.75f;")
    lines.append("")
    lines.append("        [Header(\"Dodge\")]")
    lines.append("        public float dodgeIFrameDuration = " + str(dodge_iframe_duration) + "f;")
    lines.append("        public float dodgeDistance = " + str(dodge_distance) + "f;")
    lines.append("        public float dodgeDuration = 0.4f;")
    lines.append("        public float dodgeStaminaCost = 20f;")
    lines.append("")
    lines.append("        [Header(\"Block\")]")
    lines.append("        public float blockDamageReduction = 0.5f;")
    lines.append("        public float blockStaminaDrain = " + str(block_stamina_drain) + "f;")
    lines.append("")
    lines.append("        [Header(\"Stamina\")]")
    lines.append("        public float staminaMax = " + str(stamina_max) + "f;")
    lines.append("        public float staminaCurrent;")
    lines.append("        public float staminaRegenRate = " + str(stamina_regen_rate) + "f;")
    lines.append("        public float staminaRegenDelay = 1.0f;")
    lines.append("")
    lines.append("        [Header(\"Hit Reaction\")]")
    lines.append("        public float hitReactionDuration = 0.3f;")
    lines.append("")
    lines.append("        // Runtime state")
    lines.append("        private CombatState _currentState = CombatState.Idle;")
    lines.append("        private int _currentComboIndex = 0;")
    lines.append("        private float _comboTimer = 0f;")
    lines.append("        private float _stateTimer = 0f;")
    lines.append("        private float _staminaRegenTimer = 0f;")
    lines.append("        private bool _isInvincible = false;")
    lines.append("        private float _iFrameTimer = 0f;")
    lines.append("")
    lines.append("        private Vector3 _dodgeDirection;")
    lines.append("        private float _dodgeSpeed;")
    lines.append("")
    lines.append("        // Cached components")
    lines.append("        private Combatant _combatant;")
    lines.append("        private Animator _animator;")
    lines.append("        private CharacterController _characterController;")
    lines.append("")
    lines.append("        /// <summary>Current combat state (read-only).</summary>")
    lines.append("        public CombatState CurrentState => _currentState;")
    lines.append("")
    lines.append("        /// <summary>Whether the player has active i-frames.</summary>")
    lines.append("        public bool IsInvincible => _isInvincible;")
    lines.append("")
    lines.append("        private void Start()")
    lines.append("        {")
    lines.append("            _combatant = GetComponent<Combatant>();")
    lines.append("            if (_combatant == null)")
    lines.append("                Debug.LogWarning(\"[VB_PlayerCombat] No Combatant component found on \" + gameObject.name + \". Combat damage will not function.\");")
    lines.append("            _animator = GetComponent<Animator>();")
    lines.append("            _characterController = GetComponent<CharacterController>();")
    lines.append("            staminaCurrent = staminaMax;")
    lines.append("        }")
    lines.append("")
    lines.append("        private void Update()")
    lines.append("        {")
    lines.append("            UpdateIFrames();")
    lines.append("            UpdateStamina();")
    lines.append("")
    lines.append("            switch (_currentState)")
    lines.append("            {")
    lines.append("                case CombatState.Idle:")
    lines.append("                    HandleIdleState();")
    lines.append("                    break;")
    lines.append("                case CombatState.LightAttack:")
    lines.append("                    HandleLightAttackState();")
    lines.append("                    break;")
    lines.append("                case CombatState.HeavyAttack:")
    lines.append("                    HandleHeavyAttackState();")
    lines.append("                    break;")
    lines.append("                case CombatState.Dodge:")
    lines.append("                    HandleDodgeState();")
    lines.append("                    break;")
    lines.append("                case CombatState.Block:")
    lines.append("                    HandleBlockState();")
    lines.append("                    break;")
    lines.append("                case CombatState.HitReaction:")
    lines.append("                    HandleHitReactionState();")
    lines.append("                    break;")
    lines.append("                case CombatState.Dead:")
    lines.append("                    break;")
    lines.append("            }")
    lines.append("        }")
    lines.append("")
    lines.append("        // =================================================================")
    lines.append("        // State handlers")
    lines.append("        // =================================================================")
    lines.append("")
    lines.append("        private void HandleIdleState()")
    lines.append("        {")
    lines.append("            // Combo timeout reset")
    lines.append("            if (_comboTimer > 0f)")
    lines.append("            {")
    lines.append("                _comboTimer -= Time.deltaTime;")
    lines.append("                if (_comboTimer <= 0f)")
    lines.append("                    _currentComboIndex = 0;")
    lines.append("            }")
    lines.append("")
    lines.append("            if (GetDodgeInput() && staminaCurrent >= dodgeStaminaCost)")
    lines.append("                EnterDodge();")
    lines.append("            else if (GetBlockInput())")
    lines.append("                EnterBlock();")
    lines.append("            else if (GetHeavyAttackInput())")
    lines.append("                EnterHeavyAttack();")
    lines.append("            else if (GetLightAttackInput())")
    lines.append("                EnterLightAttack();")
    lines.append("        }")
    lines.append("")
    lines.append("        private void HandleLightAttackState()")
    lines.append("        {")
    lines.append("            _stateTimer -= Time.deltaTime;")
    lines.append("            if (_stateTimer <= 0f)")
    lines.append("            {")
    lines.append("                // Allow combo chain input")
    lines.append("                if (GetLightAttackInput() && _currentComboIndex < lightComboCount)")
    lines.append("                {")
    lines.append("                    _currentComboIndex++;")
    lines.append("                    PerformLightAttack();")
    lines.append("                }")
    lines.append("                else")
    lines.append("                {")
    lines.append("                    _comboTimer = comboWindowDuration;")
    lines.append("                    TransitionTo(CombatState.Idle);")
    lines.append("                }")
    lines.append("            }")
    lines.append("        }")
    lines.append("")
    lines.append("        private void HandleHeavyAttackState()")
    lines.append("        {")
    lines.append("            _stateTimer -= Time.deltaTime;")
    lines.append("            if (_stateTimer <= 0f)")
    lines.append("            {")
    lines.append("                if (GetHeavyAttackInput() && _currentComboIndex < heavyComboCount)")
    lines.append("                {")
    lines.append("                    _currentComboIndex++;")
    lines.append("                    PerformHeavyAttack();")
    lines.append("                }")
    lines.append("                else")
    lines.append("                {")
    lines.append("                    _comboTimer = comboWindowDuration;")
    lines.append("                    TransitionTo(CombatState.Idle);")
    lines.append("                }")
    lines.append("            }")
    lines.append("        }")
    lines.append("")
    lines.append("        private void HandleDodgeState()")
    lines.append("        {")
    lines.append("            _stateTimer -= Time.deltaTime;")
    lines.append("")
    lines.append("            // Spread dodge movement across frames")
    lines.append("            if (_characterController != null)")
    lines.append("                _characterController.Move(_dodgeDirection * _dodgeSpeed * Time.deltaTime);")
    lines.append("")
    lines.append("            if (_stateTimer <= 0f)")
    lines.append("                TransitionTo(CombatState.Idle);")
    lines.append("        }")
    lines.append("")
    lines.append("        private void HandleBlockState()")
    lines.append("        {")
    lines.append("            if (!GetBlockInput() || staminaCurrent <= 0f)")
    lines.append("                TransitionTo(CombatState.Idle);")
    lines.append("        }")
    lines.append("")
    lines.append("        private void HandleHitReactionState()")
    lines.append("        {")
    lines.append("            _stateTimer -= Time.deltaTime;")
    lines.append("            if (_stateTimer <= 0f)")
    lines.append("                TransitionTo(CombatState.Idle);")
    lines.append("        }")
    lines.append("")
    lines.append("        // =================================================================")
    lines.append("        // State transitions")
    lines.append("        // =================================================================")
    lines.append("")
    lines.append("        private void EnterLightAttack()")
    lines.append("        {")
    lines.append("            _currentComboIndex = 1;")
    lines.append("            TransitionTo(CombatState.LightAttack);")
    lines.append("            PerformLightAttack();")
    lines.append("        }")
    lines.append("")
    lines.append("        private void EnterHeavyAttack()")
    lines.append("        {")
    lines.append("            _currentComboIndex = 1;")
    lines.append("            TransitionTo(CombatState.HeavyAttack);")
    lines.append("            PerformHeavyAttack();")
    lines.append("        }")
    lines.append("")
    lines.append("        private void EnterDodge()")
    lines.append("        {")
    lines.append("            staminaCurrent -= dodgeStaminaCost;")
    lines.append("            _staminaRegenTimer = staminaRegenDelay;")
    lines.append("            _isInvincible = true;")
    lines.append("            _iFrameTimer = dodgeIFrameDuration;")
    lines.append("            _stateTimer = dodgeDuration;")
    lines.append("            TransitionTo(CombatState.Dodge);")
    lines.append("")
    lines.append("            // Compute dodge direction and speed for gradual movement")
    lines.append("            _dodgeDirection = GetMoveDirection();")
    lines.append("            if (_dodgeDirection.sqrMagnitude < 0.01f)")
    lines.append("                _dodgeDirection = transform.forward;")
    lines.append("            _dodgeDirection = _dodgeDirection.normalized;")
    lines.append("            _dodgeSpeed = dodgeDuration > 0f ? dodgeDistance / dodgeDuration : dodgeDistance;")
    lines.append("")
    lines.append("            if (_animator != null)")
    lines.append("                _animator.SetTrigger(\"Dodge\");")
    lines.append("        }")
    lines.append("")
    lines.append("        private void EnterBlock()")
    lines.append("        {")
    lines.append("            TransitionTo(CombatState.Block);")
    lines.append("            if (_animator != null)")
    lines.append("                _animator.SetBool(\"IsBlocking\", true);")
    lines.append("        }")
    lines.append("")
    lines.append("        private void TransitionTo(CombatState newState)")
    lines.append("        {")
    lines.append("            if (_currentState == CombatState.Block && _animator != null)")
    lines.append("                _animator.SetBool(\"IsBlocking\", false);")
    lines.append("")
    lines.append("            _currentState = newState;")
    lines.append("        }")
    lines.append("")
    lines.append("        // =================================================================")
    lines.append("        // Attack execution (uses DamageCalculator + EventBus)")
    lines.append("        // =================================================================")
    lines.append("")
    lines.append("        private void PerformLightAttack()")
    lines.append("        {")
    lines.append("            _stateTimer = 0.35f;")
    lines.append("            int basePower = _combatant != null ? _combatant.Attack : 10;")
    lines.append("            float comboMultiplier = 1f + (_currentComboIndex - 1) * 0.15f;")
    lines.append("            int finalPower = Mathf.RoundToInt(basePower * comboMultiplier);")
    lines.append("")
    lines.append("            if (_animator != null)")
    lines.append("                _animator.SetTrigger(\"LightAttack\" + _currentComboIndex);")
    lines.append("")
    lines.append("            PerformDamageCheck(finalPower);")
    lines.append("        }")
    lines.append("")
    lines.append("        private void PerformHeavyAttack()")
    lines.append("        {")
    lines.append("            _stateTimer = 0.6f;")
    lines.append("            int basePower = _combatant != null ? _combatant.Attack : 10;")
    lines.append("            int finalPower = Mathf.RoundToInt(basePower * heavyDamageMultiplier);")
    lines.append("            staminaCurrent -= 15f;")
    lines.append("            _staminaRegenTimer = staminaRegenDelay;")
    lines.append("")
    lines.append("            if (_animator != null)")
    lines.append("                _animator.SetTrigger(\"HeavyAttack\" + _currentComboIndex);")
    lines.append("")
    lines.append("            PerformDamageCheck(finalPower);")
    lines.append("        }")
    lines.append("")
    lines.append("        private void PerformDamageCheck(int basePower)")
    lines.append("        {")
    lines.append("            if (_combatant == null) return;")
    lines.append("")
    lines.append("            // Overlap sphere to find targets")
    lines.append("            Collider[] hits = Physics.OverlapSphere(transform.position + transform.forward * 1.5f, 2f);")
    lines.append("            foreach (var hit in hits)")
    lines.append("            {")
    lines.append("                if (hit.gameObject == gameObject) continue;")
    lines.append("                var target = hit.GetComponent<Combatant>();")
    lines.append("                if (target == null || !target.IsAlive) continue;")
    lines.append("")
    lines.append("                // Use DamageCalculator for damage pipeline")
    lines.append("                DamageResult result = DamageCalculator.Calculate(")
    lines.append("                    _combatant, target, basePower, DamageType.PHYSICAL);")
    lines.append("")
    lines.append("                target.TakeDamage(result.finalDamage, result.isCritical);")
    lines.append("")
    lines.append("                // Fire EventBus event")
    lines.append("                EventBus.DamageDealt(")
    lines.append("                    _combatant != null ? _combatant.CombatantId : \"\",")
    lines.append("                    target.CombatantId,")
    lines.append("                    result.finalDamage,")
    lines.append("                    result.isCritical);")
    lines.append("            }")
    lines.append("        }")
    lines.append("")
    lines.append("        // =================================================================")
    lines.append("        // Damage reception (block / hit reaction)")
    lines.append("        // =================================================================")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Called when this player takes damage from an external source.")
    lines.append("        /// </summary>")
    lines.append("        public void ReceiveHit(int damage, bool isCritical)")
    lines.append("        {")
    lines.append("            if (_isInvincible || _currentState == CombatState.Dead)")
    lines.append("                return;")
    lines.append("")
    lines.append("            if (_currentState == CombatState.Block && staminaCurrent > 0f)")
    lines.append("            {")
    lines.append("                // Block reduces damage and drains stamina")
    lines.append("                int reduced = Mathf.RoundToInt(damage * blockDamageReduction);")
    lines.append("                staminaCurrent -= blockStaminaDrain;")
    lines.append("                _staminaRegenTimer = staminaRegenDelay;")
    lines.append("")
    lines.append("                if (staminaCurrent <= 0f)")
    lines.append("                {")
    lines.append("                    // Block broken -- take full unmitigated damage")
    lines.append("                    staminaCurrent = 0f;")
    lines.append("                    TransitionTo(CombatState.HitReaction);")
    lines.append("                    _stateTimer = hitReactionDuration;")
    lines.append("                    if (_combatant != null)")
    lines.append("                        _combatant.TakeDamage(damage, isCritical);")
    lines.append("                }")
    lines.append("                else")
    lines.append("                {")
    lines.append("                    // Block held -- apply reduced damage")
    lines.append("                    if (_combatant != null)")
    lines.append("                        _combatant.TakeDamage(reduced, isCritical);")
    lines.append("                }")
    lines.append("                return;")
    lines.append("            }")
    lines.append("")
    lines.append("            // Unblocked hit -- enter hit reaction")
    lines.append("            TransitionTo(CombatState.HitReaction);")
    lines.append("            _stateTimer = hitReactionDuration;")
    lines.append("            _currentComboIndex = 0;")
    lines.append("")
    lines.append("            if (_animator != null)")
    lines.append("                _animator.SetTrigger(\"HitReaction\");")
    lines.append("")
    lines.append("            if (_combatant != null)")
    lines.append("            {")
    lines.append("                _combatant.TakeDamage(damage, isCritical);")
    lines.append("                if (!_combatant.IsAlive)")
    lines.append("                    TransitionTo(CombatState.Dead);")
    lines.append("            }")
    lines.append("        }")
    lines.append("")
    lines.append("        // =================================================================")
    lines.append("        // i-Frame and stamina systems")
    lines.append("        // =================================================================")
    lines.append("")
    lines.append("        private void UpdateIFrames()")
    lines.append("        {")
    lines.append("            if (_isInvincible)")
    lines.append("            {")
    lines.append("                _iFrameTimer -= Time.deltaTime;")
    lines.append("                if (_iFrameTimer <= 0f)")
    lines.append("                    _isInvincible = false;")
    lines.append("            }")
    lines.append("        }")
    lines.append("")
    lines.append("        private void UpdateStamina()")
    lines.append("        {")
    lines.append("            if (_staminaRegenTimer > 0f)")
    lines.append("            {")
    lines.append("                _staminaRegenTimer -= Time.deltaTime;")
    lines.append("                return;")
    lines.append("            }")
    lines.append("")
    lines.append("            if (_currentState == CombatState.Idle && staminaCurrent < staminaMax)")
    lines.append("            {")
    lines.append("                staminaCurrent = Mathf.Min(staminaMax, staminaCurrent + staminaRegenRate * Time.deltaTime);")
    lines.append("            }")
    lines.append("        }")
    lines.append("")
    lines.append("        // =================================================================")
    lines.append("        // Input hooks (override in subclass or wire to Input System)")
    lines.append("        // =================================================================")
    lines.append("")
    lines.append("        /// <summary>Override to provide light attack input.</summary>")
    lines.append("        protected virtual bool GetLightAttackInput() { return Input.GetButtonDown(\"Fire1\"); }")
    lines.append("")
    lines.append("        /// <summary>Override to provide heavy attack input.</summary>")
    lines.append("        protected virtual bool GetHeavyAttackInput() { return Input.GetButtonDown(\"Fire2\"); }")
    lines.append("")
    lines.append("        /// <summary>Override to provide dodge input.</summary>")
    lines.append("        protected virtual bool GetDodgeInput() { return Input.GetKeyDown(KeyCode.Space); }")
    lines.append("")
    lines.append("        /// <summary>Override to provide block input.</summary>")
    lines.append("        protected virtual bool GetBlockInput() { return Input.GetKey(KeyCode.LeftShift); }")
    lines.append("")
    lines.append("        /// <summary>Override to provide movement direction.</summary>")
    lines.append("        protected virtual Vector3 GetMoveDirection()")
    lines.append("        {")
    lines.append("            float h = Input.GetAxisRaw(\"Horizontal\");")
    lines.append("            float v = Input.GetAxisRaw(\"Vertical\");")
    lines.append("            if (Mathf.Approximately(h, 0f) && Mathf.Approximately(v, 0f))")
    lines.append("                return Vector3.zero;")
    lines.append("")
    lines.append("            // Camera-relative direction for third-person control")
    lines.append("            Camera cam = Camera.main;")
    lines.append("            if (cam == null)")
    lines.append("                return new Vector3(h, 0f, v);")
    lines.append("")
    lines.append("            Vector3 camForward = cam.transform.forward;")
    lines.append("            Vector3 camRight = cam.transform.right;")
    lines.append("            camForward.y = 0f;")
    lines.append("            camRight.y = 0f;")
    lines.append("            camForward.Normalize();")
    lines.append("            camRight.Normalize();")
    lines.append("")
    lines.append("            return camForward * v + camRight * h;")
    lines.append("        }")
    lines.append("    }")
    lines.append("}")

    return "\n".join(lines)


