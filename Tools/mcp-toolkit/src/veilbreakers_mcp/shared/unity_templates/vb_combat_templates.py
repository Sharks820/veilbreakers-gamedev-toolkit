def generate_currency_system_script(
    currency_types: list[str] | None = None,
    namespace: str = "VeilBreakers.GameSystems",
) -> str:
    """Generate C# runtime MonoBehaviour for a multi-currency system.

    Produces a singleton-pattern currency manager with earn/spend/query
    methods and EventBus.CurrencyChanged(int) integration.

    Args:
        currency_types: List of currency type names (default: Gold, Souls, Marks).
        namespace: C# namespace for the generated class.

    Returns:
        Complete C# source string.
    """
    if currency_types is None:
        currency_types = ["Gold", "Souls", "Marks"]

    lines = []
    lines.append("using UnityEngine;")
    lines.append("using System.Collections.Generic;")
    lines.append("using VeilBreakers.Core;")
    lines.append("")
    lines.append("namespace " + _safe_namespace(namespace))
    lines.append("{")
    lines.append("    /// <summary>")
    lines.append("    /// Serializable currency data for save system integration.")
    lines.append("    /// </summary>")
    lines.append("    [System.Serializable]")
    lines.append("    public class CurrencyData")
    lines.append("    {")
    lines.append("        public string currencyType;")
    lines.append("        public int amount;")
    lines.append("    }")
    lines.append("")
    lines.append("    /// <summary>")
    lines.append("    /// Multi-currency system with singleton pattern.")
    lines.append("    /// Supports earn, spend, query, and EventBus integration.")
    lines.append("    /// Generated by VeilBreakers MCP toolkit.")
    lines.append("    /// </summary>")
    lines.append("    public class VB_CurrencySystem : MonoBehaviour")
    lines.append("    {")
    lines.append("        /// <summary>Singleton instance.</summary>")
    lines.append("        public static VB_CurrencySystem Instance { get; private set; }")
    lines.append("")
    lines.append("        [Header(\"Currencies\")]")
    lines.append("        [SerializeField]")
    lines.append("        private List<CurrencyData> _savedCurrencies = new List<CurrencyData>();")
    lines.append("")
    lines.append("        // Runtime currency storage")
    lines.append("        private Dictionary<string, int> _currencies = new Dictionary<string, int>();")
    lines.append("")
    lines.append("        // Transaction history")
    lines.append("        private List<string> _transactionHistory = new List<string>();")
    lines.append("")
    lines.append("        private void Awake()")
    lines.append("        {")
    lines.append("            if (Instance != null && Instance != this)")
    lines.append("            {")
    lines.append("                Destroy(gameObject);")
    lines.append("                return;")
    lines.append("            }")
    lines.append("            Instance = this;")
    lines.append("            DontDestroyOnLoad(gameObject);")
    lines.append("")
    lines.append("            // Initialize default currency types")

    for ct in currency_types:
        safe_ct = sanitize_cs_string(ct)
        lines.append('            _currencies["' + safe_ct + '"] = 0;')

    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Earn currency of the specified type.")
    lines.append("        /// </summary>")
    lines.append("        public void Earn(string currencyType, int amount)")
    lines.append("        {")
    lines.append("            if (amount <= 0) return;")
    lines.append("")
    lines.append("            if (!_currencies.ContainsKey(currencyType))")
    lines.append("                _currencies[currencyType] = 0;")
    lines.append("")
    lines.append("            _currencies[currencyType] += amount;")
    lines.append("")
    lines.append("            _transactionHistory.Add($\"+{amount} {currencyType}\");")
    lines.append("            EventBus.CurrencyChanged(_currencies[currencyType]);")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Spend currency of the specified type. Returns false if insufficient.")
    lines.append("        /// </summary>")
    lines.append("        public bool Spend(string currencyType, int amount)")
    lines.append("        {")
    lines.append("            if (amount <= 0) return false;")
    lines.append("            if (!HasEnough(currencyType, amount)) return false;")
    lines.append("")
    lines.append("            _currencies[currencyType] -= amount;")
    lines.append("")
    lines.append("            _transactionHistory.Add($\"-{amount} {currencyType}\");")
    lines.append("            EventBus.CurrencyChanged(_currencies[currencyType]);")
    lines.append("            return true;")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Get balance for a currency type.")
    lines.append("        /// </summary>")
    lines.append("        public int GetBalance(string currencyType)")
    lines.append("        {")
    lines.append("            return _currencies.TryGetValue(currencyType, out int val) ? val : 0;")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Check if the player has enough of a currency.")
    lines.append("        /// </summary>")
    lines.append("        public bool HasEnough(string currencyType, int amount)")
    lines.append("        {")
    lines.append("            return GetBalance(currencyType) >= amount;")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Format a currency amount for display (e.g. \"1,234 Gold\").")
    lines.append("        /// </summary>")
    lines.append("        public string FormatCurrency(string currencyType, int amount)")
    lines.append("        {")
    lines.append("            return $\"{amount:N0} {currencyType}\";")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Get formatted balance string for display.")
    lines.append("        /// </summary>")
    lines.append("        public string GetFormattedBalance(string currencyType)")
    lines.append("        {")
    lines.append("            return FormatCurrency(currencyType, GetBalance(currencyType));")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Get serializable currency data for save system.")
    lines.append("        /// </summary>")
    lines.append("        public List<CurrencyData> GetSaveData()")
    lines.append("        {")
    lines.append("            var data = new List<CurrencyData>();")
    lines.append("            foreach (var kvp in _currencies)")
    lines.append("            {")
    lines.append("                data.Add(new CurrencyData { currencyType = kvp.Key, amount = kvp.Value });")
    lines.append("            }")
    lines.append("            return data;")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Load currency data from save system.")
    lines.append("        /// </summary>")
    lines.append("        public void LoadSaveData(List<CurrencyData> data)")
    lines.append("        {")
    lines.append("            _currencies.Clear();")
    lines.append("            foreach (var item in data)")
    lines.append("            {")
    lines.append("                _currencies[item.currencyType] = item.amount;")
    lines.append("            }")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Get recent transaction history for UI display.")
    lines.append("        /// </summary>")
    lines.append("        public IReadOnlyList<string> GetTransactionHistory()")
    lines.append("        {")
    lines.append("            return _transactionHistory;")
    lines.append("        }")
    lines.append("    }")
    lines.append("}")

    return "\n".join(lines)


