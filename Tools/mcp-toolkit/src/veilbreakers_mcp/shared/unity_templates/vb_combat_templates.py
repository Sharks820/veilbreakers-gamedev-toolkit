def generate_xp_leveling_script(
    max_level: int = 50,
    base_xp_per_level: int = 100,
    xp_scaling_factor: float = 1.15,
    namespace: str = "VeilBreakers.GameSystems",
) -> str:
    """Generate C# runtime MonoBehaviour for an XP and leveling system.

    Integrates with EventBus.HeroLevelUp and supports per-hero-path
    stat scaling.

    Args:
        max_level: Maximum achievable level.
        base_xp_per_level: Base XP needed for the first level-up.
        xp_scaling_factor: Exponential scaling factor per level.
        namespace: C# namespace for the generated class.

    Returns:
        Complete C# source string.
    """
    lines = []
    lines.append("using UnityEngine;")
    lines.append("using VeilBreakers.Core;")
    lines.append("using VeilBreakers.Data;")
    lines.append("")
    lines.append("namespace " + _safe_namespace(namespace))
    lines.append("{")
    lines.append("    /// <summary>")
    lines.append("    /// XP and leveling system with exponential scaling.")
    lines.append("    /// Integrates with EventBus.HeroLevelUp for cross-system communication.")
    lines.append("    /// Generated by VeilBreakers MCP toolkit.")
    lines.append("    /// </summary>")
    lines.append("    public class VB_XPSystem : MonoBehaviour")
    lines.append("    {")
    lines.append("        [Header(\"Identity\")]")
    lines.append("        public string heroId;")
    lines.append("        public Path heroPath = Path.NONE;")
    lines.append("")
    lines.append("        [Header(\"XP Settings\")]")
    lines.append("        public int maxLevel = " + str(max_level) + ";")
    lines.append("        public int baseXPPerLevel = " + str(base_xp_per_level) + ";")
    lines.append("        public float xpScalingFactor = " + str(xp_scaling_factor) + "f;")
    lines.append("")
    lines.append("        [Header(\"Current State\")]")
    lines.append("        public int currentLevel = 1;")
    lines.append("        public int currentXP = 0;")
    lines.append("        public int totalXP = 0;")
    lines.append("")
    lines.append("        [Header(\"Stat Points\")]")
    lines.append("        public int unspentStatPoints = 0;")
    lines.append("        public int bonusAttack = 0;")
    lines.append("        public int bonusDefense = 0;")
    lines.append("        public int bonusMagic = 0;")
    lines.append("        public int bonusVitality = 0;")
    lines.append("")
    lines.append("        /// <summary>XP needed to reach the next level.</summary>")
    lines.append("        public int XPToNextLevel => GetXPForLevel(currentLevel + 1);")
    lines.append("")
    lines.append("        /// <summary>Progress toward next level (0-1).</summary>")
    lines.append("        public float LevelProgress => XPToNextLevel > 0 ? (float)currentXP / XPToNextLevel : 1f;")
    lines.append("")
    lines.append("        private void OnEnable()")
    lines.append("        {")
    lines.append("            // Subscribe to XP-granting events")
    lines.append("            EventBus.OnUnitDefeated += OnEnemyKilled;")
    lines.append("        }")
    lines.append("")
    lines.append("        private void OnDisable()")
    lines.append("        {")
    lines.append("            EventBus.OnUnitDefeated -= OnEnemyKilled;")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Calculate XP required for a given level.")
    lines.append("        /// Uses exponential curve: baseXP * (scalingFactor ^ level).")
    lines.append("        /// </summary>")
    lines.append("        public int GetXPForLevel(int level)")
    lines.append("        {")
    lines.append("            if (level <= 1) return 0;")
    lines.append("            return Mathf.RoundToInt(baseXPPerLevel * Mathf.Pow(xpScalingFactor, level - 1));")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Add XP from any source. Triggers level-up if threshold reached.")
