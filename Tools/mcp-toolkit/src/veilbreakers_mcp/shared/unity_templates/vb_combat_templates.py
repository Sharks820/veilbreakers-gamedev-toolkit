def generate_ability_system_script(
    max_ability_slots: int = 4,
    mana_max: float = 100.0,
    mana_regen_rate: float = 5.0,
    namespace: str = "VeilBreakers.Combat",
) -> str:
    """Generate C# runtime MonoBehaviour for a brand-specific ability system.

    Produces an ability slot system with cooldown timers, mana resource,
    brand matching, and EventBus integration.

    Args:
        max_ability_slots: Maximum number of ability slots.
        mana_max: Maximum mana pool.
        mana_regen_rate: Mana regenerated per second.
        namespace: C# namespace for the generated class.

    Returns:
        Complete C# source string.
    """
    lines = []
    lines.append("using UnityEngine;")
    lines.append("using System.Collections.Generic;")
    lines.append("using VeilBreakers.Combat;")
    lines.append("using VeilBreakers.Core;")
    lines.append("using VeilBreakers.Data;")
    lines.append("")
    lines.append("namespace " + _safe_namespace(namespace))
    lines.append("{")
    lines.append("    /// <summary>")
    lines.append("    /// Serializable ability slot for the ability system.")
    lines.append("    /// </summary>")
    lines.append("    [System.Serializable]")
    lines.append("    public class AbilitySlot")
    lines.append("    {")
    lines.append("        public string abilityName;")
    lines.append("        public Brand requiredBrand = Brand.NONE;")
    lines.append("        public float cooldownMax = 5f;")
    lines.append("        public float cooldownRemaining = 0f;")
    lines.append("        public float manaCost = 20f;")
    lines.append("        public KeyCode activationKey = KeyCode.Alpha1;")
    lines.append("        public int basePower = 30;")
    lines.append("")
    lines.append("        /// <summary>Cooldown percentage (0 = ready, 1 = full cooldown).</summary>")
    lines.append("        public float CooldownPercent => cooldownMax > 0f ? cooldownRemaining / cooldownMax : 0f;")
    lines.append("")
    lines.append("        /// <summary>Whether this ability is off cooldown.</summary>")
    lines.append("        public bool IsReady => cooldownRemaining <= 0f;")
    lines.append("    }")
    lines.append("")
    lines.append("    /// <summary>")
    lines.append("    /// Brand-specific ability system with cooldowns and mana.")
    lines.append("    /// Generated by VeilBreakers MCP toolkit.")
    lines.append("    /// </summary>")
    lines.append("    public class VB_AbilitySystem : MonoBehaviour")
    lines.append("    {")
    lines.append("        [Header(\"Ability Slots\")]")
    lines.append("        public AbilitySlot[] abilitySlots = new AbilitySlot[" + str(max_ability_slots) + "];")
    lines.append("")
    lines.append("        [Header(\"Mana\")]")
    lines.append("        public float manaMax = " + str(mana_max) + "f;")
    lines.append("        public float manaCurrent;")
    lines.append("        public float manaRegenRate = " + str(mana_regen_rate) + "f;")
    lines.append("")
    lines.append("        // Cached components")
    lines.append("        private Combatant _combatant;")
    lines.append("        private Animator _animator;")
    lines.append("")
    lines.append("        /// <summary>Maximum ability slots.</summary>")
    lines.append("        public int MaxSlots => " + str(max_ability_slots) + ";")
    lines.append("")
    lines.append("        private void Start()")
    lines.append("        {")
    lines.append("            _combatant = GetComponent<Combatant>();")
    lines.append("            _animator = GetComponent<Animator>();")
    lines.append("            manaCurrent = manaMax;")
    lines.append("        }")
    lines.append("")
    lines.append("        private void Update()")
    lines.append("        {")
    lines.append("            UpdateCooldowns();")
    lines.append("            UpdateManaRegen();")
    lines.append("            CheckAbilityInput();")
    lines.append("        }")
    lines.append("")
    lines.append("        private void UpdateCooldowns()")
    lines.append("        {")
    lines.append("            if (abilitySlots == null) return;")
    lines.append("            for (int i = 0; i < abilitySlots.Length; i++)")
    lines.append("            {")
    lines.append("                if (abilitySlots[i] != null && abilitySlots[i].cooldownRemaining > 0f)")
    lines.append("                    abilitySlots[i].cooldownRemaining -= Time.deltaTime;")
    lines.append("            }")
    lines.append("        }")
    lines.append("")
    lines.append("        private void UpdateManaRegen()")
    lines.append("        {")
    lines.append("            if (manaCurrent < manaMax)")
    lines.append("                manaCurrent = Mathf.Min(manaMax, manaCurrent + manaRegenRate * Time.deltaTime);")
    lines.append("        }")
    lines.append("")
    lines.append("        private void CheckAbilityInput()")
    lines.append("        {")
    lines.append("            if (abilitySlots == null) return;")
    lines.append("            for (int i = 0; i < abilitySlots.Length; i++)")
    lines.append("            {")
    lines.append("                if (abilitySlots[i] != null && Input.GetKeyDown(abilitySlots[i].activationKey))")
    lines.append("                    TryActivateAbility(i);")
    lines.append("            }")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Attempt to activate an ability by slot index.")
    lines.append("        /// Validates cooldown, mana cost, and brand requirement.")
    lines.append("        /// </summary>")
    lines.append("        public bool TryActivateAbility(int slotIndex)")
    lines.append("        {")
    lines.append("            if (abilitySlots == null || slotIndex < 0 || slotIndex >= abilitySlots.Length)")
    lines.append("                return false;")
    lines.append("")
    lines.append("            AbilitySlot slot = abilitySlots[slotIndex];")
    lines.append("            if (slot == null) return false;")
    lines.append("")
    lines.append("            // Cooldown check")
    lines.append("            if (!slot.IsReady)")
    lines.append("                return false;")
    lines.append("")
    lines.append("            // Mana check")
    lines.append("            if (manaCurrent < slot.manaCost)")
    lines.append("                return false;")
    lines.append("")
    lines.append("            // Brand check")
    lines.append("            if (slot.requiredBrand != Brand.NONE)")
    lines.append("            {")
    lines.append("                if (_combatant == null) return false;")
    lines.append("                if (_combatant.Brand != slot.requiredBrand)")
    lines.append("                    return false;")
    lines.append("            }")
    lines.append("")
    lines.append("            // Activate")
    lines.append("            manaCurrent -= slot.manaCost;")
    lines.append("            slot.cooldownRemaining = slot.cooldownMax;")
    lines.append("")
    lines.append("            if (_animator != null)")
    lines.append("                _animator.SetTrigger(\"Ability\" + slotIndex);")
    lines.append("")
    lines.append("            // Fire EventBus event")
    lines.append("            string heroId = _combatant != null ? _combatant.CombatantId : \"\";")
    lines.append("            Brand brand = slot.requiredBrand;")
    lines.append("            EventBus.SkillUsed(heroId, slot.abilityName);")
    lines.append("")
    lines.append("            return true;")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Set an ability in the given slot.")
    lines.append("        /// </summary>")
    lines.append("        public void SetAbility(int slotIndex, AbilitySlot ability)")
    lines.append("        {")
    lines.append("            if (slotIndex >= 0 && slotIndex < abilitySlots.Length)")
    lines.append("                abilitySlots[slotIndex] = ability;")
    lines.append("        }")
    lines.append("    }")
    lines.append("}")

    return "\n".join(lines)


