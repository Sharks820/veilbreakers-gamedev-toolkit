def _generate_turnbased_combat_script(
    namespace: str = "VeilBreakers.Combat",
) -> str:
    """Generate a turn-based combat wrapper that delegates to BattleManager.

    Produces a MonoBehaviour that bridges between the overworld and the
    existing BattleManager/Combatant turn-based system. Manages turn state
    (PLAYER_TURN, ENEMY_TURN), action selection, and EventBus integration.
    """
    ns = _safe_namespace(namespace)
    lines = []
    lines.append("using UnityEngine;")
    lines.append("using VeilBreakers.Combat;")
    lines.append("using VeilBreakers.Core;")
    lines.append("using VeilBreakers.Systems;")
    lines.append("using VeilBreakers.Data;")
    lines.append("")
    lines.append(f"namespace {ns}")
    lines.append("{")
    lines.append("    /// <summary>")
    lines.append("    /// Turn-based combat controller that delegates to BattleManager.")
    lines.append("    /// Manages PLAYER_TURN / ENEMY_TURN states for structured encounters.")
    lines.append("    /// For real-time overworld combat, use VB_PlayerCombat instead.")
    lines.append("    /// Generated by VeilBreakers MCP toolkit.")
    lines.append("    /// </summary>")
    lines.append("    public class VB_TurnBasedCombat : MonoBehaviour")
    lines.append("    {")
    lines.append("        /// <summary>Turn-based battle states.</summary>")
    lines.append("        public enum TurnState")
    lines.append("        {")
    lines.append("            Inactive,")
    lines.append("            BattleStart,")
    lines.append("            PlayerTurn,")
    lines.append("            EnemyTurn,")
    lines.append("            Victory,")
    lines.append("            Defeat")
    lines.append("        }")
    lines.append("")
    lines.append("        [Header(\"Battle References\")]")
    lines.append("        public Combatant playerCombatant;")
    lines.append("        public Combatant[] enemyCombatants;")
    lines.append("")
    lines.append("        // Runtime state")
    lines.append("        private TurnState _currentState = TurnState.Inactive;")
    lines.append("        private int _currentEnemyIndex = 0;")
    lines.append("")
    lines.append("        /// <summary>Current turn state (read-only).</summary>")
    lines.append("        public TurnState CurrentState => _currentState;")
    lines.append("")
    lines.append("        /// <summary>Start a new battle encounter.</summary>")
    lines.append("        public virtual void StartBattle(Combatant player, Combatant[] enemies)")
    lines.append("        {")
    lines.append("            playerCombatant = player;")
    lines.append("            enemyCombatants = enemies;")
    lines.append("            _currentState = TurnState.BattleStart;")
    lines.append("            _currentEnemyIndex = 0;")
    lines.append("            OnBattleStarted?.Invoke();")
    lines.append("            TransitionToPlayerTurn();")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>Transition to player's turn.</summary>")
    lines.append("        protected virtual void TransitionToPlayerTurn()")
    lines.append("        {")
    lines.append("            _currentState = TurnState.PlayerTurn;")
    lines.append("            OnPlayerTurnStarted?.Invoke();")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>Execute a player action and advance to enemy turn.</summary>")
    lines.append("        public virtual void ExecutePlayerAction(string actionId, int targetIndex)")
    lines.append("        {")
    lines.append("            if (_currentState != TurnState.PlayerTurn) return;")
    lines.append("")
    lines.append("            // Delegate damage calculation to existing systems")
    lines.append("            var target = enemyCombatants[Mathf.Clamp(targetIndex, 0, enemyCombatants.Length - 1)];")
    lines.append("            DamageResult result = DamageCalculator.Calculate(playerCombatant, target, playerCombatant.stats.attack, DamageType.PHYSICAL);")
    lines.append("            float damage = result.finalDamage;")
    lines.append("            target.TakeDamage(result);")
    lines.append("            OnPlayerActionExecuted?.Invoke(actionId);")
    lines.append("")
    lines.append("            // Check for victory")
    lines.append("            bool allDefeated = true;")
    lines.append("            foreach (var enemy in enemyCombatants)")
    lines.append("            {")
    lines.append("                if (enemy != null && enemy.gameObject.activeSelf)")
    lines.append("                    allDefeated = false;")
    lines.append("            }")
    lines.append("")
    lines.append("            if (allDefeated)")
    lines.append("            {")
    lines.append("                _currentState = TurnState.Victory;")
    lines.append("                OnBattleVictory?.Invoke();")
    lines.append("                return;")
    lines.append("            }")
    lines.append("")
    lines.append("            TransitionToEnemyTurn();")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>Transition to enemy turn and process AI actions.</summary>")
    lines.append("        protected virtual void TransitionToEnemyTurn()")
    lines.append("        {")
    lines.append("            _currentState = TurnState.EnemyTurn;")
    lines.append("            _currentEnemyIndex = 0;")
    lines.append("            OnEnemyTurnStarted?.Invoke();")
    lines.append("            ProcessNextEnemy();")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>Process the next enemy's action.</summary>")
    lines.append("        protected virtual void ProcessNextEnemy()")
    lines.append("        {")
    lines.append("            while (_currentEnemyIndex < enemyCombatants.Length)")
    lines.append("            {")
    lines.append("                var enemy = enemyCombatants[_currentEnemyIndex];")
    lines.append("                _currentEnemyIndex++;")
    lines.append("")
    lines.append("                if (enemy == null || !enemy.gameObject.activeSelf)")
    lines.append("                    continue;")
    lines.append("")
    lines.append("                // Delegate enemy AI damage to existing systems")
    lines.append("                DamageResult result = DamageCalculator.Calculate(enemy, playerCombatant, enemy.stats.attack, DamageType.PHYSICAL);")
    lines.append("                float damage = result.finalDamage;")
    lines.append("                playerCombatant.TakeDamage(result);")
    lines.append("                OnEnemyActionExecuted?.Invoke();")
    lines.append("                break;")
    lines.append("            }")
    lines.append("")
    lines.append("            // Check for defeat")
    lines.append("            if (playerCombatant == null || !playerCombatant.gameObject.activeSelf)")
    lines.append("            {")
    lines.append("                _currentState = TurnState.Defeat;")
    lines.append("                OnBattleDefeat?.Invoke();")
    lines.append("                return;")
    lines.append("            }")
    lines.append("")
    lines.append("            if (_currentEnemyIndex >= enemyCombatants.Length)")
    lines.append("                TransitionToPlayerTurn();")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>End the battle and clean up.</summary>")
    lines.append("        public virtual void EndBattle()")
    lines.append("        {")
    lines.append("            _currentState = TurnState.Inactive;")
    lines.append("            OnBattleEnded?.Invoke();")
    lines.append("        }")
    lines.append("    }")
    lines.append("}")
    return "\n".join(lines)


