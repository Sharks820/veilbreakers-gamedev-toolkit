def generate_damage_type_script(
    namespace: str = "VeilBreakers.Combat",
) -> str:
    """Generate C# static utility class for brand-specific damage types.

    Produces a wiring layer that delegates effectiveness calculations to
    BrandSystem.GetEffectiveness(). Does NOT contain an effectiveness matrix.

    Args:
        namespace: C# namespace for the generated class.

    Returns:
        Complete C# source string.
    """
    lines = []
    lines.append("using UnityEngine;")
    lines.append("using VeilBreakers.Systems;")
    lines.append("using VeilBreakers.Data;")
    lines.append("")
    lines.append("namespace " + _safe_namespace(namespace))
    lines.append("{")
    lines.append("    /// <summary>")
    lines.append("    /// Extended damage type system with brand-specific resistance.")
    lines.append("    /// Delegates effectiveness calculations to BrandSystem.GetEffectiveness().")
    lines.append("    /// Does NOT reimplement brand effectiveness matrix.")
    lines.append("    /// Generated by VeilBreakers MCP toolkit.")
    lines.append("    /// </summary>")
    lines.append("    public static class VB_DamageTypeSystem")
    lines.append("    {")
    lines.append("        /// <summary>")
    lines.append("        /// Extended damage type categories:")
    lines.append("        /// Physical/Magical (generic), brand-specific, and True (bypasses resistance).")
    lines.append("        /// </summary>")
    lines.append("        public enum ExtendedDamageType")
    lines.append("        {")
    lines.append("            Physical,")
    lines.append("            Magical,")
    lines.append("            Iron,")
    lines.append("            Venom,")
    lines.append("            Surge,")
    lines.append("            Dread,")
    lines.append("            Blaze,")
    lines.append("            Savage,")
    lines.append("            Leech,")
    lines.append("            Grace,")
    lines.append("            Mend,")
    lines.append("            Ruin,")
    lines.append("            Void,")
    lines.append("            True  // Bypasses all resistance")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Apply resistance to base damage based on damage type and target brand.")
    lines.append("        /// Delegates to BrandSystem.GetEffectiveness() for multiplier.")
    lines.append("        /// </summary>")
    lines.append("        public static int ApplyResistance(int baseDamage, ExtendedDamageType damageType, Brand targetBrand)")
    lines.append("        {")
    lines.append("            // True damage bypasses all resistance")
    lines.append("            if (damageType == ExtendedDamageType.True)")
    lines.append("                return baseDamage;")
    lines.append("")
    lines.append("            // Generic Physical/Magical have no brand multiplier")
    lines.append("            if (damageType == ExtendedDamageType.Physical || damageType == ExtendedDamageType.Magical)")
    lines.append("                return baseDamage;")
    lines.append("")
    lines.append("            // Convert damage type to brand for effectiveness lookup")
    lines.append("            Brand attackBrand = DamageTypeToBrand(damageType);")
    lines.append("            if (attackBrand == Brand.NONE)")
    lines.append("                return baseDamage;")
    lines.append("")
    lines.append("            // Delegate to BrandSystem -- no reimplementation")
    lines.append("            float multiplier = BrandSystem.GetEffectiveness(attackBrand, targetBrand);")
    lines.append("            return Mathf.Max(1, Mathf.RoundToInt(baseDamage * multiplier));")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Map an extended damage type to a Brand enum value.")
    lines.append("        /// </summary>")
    lines.append("        public static Brand DamageTypeToBrand(ExtendedDamageType damageType)")
    lines.append("        {")
    lines.append("            switch (damageType)")
    lines.append("            {")
    lines.append("                case ExtendedDamageType.Iron:   return Brand.IRON;")
    lines.append("                case ExtendedDamageType.Venom:  return Brand.VENOM;")
    lines.append("                case ExtendedDamageType.Surge:  return Brand.SURGE;")
    lines.append("                case ExtendedDamageType.Dread:  return Brand.DREAD;")
    lines.append("                case ExtendedDamageType.Savage: return Brand.SAVAGE;")
    lines.append("                case ExtendedDamageType.Leech:  return Brand.LEECH;")
    lines.append("                case ExtendedDamageType.Grace:  return Brand.GRACE;")
    lines.append("                case ExtendedDamageType.Mend:   return Brand.MEND;")
    lines.append("                case ExtendedDamageType.Ruin:   return Brand.RUIN;")
    lines.append("                case ExtendedDamageType.Void:   return Brand.VOID;")
    lines.append("                default:                        return Brand.NONE;")
    lines.append("            }")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Map a Brand to its corresponding extended damage type.")
    lines.append("        /// </summary>")
    lines.append("        public static ExtendedDamageType BrandToDamageType(Brand brand)")
    lines.append("        {")
    lines.append("            switch (brand)")
    lines.append("            {")
    lines.append("                case Brand.IRON:   return ExtendedDamageType.Iron;")
