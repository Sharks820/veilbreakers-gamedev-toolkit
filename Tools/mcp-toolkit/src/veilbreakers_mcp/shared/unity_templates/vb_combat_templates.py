    lines.append("        {")
    lines.append("            // Each path gets automatic stat allocation on level-up")
    lines.append("            switch (path)")
    lines.append("            {")
    lines.append("                case Path.IRONBOUND:")
    lines.append("                    bonusDefense += 2;")
    lines.append("                    bonusVitality += 2;")
    lines.append("                    bonusAttack += 1;")
    lines.append("                    break;")
    lines.append("                case Path.FANGBORN:")
    lines.append("                    bonusAttack += 3;")
    lines.append("                    bonusDefense += 1;")
    lines.append("                    bonusVitality += 1;")
    lines.append("                    break;")
    lines.append("                case Path.VOIDTOUCHED:")
    lines.append("                    bonusMagic += 3;")
    lines.append("                    bonusAttack += 1;")
    lines.append("                    bonusVitality += 1;")
    lines.append("                    break;")
    lines.append("                case Path.UNCHAINED:")
    lines.append("                    // Balanced -- equal distribution")
    lines.append("                    bonusAttack += 1;")
    lines.append("                    bonusDefense += 1;")
    lines.append("                    bonusMagic += 1;")
    lines.append("                    bonusVitality += 1;")
    lines.append("                    break;")
    lines.append("                default:")
    lines.append("                    bonusAttack += 1;")
    lines.append("                    bonusDefense += 1;")
    lines.append("                    break;")
    lines.append("            }")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Manually allocate a stat point.")
    lines.append("        /// </summary>")
