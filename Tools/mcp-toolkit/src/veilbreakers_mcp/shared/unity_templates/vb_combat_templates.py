    lines.append("                case \"atk\":")
    lines.append("                    bonusAttack++;")
    lines.append("                    break;")
    lines.append("                case \"defense\":")
    lines.append("                case \"def\":")
    lines.append("                    bonusDefense++;")
    lines.append("                    break;")
