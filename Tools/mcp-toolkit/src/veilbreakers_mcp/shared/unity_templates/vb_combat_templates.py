    lines.append("                    bonusMagic++;")
    lines.append("                    break;")
    lines.append("                case \"vitality\":")
    lines.append("                case \"vit\":")
    lines.append("                    bonusVitality++;")
    lines.append("                    break;")
    lines.append("                default:")
    lines.append("                    return false;")
    lines.append("            }")
