    lines.append("")
    lines.append("            if (currentState != _previousState)")
    lines.append("            {")
    lines.append("                OnCorruptionStateChanged(_previousState, currentState);")
    lines.append("                _previousState = currentState;")
    lines.append("            }")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Apply stat multiplier from CorruptionSystem to a base stat value.")
    lines.append("        /// </summary>")
    lines.append("        public int GetModifiedStat(int baseStat)")
    lines.append("        {")
    lines.append("            float multiplier = CorruptionSystem.GetStatMultiplier(corruptionPercent);")
    lines.append("            return Mathf.RoundToInt(baseStat * multiplier);")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Get stat multiplier for current corruption level.")
    lines.append("        /// Delegates to CorruptionSystem.GetStatMultiplier().")
    lines.append("        /// </summary>")
    lines.append("        public float GetStatMultiplier()")
    lines.append("        {")
    lines.append("            return CorruptionSystem.GetStatMultiplier(corruptionPercent);")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Change corruption percentage and fire events.")
    lines.append("        /// </summary>")
    lines.append("        public void SetCorruption(float newPercent)")
    lines.append("        {")
    lines.append("            corruptionPercent = Mathf.Clamp(newPercent, 0f, 100f);")
    lines.append("")
    lines.append("            if (_combatant != null)")
    lines.append("            {")
    lines.append("                _combatant.SetCorruption(corruptionPercent);")
    lines.append("                EventBus.CorruptionChanged(_combatant.CombatantId, corruptionPercent);")
    lines.append("            }")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Called when corruption state transitions.")
    lines.append("        /// </summary>")
    lines.append("        private void OnCorruptionStateChanged(CorruptionState oldState, CorruptionState newState)")
    lines.append("        {")
    lines.append("            Debug.Log($\"[Corruption] State changed: {oldState} -> {newState}\");")
    lines.append("")
    lines.append("            // NPC reaction flag -- dialogue system can check this")
    lines.append("            // High corruption makes NPCs react differently")
    lines.append("            if (newState == CorruptionState.CORRUPTED || newState == CorruptionState.ABYSSAL)")
    lines.append("                Debug.Log(\"[Corruption] NPCs will react to high corruption!\");")
    lines.append("")
    lines.append("            // Ability mutation trigger at high corruption")
    lines.append("            if (newState == CorruptionState.ABYSSAL)")
    lines.append("                Debug.Log(\"[Corruption] Abilities mutated -- stronger but uncontrollable!\");")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Update visual corruption effects via shader parameter.")
    lines.append("        /// </summary>")
    lines.append("        private void UpdateVisualFeedback()")
    lines.append("        {")
    lines.append("            if (characterRenderer != null && _materialBlock != null)")
    lines.append("            {")
    lines.append("                // Use MaterialPropertyBlock to avoid creating material instances")
    lines.append("                characterRenderer.GetPropertyBlock(_materialBlock);")
    lines.append("                _materialBlock.SetFloat(CorruptionLevelProp, corruptionPercent / 100f);")
    lines.append("                characterRenderer.SetPropertyBlock(_materialBlock);")
    lines.append("            }")
    lines.append("        }")
    lines.append("")
    lines.append("        /// <summary>")
    lines.append("        /// Check if corruption is at or above a threshold.")
    lines.append("        /// </summary>")
    lines.append("        public bool IsAboveThreshold(int thresholdIndex)")
    lines.append("        {")
    lines.append("            if (thresholdIndex < 0 || thresholdIndex >= thresholds.Length)")
    lines.append("                return false;")
    lines.append("            return corruptionPercent >= thresholds[thresholdIndex];")
    lines.append("        }")
    lines.append("    }")
    lines.append("}")

    return "\n".join(lines)


