    lines.append(f"{indent}            while (enemiesAlive > 0 && pHP > 0)")
    lines.append(f"{indent}            {{")
    lines.append(f"{indent}                timeElapsed += dt;")
    lines.append("")
    lines.append(f"{indent}                // Player deals DPS * dt spread across alive enemies")
