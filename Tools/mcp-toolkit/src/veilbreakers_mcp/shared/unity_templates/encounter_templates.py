    lines.append(f"{indent}            EditorGUILayout.Space();")
    lines.append(f'{indent}            EditorGUILayout.LabelField("Difficulty Recommendation", _difficultyRecommendation, EditorStyles.boldLabel);')
    lines.append(f"{indent}            EditorGUILayout.Space();")
    lines.append("")
    lines.append(f'{indent}            if (GUILayout.Button("Export Results to JSON"))')
    lines.append(f"{indent}            {{")
    lines.append(f"{indent}                ExportResults();")
    lines.append(f"{indent}            }}")
