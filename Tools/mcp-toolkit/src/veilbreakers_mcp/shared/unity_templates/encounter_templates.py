    lines.append(f"{indent}                    float enemyTotalDPS = _enemyDPS * enemiesAlive * diffMultiplier * dt;")
    lines.append(f"{indent}                    pHP -= enemyTotalDPS;")
    lines.append(f"{indent}                }}")
    lines.append("")
    lines.append(f"{indent}                // Safety: prevent infinite loops")
    lines.append(f"{indent}                if (timeElapsed > 600f) break;")
    lines.append(f"{indent}            }}")
    lines.append("")
    lines.append(f"{indent}            if (pHP <= 0) break;")
    lines.append(f"{indent}        }}")
    lines.append("")
    lines.append(f"{indent}        bool win = pHP > 0;")
    lines.append(f"{indent}        if (win) _totalWins++;")
    lines.append(f"{indent}        _allClearTimes.Add(timeElapsed);")
    lines.append(f"{indent}        _totalPlayerHPRemaining += Mathf.Max(0f, pHP);")
    lines.append(f"{indent}        _totalDamageDealt += totalDamageDealt;")
    lines.append(f"{indent}        _totalTimeElapsed += timeElapsed;")
    lines.append(f"{indent}    }}")
    lines.append("")

    # FinalizeResults
    lines.append(f"{indent}    private void FinalizeResults()")
    lines.append(f"{indent}    {{")
    lines.append(f"{indent}        _winRate = (float)_totalWins / _simulationCount;")
    lines.append("")
    lines.append(f"{indent}        // Average clear time")
    lines.append(f"{indent}        float sumTime = 0f;")
    lines.append(f"{indent}        foreach (var t in _allClearTimes) sumTime += t;")
    lines.append(f"{indent}        _avgClearTime = sumTime / _allClearTimes.Count;")
    lines.append("")
    lines.append(f"{indent}        // Standard deviation")
    lines.append(f"{indent}        float sumSqDiff = 0f;")
    lines.append(f"{indent}        foreach (var t in _allClearTimes)")
    lines.append(f"{indent}        {{")
    lines.append(f"{indent}            float diff = t - _avgClearTime;")
    lines.append(f"{indent}            sumSqDiff += diff * diff;")
    lines.append(f"{indent}        }}")
    lines.append(f"{indent}        _stdDevClearTime = Mathf.Sqrt(sumSqDiff / _allClearTimes.Count);")
    lines.append("")
    lines.append(f"{indent}        _avgPlayerHPRemaining = _totalPlayerHPRemaining / _simulationCount;")
    lines.append(f"{indent}        _avgDPSDealt = _totalDamageDealt / Mathf.Max(1f, _totalTimeElapsed);")
    lines.append("")
    lines.append(f"{indent}        // Difficulty recommendation")
    lines.append(f"{indent}        if (_winRate > 0.85f)")
    lines.append(f'{indent}            _difficultyRecommendation = "Too Easy";')
    lines.append(f"{indent}        else if (_winRate >= 0.45f)")
    lines.append(f'{indent}            _difficultyRecommendation = "Balanced";')
    lines.append(f"{indent}        else")
    lines.append(f'{indent}            _difficultyRecommendation = "Too Hard";')
    lines.append("")
    lines.append(f"{indent}        _isSimulating = false;")
    lines.append(f"{indent}        _hasResults = true;")
    lines.append(f"{indent}    }}")
    lines.append("")

    # ExportResults
    lines.append(f"{indent}    private void ExportResults()")
    lines.append(f"{indent}    {{")
    lines.append(f'{indent}        string path = EditorUtility.SaveFilePanel("Export Simulation Results", "", "encounter_simulation", "json");')
    lines.append(f'{indent}        if (string.IsNullOrEmpty(path)) return;')
    lines.append("")
    lines.append(f"{indent}        var json = JsonUtility.ToJson(new SimulationResults")
    lines.append(f"{indent}        {{")
    lines.append(f"{indent}            simulationCount = _simulationCount,")
    lines.append(f"{indent}            winRate = _winRate,")
    lines.append(f"{indent}            avgClearTime = _avgClearTime,")
    lines.append(f"{indent}            stdDevClearTime = _stdDevClearTime,")
    lines.append(f"{indent}            avgPlayerHPRemaining = _avgPlayerHPRemaining,")
    lines.append(f"{indent}            avgDPSDealt = _avgDPSDealt,")
    lines.append(f"{indent}            difficultyRecommendation = _difficultyRecommendation,")
    lines.append(f"{indent}            difficultyScore = _difficultyScore")
    lines.append(f"{indent}        }}, true);")
    lines.append("")
    lines.append(f"{indent}        File.WriteAllText(path, json);")
    lines.append(f'{indent}        Debug.Log($"Simulation results exported to {{path}}");')
    lines.append(f"{indent}    }}")
    lines.append("")

    # Results data class
    lines.append(f"{indent}    [Serializable]")
    lines.append(f"{indent}    private class SimulationResults")
    lines.append(f"{indent}    {{")
    lines.append(f"{indent}        public int simulationCount;")
    lines.append(f"{indent}        public float winRate;")
    lines.append(f"{indent}        public float avgClearTime;")
    lines.append(f"{indent}        public float stdDevClearTime;")
    lines.append(f"{indent}        public float avgPlayerHPRemaining;")
    lines.append(f"{indent}        public float avgDPSDealt;")
    lines.append(f"{indent}        public string difficultyRecommendation;")
    lines.append(f"{indent}        public float difficultyScore;")
    lines.append(f"{indent}    }}")

    lines.append(f"{indent}}}")
    if ns_close:
        lines.append(ns_close)
    lines.append("#endif")

    return "\n".join(lines)


