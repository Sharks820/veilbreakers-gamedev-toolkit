    lines.append(f"{indent}    private float _winRate;")
    lines.append(f"{indent}    private float _avgClearTime;")
    lines.append(f"{indent}    private float _stdDevClearTime;")
    lines.append(f"{indent}    private float _avgPlayerHPRemaining;")
    lines.append(f"{indent}    private float _avgDPSDealt;")
    lines.append(f"{indent}    private string _difficultyRecommendation = \"\";")
    lines.append("")
    lines.append(f"{indent}    // Accumulated stats for running simulation")
    lines.append(f"{indent}    private int _totalWins;")
    lines.append(f"{indent}    private List<float> _allClearTimes;")
    lines.append(f"{indent}    private float _totalPlayerHPRemaining;")
    lines.append(f"{indent}    private float _totalDamageDealt;")
    lines.append(f"{indent}    private float _totalTimeElapsed;")
    lines.append("")

