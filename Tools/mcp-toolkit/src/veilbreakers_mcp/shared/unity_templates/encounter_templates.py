    lines.append(f"{indent}        _allClearTimes = new List<float>();")
    lines.append(f"{indent}        _totalPlayerHPRemaining = 0f;")
    lines.append(f"{indent}        _totalDamageDealt = 0f;")
    lines.append(f"{indent}        _totalTimeElapsed = 0f;")
