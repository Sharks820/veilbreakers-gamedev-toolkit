    lines.append(f"{indent}    private void OnGUI()")
    lines.append(f"{indent}    {{")
    lines.append(f'{indent}        GUILayout.Label("Monte Carlo Encounter Simulator", EditorStyles.boldLabel);')
    lines.append(f"{indent}        EditorGUILayout.Space();")
    lines.append("")
    lines.append(f"{indent}        // Player stats")
    lines.append(f'{indent}        GUILayout.Label("Player Stats", EditorStyles.miniBoldLabel);')
    lines.append(f'{indent}        _playerDPS = EditorGUILayout.FloatField("Player DPS", _playerDPS);')
    lines.append(f'{indent}        _playerHP = EditorGUILayout.FloatField("Player HP", _playerHP);')
    lines.append(f'{indent}        _playerDodgeChance = EditorGUILayout.Slider("Dodge Chance", _playerDodgeChance, 0f, 1f);')
    lines.append(f"{indent}        EditorGUILayout.Space();")
    lines.append("")
    lines.append(f"{indent}        // Wave config")
    lines.append(f'{indent}        GUILayout.Label("Wave Configuration", EditorStyles.miniBoldLabel);')
    lines.append(f'{indent}        _waveCount = EditorGUILayout.IntField("Wave Count", _waveCount);')
    lines.append(f'{indent}        _enemiesPerWave = EditorGUILayout.IntField("Enemies Per Wave", _enemiesPerWave);')
    lines.append(f'{indent}        _enemyHP = EditorGUILayout.FloatField("Enemy HP", _enemyHP);')
    lines.append(f'{indent}        _enemyDPS = EditorGUILayout.FloatField("Enemy DPS", _enemyDPS);')
    lines.append(f"{indent}        EditorGUILayout.Space();")
    lines.append("")
    lines.append(f"{indent}        // Simulation settings")
    lines.append(f'{indent}        GUILayout.Label("Simulation Settings", EditorStyles.miniBoldLabel);')
    lines.append(f'{indent}        _simulationCount = EditorGUILayout.IntField("Simulation Count", _simulationCount);')
    lines.append(f'{indent}        _difficultyScore = EditorGUILayout.Slider("Difficulty Score", _difficultyScore, 0f, 1f);')
    lines.append(f"{indent}        EditorGUILayout.Space();")
    lines.append("")

    # Run button
    lines.append(f"{indent}        EditorGUI.BeginDisabledGroup(_isSimulating);")
    lines.append(f'{indent}        if (GUILayout.Button("Run Simulation"))')
    lines.append(f"{indent}        {{")
    lines.append(f"{indent}            StartSimulation();")
    lines.append(f"{indent}        }}")
    lines.append(f"{indent}        EditorGUI.EndDisabledGroup();")
    lines.append("")

    # Progress bar
    lines.append(f"{indent}        if (_isSimulating)")
    lines.append(f"{indent}        {{")
    lines.append(f"{indent}            float progress = (float)_completedRuns / _simulationCount;")
    lines.append(f'{indent}            EditorGUI.ProgressBar(EditorGUILayout.GetControlRect(false, 20f), progress, $"Simulating... {{_completedRuns}}/{{_simulationCount}}");')
    lines.append(f"{indent}        }}")
    lines.append("")

    # Results
    lines.append(f"{indent}        if (_hasResults)")
    lines.append(f"{indent}        {{")
