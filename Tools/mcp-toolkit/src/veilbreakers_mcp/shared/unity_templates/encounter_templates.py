    lines.append(f"{indent}        EditorApplication.update += SimulationBatch;")
    lines.append(f"{indent}    }}")
    lines.append("")

    # SimulationBatch (per-frame batched execution)
    lines.append(f"{indent}    private void SimulationBatch()")
    lines.append(f"{indent}    {{")
