"""Encounter and AI system C# template generators for Unity.

Each function returns a complete C# source string (or tuple of strings for
multi-file generators) for encounter scripting, dynamic difficulty adjustment,
encounter balance simulation, and boss AI behavior systems.

Exports:
    generate_encounter_system_script    -- AID-01: SO wave definitions + MonoBehaviour manager
    generate_ai_director_script         -- AID-02: AnimationCurve-driven difficulty adjustment
    generate_encounter_simulator_script -- AID-03: Monte Carlo encounter simulation EditorWindow
    generate_boss_ai_script             -- VB-10: Multi-phase hierarchical FSM boss AI
"""

from __future__ import annotations

import re

from ._cs_sanitize import sanitize_cs_string, sanitize_cs_identifier


_CS_RESERVED = frozenset({
    "abstract", "as", "base", "bool", "break", "byte", "case", "catch", "char",
    "checked", "class", "const", "continue", "decimal", "default", "delegate",
    "do", "double", "else", "enum", "event", "explicit", "extern", "false",
    "finally", "fixed", "float", "for", "foreach", "goto", "if", "implicit",
    "in", "int", "interface", "internal", "is", "lock", "long", "namespace",
    "new", "null", "object", "operator", "out", "override", "params", "private",
    "protected", "public", "readonly", "ref", "return", "sbyte", "sealed",
    "short", "sizeof", "stackalloc", "static", "string", "struct", "switch",
    "this", "throw", "true", "try", "typeof", "uint", "ulong", "unchecked",
    "unsafe", "ushort", "using", "virtual", "void", "volatile", "while",
})


def _safe_namespace(ns: str) -> str:
    """Sanitize a C# namespace to prevent code injection."""
    sanitized = re.sub(r"[^a-zA-Z0-9_.]", "", ns)
    sanitized = re.sub(r"\.{2,}", ".", sanitized).strip(".")
    if not sanitized:
        return "Generated"
    segments = sanitized.split(".")
    fixed: list[str] = []
    for seg in segments:
        if not seg:
            continue
        if seg[0].isdigit():
            seg = f"_{seg}"
        if seg in _CS_RESERVED:
            seg = f"@{seg}"
        fixed.append(seg)
    return ".".join(fixed) or "Generated"


# ---------------------------------------------------------------------------
# AID-01: Encounter system (SO wave definitions + manager)
# ---------------------------------------------------------------------------


def generate_encounter_system_script(
    name: str = "Encounter",
    namespace: str = "",
) -> tuple[str, str]:
    """Generate SO-based wave definitions and encounter manager MonoBehaviour.

    Returns a 2-tuple: (wave_data_so_cs, encounter_manager_cs).

    The wave data ScriptableObject defines SpawnEntry arrays with spawn
    conditions and victory/defeat callbacks. The encounter manager uses
    trigger volumes and coroutine-based wave spawning with fog gate support.

    Args:
        name: Encounter name (sanitized for C# identifier).
        namespace: Optional C# namespace.

    Returns:
        Tuple of (wave_definition_so, encounter_manager) C# source strings.
    """
    safe_name = sanitize_cs_identifier(name)
    safe_str = sanitize_cs_string(name)

    ns = _safe_namespace(namespace) if namespace else ""
    ns_open = f"namespace {ns}\n{{" if ns else ""
    ns_close = "}" if ns else ""
    indent = "    " if ns else ""

    # ---- Wave Definition ScriptableObject ----
    so_lines: list[str] = []
    so_lines.append("using UnityEngine;")
    so_lines.append("using System;")
    so_lines.append("")
    if ns_open:
        so_lines.append(ns_open)

    so_lines.append(f"{indent}/// <summary>")
    so_lines.append(f"{indent}/// Serializable spawn entry for encounter wave definitions.")
    so_lines.append(f"{indent}/// Generated by VeilBreakers MCP toolkit.")
    so_lines.append(f"{indent}/// </summary>")
    so_lines.append(f"{indent}[Serializable]")
    so_lines.append(f"{indent}public class SpawnEntry")
    so_lines.append(f"{indent}{{")
    so_lines.append(f"{indent}    [Tooltip(\"Prefab name to spawn\")]")
    so_lines.append(f"{indent}    public string prefabName;")
    so_lines.append(f"{indent}    [Tooltip(\"Number of enemies to spawn\")]")
    so_lines.append(f"{indent}    public int count = 1;")
    so_lines.append(f"{indent}    [Tooltip(\"Delay between each spawn in seconds\")]")
    so_lines.append(f"{indent}    public float spawnDelay = 0.5f;")
    so_lines.append(f"{indent}    [Tooltip(\"Difficulty multiplier applied by AI director\")]")
    so_lines.append(f"{indent}    public float difficultyMultiplier = 1.0f;")
    so_lines.append(f"{indent}}}")
    so_lines.append("")

    so_lines.append(f"{indent}/// <summary>")
    so_lines.append(f"{indent}/// ScriptableObject defining a single wave in an encounter.")
    so_lines.append(f"{indent}/// Contains spawn entries, victory/defeat conditions, and timing.")
    so_lines.append(f"{indent}/// </summary>")
    so_lines.append(f'{indent}[CreateAssetMenu(menuName = "VeilBreakers/Encounter/Wave Definition")]')
    so_lines.append(f"{indent}public class VB_WaveDefinition : ScriptableObject")
    so_lines.append(f"{indent}{{")
    so_lines.append(f"{indent}    [Header(\"Spawn Configuration\")]")
    so_lines.append(f"{indent}    public SpawnEntry[] entries;")
    so_lines.append("")
    so_lines.append(f"{indent}    [Header(\"Timing\")]")
    so_lines.append(f"{indent}    [Tooltip(\"Delay before this wave starts after previous wave completes\")]")
    so_lines.append(f"{indent}    public float waveDelay = 2.0f;")
    so_lines.append("")
    so_lines.append(f"{indent}    [Header(\"Victory Conditions\")]")
    so_lines.append(f"{indent}    [Tooltip(\"Conditions: all_dead, timer, objective, boss_dead\")]")
    so_lines.append(f"{indent}    public string[] victoryConditions = new string[] {{ \"all_dead\" }};")
    so_lines.append(f"{indent}    [Tooltip(\"Time limit for timer victory condition\")]")
    so_lines.append(f"{indent}    public float timeLimit = 60f;")
    so_lines.append("")
    so_lines.append(f"{indent}    [Header(\"Defeat Conditions\")]")
    so_lines.append(f"{indent}    [Tooltip(\"Condition: player_death, time_expired, objective_failed\")]")
    so_lines.append(f"{indent}    public string defeatCondition = \"player_death\";")
    so_lines.append(f"{indent}}}")

    if ns_close:
        so_lines.append(ns_close)

    wave_so_cs = "\n".join(so_lines)

    # ---- Encounter Manager MonoBehaviour ----
    mgr_lines: list[str] = []
    mgr_lines.append("using UnityEngine;")
    mgr_lines.append("using UnityEngine.Events;")
    mgr_lines.append("using System.Collections;")
    mgr_lines.append("using System.Collections.Generic;")
    mgr_lines.append("")
    if ns_open:
        mgr_lines.append(ns_open)

    mgr_lines.append(f"{indent}/// <summary>")
    mgr_lines.append(f"{indent}/// Encounter manager for {safe_str}.")
    mgr_lines.append(f"{indent}/// Manages wave-based encounter spawning with trigger volumes,")
    mgr_lines.append(f"{indent}/// victory/defeat conditions, fog gates, and AI director integration.")
    mgr_lines.append(f"{indent}/// Generated by VeilBreakers MCP toolkit.")
    mgr_lines.append(f"{indent}/// </summary>")
    mgr_lines.append(f"{indent}public class VB_EncounterManager_{safe_name} : MonoBehaviour")
    mgr_lines.append(f"{indent}{{")

    # Fields
    mgr_lines.append(f"{indent}    [Header(\"Wave Configuration\")]")
    mgr_lines.append(f"{indent}    [SerializeField] private List<VB_WaveDefinition> _waves;")
    mgr_lines.append("")
    mgr_lines.append(f"{indent}    [Header(\"Fog Gates\")]")
    mgr_lines.append(f"{indent}    [SerializeField] private GameObject[] _fogGates;")
    mgr_lines.append("")
    mgr_lines.append(f"{indent}    [Header(\"Events\")]")
    mgr_lines.append(f"{indent}    public UnityEvent OnEncounterStarted;")
    mgr_lines.append(f"{indent}    public UnityEvent<int> OnWaveStarted;")
    mgr_lines.append(f"{indent}    public UnityEvent OnWaveComplete;")
    mgr_lines.append(f"{indent}    public UnityEvent OnEncounterVictory;")
    mgr_lines.append(f"{indent}    public UnityEvent OnEncounterDefeat;")
    mgr_lines.append("")
    mgr_lines.append(f"{indent}    // Runtime state")
    mgr_lines.append(f"{indent}    private int _currentWaveIndex;")
    mgr_lines.append(f"{indent}    private int _spawnedCount;")
    mgr_lines.append(f"{indent}    private int _defeatedCount;")
    mgr_lines.append(f"{indent}    private float _waveElapsedTime;")
    mgr_lines.append(f"{indent}    private bool _encounterActive;")
    mgr_lines.append(f"{indent}    private bool _encounterComplete;")
    mgr_lines.append(f"{indent}    private float _difficultyModifier = 1.0f;")
    mgr_lines.append(f"{indent}    private Coroutine _encounterCoroutine;")
    mgr_lines.append("")

    # OnTriggerEnter
    mgr_lines.append(f"{indent}    private void OnTriggerEnter(Collider other)")
    mgr_lines.append(f"{indent}    {{")
    mgr_lines.append(f'{indent}        if (!_encounterActive && !_encounterComplete && other.CompareTag("Player"))')
    mgr_lines.append(f"{indent}        {{")
    mgr_lines.append(f"{indent}            StartEncounter();")
    mgr_lines.append(f"{indent}        }}")
    mgr_lines.append(f"{indent}    }}")
    mgr_lines.append("")

    # StartEncounter
    mgr_lines.append(f"{indent}    /// <summary>Start the encounter sequence.</summary>")
    mgr_lines.append(f"{indent}    public void StartEncounter()")
    mgr_lines.append(f"{indent}    {{")
    mgr_lines.append(f"{indent}        if (_encounterActive || _encounterComplete) return;")
    mgr_lines.append(f"{indent}        _encounterActive = true;")
    mgr_lines.append(f"{indent}        _currentWaveIndex = 0;")
    mgr_lines.append(f"{indent}        _spawnedCount = 0;")
    mgr_lines.append(f"{indent}        _defeatedCount = 0;")
    mgr_lines.append("")
    mgr_lines.append(f"{indent}        // Enable fog gates")
    mgr_lines.append(f"{indent}        if (_fogGates != null)")
    mgr_lines.append(f"{indent}        {{")
    mgr_lines.append(f"{indent}            foreach (var gate in _fogGates)")
    mgr_lines.append(f"{indent}                if (gate != null) gate.SetActive(true);")
    mgr_lines.append(f"{indent}        }}")
    mgr_lines.append("")
    mgr_lines.append(f"{indent}        OnEncounterStarted?.Invoke();")
    mgr_lines.append(f"{indent}        _encounterCoroutine = StartCoroutine(RunEncounter());")
    mgr_lines.append(f"{indent}    }}")
    mgr_lines.append("")

    # RunEncounter coroutine
    mgr_lines.append(f"{indent}    private IEnumerator RunEncounter()")
    mgr_lines.append(f"{indent}    {{")
    mgr_lines.append(f"{indent}        for (int w = 0; w < _waves.Count; w++)")
    mgr_lines.append(f"{indent}        {{")
    mgr_lines.append(f"{indent}            _currentWaveIndex = w;")
    mgr_lines.append(f"{indent}            var wave = _waves[w];")
    mgr_lines.append("")
    mgr_lines.append(f"{indent}            // Wait for wave delay")
    mgr_lines.append(f"{indent}            if (wave.waveDelay > 0)")
    mgr_lines.append(f"{indent}                yield return new WaitForSeconds(wave.waveDelay);")
    mgr_lines.append("")
    mgr_lines.append(f"{indent}            _spawnedCount = 0;")
    mgr_lines.append(f"{indent}            _defeatedCount = 0;")
    mgr_lines.append(f"{indent}            _waveElapsedTime = 0f;")
    mgr_lines.append(f"{indent}            OnWaveStarted?.Invoke(w);")
    mgr_lines.append("")
    mgr_lines.append(f"{indent}            // Spawn enemies for this wave")
    mgr_lines.append(f"{indent}            if (wave.entries != null)")
    mgr_lines.append(f"{indent}            {{")
    mgr_lines.append(f"{indent}                foreach (var entry in wave.entries)")
    mgr_lines.append(f"{indent}                {{")
    mgr_lines.append(f"{indent}                    int adjustedCount = Mathf.RoundToInt(entry.count * entry.difficultyMultiplier * _difficultyModifier);")
    mgr_lines.append(f"{indent}                    for (int i = 0; i < adjustedCount; i++)")
    mgr_lines.append(f"{indent}                    {{")
    mgr_lines.append(f"{indent}                        SpawnEnemy(entry.prefabName);")
    mgr_lines.append(f"{indent}                        _spawnedCount++;")
    mgr_lines.append(f"{indent}                        if (entry.spawnDelay > 0)")
    mgr_lines.append(f"{indent}                            yield return new WaitForSeconds(entry.spawnDelay);")
    mgr_lines.append(f"{indent}                    }}")
    mgr_lines.append(f"{indent}                }}")
    mgr_lines.append(f"{indent}            }}")
    mgr_lines.append("")
    mgr_lines.append(f"{indent}            // Wait for victory condition")
    mgr_lines.append(f"{indent}            bool waveComplete = false;")
    mgr_lines.append(f"{indent}            while (!waveComplete)")
    mgr_lines.append(f"{indent}            {{")
    mgr_lines.append(f"{indent}                _waveElapsedTime += Time.deltaTime;")
    mgr_lines.append("")
    mgr_lines.append(f"{indent}                // Check defeat")
    mgr_lines.append(f'{indent}                if (wave.defeatCondition == "time_expired" && _waveElapsedTime >= wave.timeLimit)')
    mgr_lines.append(f"{indent}                {{")
    mgr_lines.append(f"{indent}                    HandleDefeat();")
    mgr_lines.append(f"{indent}                    yield break;")
    mgr_lines.append(f"{indent}                }}")
    mgr_lines.append("")
    mgr_lines.append(f"{indent}                // Check victory conditions")
    mgr_lines.append(f"{indent}                if (wave.victoryConditions != null && wave.victoryConditions.Length > 0)")
    mgr_lines.append(f"{indent}                {{")
    mgr_lines.append(f"{indent}                    foreach (var condition in wave.victoryConditions)")
    mgr_lines.append(f"{indent}                    {{")
    mgr_lines.append(f'{indent}                        if (condition == "all_dead" && _defeatedCount >= _spawnedCount && _spawnedCount > 0)')
    mgr_lines.append(f"{indent}                            waveComplete = true;")
    mgr_lines.append(f'{indent}                        else if (condition == "timer" && _waveElapsedTime >= wave.timeLimit)')
    mgr_lines.append(f"{indent}                            waveComplete = true;")
    mgr_lines.append(f'{indent}                        else if (condition == "boss_dead" && _defeatedCount >= _spawnedCount && _spawnedCount > 0)')
    mgr_lines.append(f"{indent}                            waveComplete = true;")
    mgr_lines.append(f"{indent}                    }}")
    mgr_lines.append(f"{indent}                }}")
    mgr_lines.append(f"{indent}                else")
    mgr_lines.append(f"{indent}                {{")
    mgr_lines.append(f"{indent}                    // Default: wave complete when all spawned enemies are dead")
    mgr_lines.append(f"{indent}                    if (_defeatedCount >= _spawnedCount && _spawnedCount > 0)")
    mgr_lines.append(f"{indent}                        waveComplete = true;")
    mgr_lines.append(f"{indent}                }}")
    mgr_lines.append("")
    mgr_lines.append(f"{indent}                yield return null;")
    mgr_lines.append(f"{indent}            }}")
    mgr_lines.append("")
    mgr_lines.append(f"{indent}            OnWaveComplete?.Invoke();")
    mgr_lines.append(f"{indent}        }}")
    mgr_lines.append("")
    mgr_lines.append(f"{indent}        // All waves complete = victory")
    mgr_lines.append(f"{indent}        HandleVictory();")
    mgr_lines.append(f"{indent}    }}")
    mgr_lines.append("")

    # SpawnEnemy
    mgr_lines.append(f"{indent}    private void SpawnEnemy(string prefabName)")
    mgr_lines.append(f"{indent}    {{")
    mgr_lines.append(f"{indent}        var prefab = Resources.Load<GameObject>(prefabName);")
    mgr_lines.append(f"{indent}        if (prefab == null)")
    mgr_lines.append(f"{indent}        {{")
    mgr_lines.append(f'{indent}            Debug.LogWarning($"Encounter: Could not load prefab \'{{prefabName}}\'");')
    mgr_lines.append(f"{indent}            return;")
    mgr_lines.append(f"{indent}        }}")
    mgr_lines.append(f"{indent}        Vector3 spawnPos = transform.position + Random.insideUnitSphere * 5f;")
    mgr_lines.append(f"{indent}        spawnPos.y = transform.position.y;")
    mgr_lines.append(f"{indent}        Instantiate(prefab, spawnPos, Quaternion.identity);")
    mgr_lines.append(f"{indent}    }}")
    mgr_lines.append("")

    # OnEnemyDefeated
    mgr_lines.append(f"{indent}    /// <summary>Call when an enemy in this encounter is defeated.</summary>")
    mgr_lines.append(f"{indent}    public void OnEnemyDefeated()")
    mgr_lines.append(f"{indent}    {{")
    mgr_lines.append(f"{indent}        _defeatedCount++;")
    mgr_lines.append(f"{indent}    }}")
    mgr_lines.append("")

    # HandleVictory / HandleDefeat
    mgr_lines.append(f"{indent}    private void HandleVictory()")
    mgr_lines.append(f"{indent}    {{")
    mgr_lines.append(f"{indent}        _encounterActive = false;")
    mgr_lines.append(f"{indent}        _encounterComplete = true;")
    mgr_lines.append(f"{indent}        DisableFogGates();")
    mgr_lines.append(f"{indent}        OnEncounterVictory?.Invoke();")
    mgr_lines.append(f"{indent}    }}")
    mgr_lines.append("")
    mgr_lines.append(f"{indent}    private void HandleDefeat()")
    mgr_lines.append(f"{indent}    {{")
    mgr_lines.append(f"{indent}        if (!_encounterActive) return;")
    mgr_lines.append(f"{indent}        _encounterActive = false;")
    mgr_lines.append(f"{indent}        StopAllCoroutines();")
    mgr_lines.append(f"{indent}        _encounterCoroutine = null;")
    mgr_lines.append(f"{indent}        OnEncounterDefeat?.Invoke();")
    mgr_lines.append(f"{indent}    }}")
    mgr_lines.append("")

    # DisableFogGates
    mgr_lines.append(f"{indent}    private void DisableFogGates()")
    mgr_lines.append(f"{indent}    {{")
    mgr_lines.append(f"{indent}        if (_fogGates != null)")
    mgr_lines.append(f"{indent}        {{")
    mgr_lines.append(f"{indent}            foreach (var gate in _fogGates)")
    mgr_lines.append(f"{indent}                if (gate != null) gate.SetActive(false);")
    mgr_lines.append(f"{indent}        }}")
    mgr_lines.append(f"{indent}    }}")
    mgr_lines.append("")

    # SetDifficultyModifier
    mgr_lines.append(f"{indent}    /// <summary>Set difficulty modifier from AI director.</summary>")
    mgr_lines.append(f"{indent}    public void SetDifficultyModifier(float modifier)")
    mgr_lines.append(f"{indent}    {{")
    mgr_lines.append(f"{indent}        _difficultyModifier = Mathf.Max(0.1f, modifier);")
    mgr_lines.append(f"{indent}    }}")
    mgr_lines.append("")

    # ResetEncounter
    mgr_lines.append(f"{indent}    /// <summary>Reset encounter to allow replay.</summary>")
    mgr_lines.append(f"{indent}    public void ResetEncounter()")
    mgr_lines.append(f"{indent}    {{")
    mgr_lines.append(f"{indent}        if (_encounterCoroutine != null)")
    mgr_lines.append(f"{indent}            StopCoroutine(_encounterCoroutine);")
    mgr_lines.append(f"{indent}        _encounterActive = false;")
    mgr_lines.append(f"{indent}        _encounterComplete = false;")
    mgr_lines.append(f"{indent}        _currentWaveIndex = 0;")
    mgr_lines.append(f"{indent}        _spawnedCount = 0;")
    mgr_lines.append(f"{indent}        _defeatedCount = 0;")
    mgr_lines.append(f"{indent}        _waveElapsedTime = 0f;")
    mgr_lines.append(f"{indent}        DisableFogGates();")
    mgr_lines.append(f"{indent}    }}")
    mgr_lines.append("")

    # ForceEndEncounter
    mgr_lines.append(f"{indent}    /// <summary>Force-end the encounter immediately.</summary>")
    mgr_lines.append(f"{indent}    public void ForceEndEncounter()")
    mgr_lines.append(f"{indent}    {{")
    mgr_lines.append(f"{indent}        if (_encounterCoroutine != null)")
    mgr_lines.append(f"{indent}            StopCoroutine(_encounterCoroutine);")
    mgr_lines.append(f"{indent}        _encounterActive = false;")
    mgr_lines.append(f"{indent}        _encounterComplete = true;")
    mgr_lines.append(f"{indent}        DisableFogGates();")
    mgr_lines.append(f"{indent}    }}")

    mgr_lines.append(f"{indent}}}")
    if ns_close:
        mgr_lines.append(ns_close)

    manager_cs = "\n".join(mgr_lines)

    return (wave_so_cs, manager_cs)


# ---------------------------------------------------------------------------
# AID-02: AI Director (dynamic difficulty adjustment)
# ---------------------------------------------------------------------------


def generate_ai_director_script(
    name: str = "AIDirector",
    namespace: str = "",
) -> str:
    """Generate AI director MonoBehaviour with AnimationCurve-driven difficulty.

    Tracks player performance (deaths, clear time, damage ratio) across
    encounters and adjusts difficulty score (0.0-1.0) using lerped smoothing.
    Difficulty score is evaluated against AnimationCurve fields for spawn
    rate and enemy stat multipliers.

    Args:
        name: Director name (sanitized for C# identifier).
        namespace: Optional C# namespace.

    Returns:
        Complete C# source string.
    """
    safe_name = sanitize_cs_identifier(name)
    safe_str = sanitize_cs_string(name)

    ns = _safe_namespace(namespace) if namespace else ""
    ns_open = f"namespace {ns}\n{{" if ns else ""
    ns_close = "}" if ns else ""
    indent = "    " if ns else ""

    lines: list[str] = []
    lines.append("using UnityEngine;")
    lines.append("using System.Collections.Generic;")
    lines.append("")
    if ns_open:
        lines.append(ns_open)

    lines.append(f"{indent}/// <summary>")
    lines.append(f"{indent}/// AI Director for {safe_str}.")
    lines.append(f"{indent}/// Tracks player performance and dynamically adjusts difficulty")
    lines.append(f"{indent}/// using AnimationCurve-driven spawn rate and stat multipliers.")
    lines.append(f"{indent}/// Generated by VeilBreakers MCP toolkit.")
    lines.append(f"{indent}/// </summary>")
    lines.append(f"{indent}public class VB_AIDirector_{safe_name} : MonoBehaviour")
    lines.append(f"{indent}{{")

    # Difficulty fields
    lines.append(f"{indent}    [Header(\"Difficulty Configuration\")]")
    lines.append(f"{indent}    [SerializeField] private AnimationCurve _spawnRateCurve = AnimationCurve.Linear(0f, 0.5f, 1f, 2.0f);")
    lines.append(f"{indent}    [SerializeField] private AnimationCurve _enemyStatCurve = AnimationCurve.Linear(0f, 0.7f, 1f, 1.5f);")
    lines.append(f"{indent}    [SerializeField] private float _adjustmentSpeed = 0.1f;")
    lines.append("")

    # Performance tracking
    lines.append(f"{indent}    [Header(\"Performance Tracking\")]")
    lines.append(f"{indent}    [SerializeField, Range(0f, 1f)] private float _difficultyScore = 0.5f;")
    lines.append(f"{indent}    private float _targetDifficulty = 0.5f;")
    lines.append(f"{indent}    private int _recentDeaths;")
    lines.append(f"{indent}    private float _averageClearTime;")
    lines.append(f"{indent}    private float _damageTakenRatio;")
    lines.append("")

    # Sliding window
    lines.append(f"{indent}    // Sliding window for moving averages (last 5 encounters)")
    lines.append(f"{indent}    private List<float> _clearTimes = new List<float>();")
    lines.append(f"{indent}    private List<int> _deathCounts = new List<int>();")
    lines.append(f"{indent}    private List<float> _damageRatios = new List<float>();")
    lines.append(f"{indent}    private const int _windowSize = 5;")
    lines.append("")

    # Per-encounter tracking
    lines.append(f"{indent}    // Per-encounter tracking")
    lines.append(f"{indent}    private int _encounterKills;")
    lines.append(f"{indent}    private int _encounterDeaths;")
    lines.append(f"{indent}    private float _encounterDamageDealt;")
    lines.append(f"{indent}    private float _encounterDamageTaken;")
    lines.append(f"{indent}    private float _encounterTimeElapsed;")
    lines.append("")

    # Properties
    lines.append(f"{indent}    /// <summary>Current difficulty score (0.0 to 1.0).</summary>")
    lines.append(f"{indent}    public float DifficultyScore => _difficultyScore;")
    lines.append("")

    # Update
    lines.append(f"{indent}    private void Update()")
    lines.append(f"{indent}    {{")
    lines.append(f"{indent}        UpdateDifficulty();")
    lines.append(f"{indent}    }}")
    lines.append("")

    # UpdateDifficulty
    lines.append(f"{indent}    /// <summary>Lerp difficulty toward target. Called each frame.</summary>")
    lines.append(f"{indent}    public void UpdateDifficulty()")
    lines.append(f"{indent}    {{")
    lines.append(f"{indent}        _difficultyScore = Mathf.Lerp(_difficultyScore, _targetDifficulty, _adjustmentSpeed * Time.deltaTime);")
    lines.append(f"{indent}        _difficultyScore = Mathf.Clamp01(_difficultyScore);")
    lines.append(f"{indent}    }}")
    lines.append("")

    # GetSpawnRateMultiplier
    lines.append(f"{indent}    /// <summary>Evaluate spawn rate multiplier from difficulty curve.</summary>")
    lines.append(f"{indent}    public float GetSpawnRateMultiplier()")
    lines.append(f"{indent}    {{")
    lines.append(f"{indent}        return _spawnRateCurve.Evaluate(_difficultyScore);")
    lines.append(f"{indent}    }}")
    lines.append("")

    # GetStatMultiplier
    lines.append(f"{indent}    /// <summary>Evaluate enemy stat multiplier from difficulty curve.</summary>")
    lines.append(f"{indent}    public float GetStatMultiplier()")
    lines.append(f"{indent}    {{")
    lines.append(f"{indent}        return _enemyStatCurve.Evaluate(_difficultyScore);")
    lines.append(f"{indent}    }}")
    lines.append("")

    # RecordEncounterResult
    lines.append(f"{indent}    /// <summary>Record the result of a completed encounter.</summary>")
    lines.append(f"{indent}    public void RecordEncounterResult(float clearTime, int deaths, float damageRatio)")
    lines.append(f"{indent}    {{")
    lines.append(f"{indent}        // Update sliding windows")
    lines.append(f"{indent}        _clearTimes.Add(clearTime);")
    lines.append(f"{indent}        if (_clearTimes.Count > _windowSize) _clearTimes.RemoveAt(0);")
    lines.append("")
    lines.append(f"{indent}        _deathCounts.Add(deaths);")
    lines.append(f"{indent}        if (_deathCounts.Count > _windowSize) _deathCounts.RemoveAt(0);")
    lines.append("")
    lines.append(f"{indent}        _damageRatios.Add(damageRatio);")
    lines.append(f"{indent}        if (_damageRatios.Count > _windowSize) _damageRatios.RemoveAt(0);")
    lines.append("")
    lines.append(f"{indent}        // Calculate moving averages")
    lines.append(f"{indent}        _averageClearTime = 0f;")
    lines.append(f"{indent}        foreach (var t in _clearTimes) _averageClearTime += t;")
    lines.append(f"{indent}        _averageClearTime /= _clearTimes.Count;")
    lines.append("")
    lines.append(f"{indent}        _recentDeaths = 0;")
    lines.append(f"{indent}        foreach (var d in _deathCounts) _recentDeaths += d;")
    lines.append("")
    lines.append(f"{indent}        _damageTakenRatio = 0f;")
    lines.append(f"{indent}        foreach (var r in _damageRatios) _damageTakenRatio += r;")
    lines.append(f"{indent}        _damageTakenRatio /= _damageRatios.Count;")
    lines.append("")
    lines.append(f"{indent}        // Calculate target difficulty")
    lines.append(f"{indent}        // Decrease on deaths, increase on fast clears and low damage")
    lines.append(f"{indent}        float deathPenalty = _recentDeaths * 0.05f;")
    lines.append(f"{indent}        float speedBonus = (_averageClearTime < 30f) ? 0.1f : (_averageClearTime < 60f) ? 0.05f : 0f;")
    lines.append(f"{indent}        float damageBonus = (1f - _damageTakenRatio) * 0.1f;")
    lines.append("")
    lines.append(f"{indent}        _targetDifficulty = Mathf.Clamp01(0.5f - deathPenalty + speedBonus + damageBonus);")
    lines.append(f"{indent}    }}")
    lines.append("")

    # Integration: push difficulty to EncounterManagers
    lines.append(f"{indent}    /// <summary>Push current difficulty modifier to all encounter managers in scene.</summary>")
    lines.append(f"{indent}    public void ApplyDifficultyToEncounters()")
    lines.append(f"{indent}    {{")
    lines.append(f"{indent}        float modifier = GetSpawnRateMultiplier();")
    lines.append(f"{indent}        var managers = FindObjectsByType<MonoBehaviour>(FindObjectsSortMode.None);")
    lines.append(f"{indent}        foreach (var mgr in managers)")
    lines.append(f"{indent}        {{")
    lines.append(f'{indent}            var method = mgr.GetType().GetMethod("SetDifficultyModifier");')
    lines.append(f"{indent}            if (method != null)")
    lines.append(f"{indent}                method.Invoke(mgr, new object[] {{ modifier }});")
    lines.append(f"{indent}        }}")
    lines.append(f"{indent}    }}")

    lines.append(f"{indent}}}")
    if ns_close:
        lines.append(ns_close)

    return "\n".join(lines)


